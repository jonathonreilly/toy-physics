PASS_COUNT = 0
FAIL_COUNT = 0


def check(name: str, condition: bool, detail: str = "", kind: str = "EXACT") -> bool:
    global PASS_COUNT, FAIL_COUNT
    status = "PASS" if condition else "FAIL"
    if condition:
        PASS_COUNT += 1
    else:
        FAIL_COUNT += 1
    tag = f" [{kind}]" if kind != "EXACT" else ""
    msg = f"  [{status}]{tag} {name}"
    if detail:
        msg += f"  ({detail})"
    print(msg)
    return condition


def matrix_unit(i: int, j: int) -> sp.Matrix:
    M = sp.zeros(3)
    M[i, j] = 1
    return M


def build_cycle_objects():
    C = sp.Matrix([[0, 0, 1], [1, 0, 0], [0, 1, 0]])
    Cd = C.T
    I3 = sp.eye(3)
    X = C + Cd
    Y = sp.I * (C - Cd)
    return I3, C, Cd, X, Y


def part1_c3_covariant_hermitian_target():
    print("=" * 88)
    print("PART 1: the C_3-covariant Hermitian target is 3-real-dimensional")
    print("=" * 88)

    I3, C, Cd, X, Y = build_cycle_objects()

    d1, d2, d3 = sp.symbols("d1 d2 d3", real=True)
    x12, x13, x23 = sp.symbols("x12 x13 x23", real=True)
    y12, y13, y23 = sp.symbols("y12 y13 y23", real=True)
    unknowns = [d1, d2, d3, x12, x13, x23, y12, y13, y23]
    H = sp.Matrix(
        [
            [d1, x12 + sp.I * y12, x13 + sp.I * y13],
            [x12 - sp.I * y12, d2, x23 + sp.I * y23],
            [x13 - sp.I * y13, x23 - sp.I * y23, d3],
        ]
    )
    eqs = list((H * C - C * H).reshape(9, 1))
    A, _ = sp.linear_eq_to_matrix(eqs, unknowns)
    dim_comm = len(unknowns) - A.rank()

    flat_basis = sp.Matrix.hstack(*(M.reshape(9, 1) for M in [I3, X, Y]))
    rank_basis = flat_basis.rank()

    a, x, y = sp.symbols("a x y", real=True)
    H_cyc = sp.simplify(a * I3 + x * X + y * Y)

    check(
        "Herm(3) has 9 real dimensions",
        len(unknowns) == 9,
    )
    check(
        "The Hermitian commutant of the retained 3-cycle has real dimension 3",
        dim_comm == 3,
        detail=f"rank={A.rank()}",
    )
    check(
        "I, C + C^2, and i(C - C^2) are real-linearly independent",
        rank_basis == 3,
    )
    check(
        "The displayed 3-generator family is Hermitian",
        sp.simplify(H_cyc - H_cyc.H) == sp.zeros(3),
    )
    check(
        "The displayed 3-generator family commutes with the retained 3-cycle",
        sp.simplify(H_cyc * C - C * H_cyc) == sp.zeros(3),
    )


