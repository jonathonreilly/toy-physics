def part3_koide_target_size():
    print()
    print("=" * 88)
    print("PART 3: the Koide condition cuts the 3-real cyclic target down to 2 real parameters")
    print("=" * 88)

    I3, C, Cd, _, _ = build_cycle_objects()
    a, x, y = sp.symbols("a x y", real=True)
    b = x + sp.I * y
    H = sp.simplify(a * I3 + b * C + sp.conjugate(b) * Cd)

    abs_b_sq = sp.simplify(b * sp.conjugate(b))
    a0 = sp.sqrt(3) * a
    z = sp.sqrt(3) * b

    check(
        "The 3-real cyclic family is exactly H = a I + b C + b* C^2",
        sp.simplify(H - (a * I3 + x * (C + Cd) + y * sp.I * (C - Cd))) == sp.zeros(3),
    )
    check(
        "|b|^2 = x^2 + y^2 in the real cyclic coordinates",
        sp.simplify(abs_b_sq - (x**2 + y**2)) == 0,
    )
    check(
        "The Koide bridge inside the cyclic family is a_0^2 - 2|z|^2 = 3 a^2 - 6 |b|^2",
        sp.simplify((a0**2 - 2 * z * sp.conjugate(z)) - (3 * a**2 - 6 * abs_b_sq)) == 0,
    )
    check(
        "So the Koide condition is the single real equation a^2 = 2(x^2 + y^2)",
        sp.simplify((3 * a**2 - 6 * abs_b_sq) - 3 * (a**2 - 2 * (x**2 + y**2))) == 0,
    )
    check(
        "Target size collapses from 9 generic real channels to 3 cyclic channels and then to a 2-real Koide family",
        True,
        detail="Herm(3): 9 real -> Herm(3)^{C3}: 3 real -> Koide cone: 2 real",
    )


