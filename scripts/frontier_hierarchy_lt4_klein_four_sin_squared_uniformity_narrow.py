def orbit_decompose(L_t: int):
    """Decompose M(L_t) into V_4 orbits. Returns list of (orbit_set, w_value)."""
    modes = [simplify(m) for m in apbc_modes(L_t)]
    visited = set()
    orbits = []
    for m in modes:
        if m in visited:
            continue
        orbit = set(simplify(x) for x in v4_action(m))
        # restrict to elements of M(L_t)
        orbit = {x for x in orbit if any(simplify(x - mm) == 0 for mm in modes)}
        visited |= orbit
        # representative weight on the orbit
        rep = next(iter(orbit))
        w_val = simplify(sin(rep) ** 2)
        orbits.append((orbit, w_val))
    return orbits


