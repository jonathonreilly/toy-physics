section("Part 8: single-orbit at L_t=4 vs split-orbit at L_t=6 (T8)")
# At L_t = 4, M(4) is a single V_4 orbit (size 4).
# At L_t = 6, M(6) decomposes into orbits {pi/6, 5pi/6, 7pi/6, 11pi/6} (w=1/4)
