def section(title: str) -> None:
    print("\n" + "-" * 88 + f"\n{title}\n" + "-" * 88)


def apbc_modes(L_t: int):
    """Return SymPy expressions for the APBC fermion Matsubara modes M(L_t)."""
    return [Rational(2 * n + 1, L_t) * pi for n in range(L_t)]


def sin_squared_values(L_t: int):
    """Return list of simplified sin^2(omega) for omega in M(L_t)."""
    return [simplify(sin(omega) ** 2) for omega in apbc_modes(L_t)]


def is_uniform(values) -> bool:
    """Check if every entry in `values` is symbolically equal to the first."""
    if not values:
        return True
    first = values[0]
    return all(sp.simplify(v - first) == 0 for v in values[1:])


def reduce_mod_2pi(x):
    """Reduce a SymPy angle expression to the canonical interval [0, 2 pi)."""
    coeff = simplify(x / pi)
    coeff_mod = coeff - floor(coeff / 2) * 2
    return simplify(coeff_mod * pi)


def v4_action(omega):
    """Return the four images { id, sigma_1, sigma_2, sigma_3 } applied to omega.

    sigma_1 : omega -> pi - omega
    sigma_2 : omega -> omega + pi (mod 2 pi)
    sigma_3 : sigma_1 sigma_2 : omega -> -omega (mod 2 pi)
    """
    return [
        reduce_mod_2pi(omega),
        reduce_mod_2pi(pi - omega),
        reduce_mod_2pi(omega + pi),
        reduce_mod_2pi(-omega),
    ]


