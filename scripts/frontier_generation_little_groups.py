def verify_little_group_numerically(sym_group, K):
    """Verify the little group by checking that H(K) is invariant under it.

    For g in the little group of K (i.e., g.K = K mod 2pi), H(K) should
    be unitarily equivalent to itself under the taste-space representation of g.
    This means the eigenvalue structure is preserved.
    """
    H_K = staggered_H_momentum(K)
    eigs_K = np.sort(np.linalg.eigvalsh(1j * H_K))

    for g in sym_group:
        gK = g @ K
        diff = (gK - K) / np.pi
        if np.allclose(diff, np.round(diff), atol=1e-10) and np.allclose(np.round(diff) % 2, 0, atol=1e-10):
            # g fixes K, so H(gK) = H(K)
            H_gK = staggered_H_momentum(gK)
            eigs_gK = np.sort(np.linalg.eigvalsh(1j * H_gK))
            if not np.allclose(eigs_K, eigs_gK, atol=1e-10):
                return False
    return True


