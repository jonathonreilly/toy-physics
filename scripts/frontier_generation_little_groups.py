# Part 0: Oh point group (48 elements) as 3x3 integer matrices
# =============================================================================

def generate_Oh():
    """Generate all 48 elements of Oh as 3x3 integer matrices.

    Oh = O x {I, -I} where O is the rotation group of the cube (24 elements).
    O is generated by C4z (90-degree rotation about z) and C3_111 (120-degree
    rotation about [111]).
    """
    # Generators of O (proper rotations)
    # C4z: (x,y,z) -> (-y,x,z)
    C4z = np.array([[0, -1, 0],
                     [1,  0, 0],
                     [0,  0, 1]], dtype=int)
    # C3_111: (x,y,z) -> (y,z,x)
    C3 = np.array([[0, 0, 1],
                    [1, 0, 0],
                    [0, 1, 0]], dtype=int)
    # Inversion
    inv = -np.eye(3, dtype=int)

    # Generate O by closure
    O_group = set()
    queue = [np.eye(3, dtype=int), C4z, C3]
    while queue:
        g = queue.pop()
        key = tuple(g.flatten())
        if key in O_group:
            continue
        O_group.add(key)
        for gen in [C4z, C3]:
            new = g @ gen
            queue.append(new)
            new2 = gen @ g
            queue.append(new2)

    O_matrices = [np.array(k, dtype=int).reshape(3, 3) for k in O_group]

    # Oh = O x {I, -I}
    Oh_matrices = []
    seen = set()
    for g in O_matrices:
        for s in [1, -1]:
            m = s * g
            key = tuple(m.flatten())
            if key not in seen:
                seen.add(key)
                Oh_matrices.append(m)

    return Oh_matrices


def classify_element(g):
    """Classify an Oh element by its trace and determinant."""
    tr = np.trace(g)
    det = int(round(np.linalg.det(g)))
    return tr, det


