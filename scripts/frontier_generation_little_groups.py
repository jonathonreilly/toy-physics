def oh_action_on_momentum(g, K):
    """Act with Oh element g on momentum K."""
    return g @ K


def oh_action_on_H(g, K):
    """Check if Oh element g is a symmetry of H at momentum K.

    g is a symmetry if there exists a unitary U(g) on the 8-dim taste space
    such that: U(g) H(K) U(g)^dag = H(g.K)

    For the staggered Hamiltonian, the symmetry is more subtle because
    the eta phases break the full Oh symmetry.
    """
    H_K = staggered_H_momentum(K)
    gK = oh_action_on_momentum(g, K)
    H_gK = staggered_H_momentum(gK)

    # Check if H(K) and H(gK) have the same spectrum
    eigs_K = np.sort(np.linalg.eigvalsh(1j * H_K))  # i*H is Hermitian
    eigs_gK = np.sort(np.linalg.eigvalsh(1j * H_gK))

    return np.allclose(eigs_K, eigs_gK, atol=1e-12)


