def little_group(symmetry_group, K):
    """Compute the little group (stabilizer) of K under the symmetry group.

    The little group at K consists of all g in G such that g.K = K (mod 2*pi).
    For BZ corners, K is at (0 or pi)^3, so g.K = K mod 2*pi iff g.K = K
    since all components are 0 or pi.
    """
    stab = []
    for g in symmetry_group:
        gK = g @ K
        # Check if gK = K mod 2*pi
        diff = (gK - K) / np.pi  # Should be even integers
        if np.allclose(diff, np.round(diff), atol=1e-10):
            if np.allclose(np.round(diff) % 2, 0, atol=1e-10):
                stab.append(g)
    return stab


def identify_group_order_and_structure(elements):
    """Identify the abstract group from its elements."""
    n = len(elements)

    # Count elements by (trace, det) = conjugacy class indicator
    class_counts = {}
    for g in elements:
        tr = int(round(np.trace(g)))
        det = int(round(np.linalg.det(g)))
        key = (tr, det)
        class_counts[key] = class_counts.get(key, 0) + 1

    return n, class_counts


def name_group(order, class_counts):
    """Heuristic naming of small point groups by order and class structure."""
    if order == 48:
        return "Oh"
    elif order == 24:
        # Could be O or Td
        if (-3, -1) in class_counts:
            return "O (or Td)"
        return "O or Td (24 elements)"
    elif order == 16:
        return "D4h (or similar, 16 elements)"
    elif order == 12:
        return "D3d or C6v (12 elements)"
    elif order == 8:
        return "D2h or C4v (8 elements)"
    elif order == 4:
        return "D2 or C2v or C4 (4 elements)"
    elif order == 2:
        return "C2 or Ci or Cs (2 elements)"
    elif order == 1:
        return "C1 (trivial)"
    else:
        return f"Unknown ({order} elements)"


