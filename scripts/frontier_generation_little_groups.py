def staggered_H_momentum(K):
    """Build the 8x8 momentum-space staggered Hamiltonian H(K).

    The staggered fermion on Z^3 has unit cell 2x2x2 (8 sites).
    Internal index alpha = (a1, a2, a3) in {0,1}^3.

    The Hamiltonian is:
      H(K) = sum_mu  i * sin(K_mu) * Gamma_mu  (naive part)

    But the staggered phases modify this. The full expression in terms of
    the internal indices is:

    For hopping in direction mu from site (n, alpha) to (n + e_mu, alpha):
      The hopping amplitude includes the KS phase eta_mu(2n + alpha).

    eta_1(n) = 1
    eta_2(n) = (-1)^{n_1}
    eta_3(n) = (-1)^{n_1 + n_2}

    In the 2x2x2 unit cell, the hopping in direction mu connects:
      - alpha to alpha (if the hop stays within the cell): factor e^{iK_mu} or 1
      - alpha to alpha +/- e_mu mod 2

    More precisely, for direction mu:
      If alpha_mu = 0: hop to alpha + e_mu (alpha_mu -> 1), within cell, amplitude = +1
      If alpha_mu = 1: hop to alpha - e_mu (alpha_mu -> 0), crosses cell boundary,
                        amplitude = e^{i * 2 * K_mu}  (factor of 2 because cell size = 2)

    The eta phase at site (2n + alpha) is:
      eta_1 = 1
      eta_2(2n+alpha) = (-1)^{2n_1 + a_1} = (-1)^{a_1}
      eta_3(2n+alpha) = (-1)^{2n_1+a_1 + 2n_2+a_2} = (-1)^{a_1+a_2}

    So the eta phase depends only on the internal index, not on n.

    The full H(K) in the 8-dimensional internal space:
      H_{alpha, beta}(K) = sum_mu  eta_mu(alpha) * t_mu(K, alpha, mu) * delta_{...}

    where t_mu connects alpha to beta = alpha XOR e_mu with appropriate phase.
    """
    # Enumerate the 8 internal sites
    alphas = [(a1, a2, a3) for a1 in range(2) for a2 in range(2) for a3 in range(2)]
    alpha_idx = {a: i for i, a in enumerate(alphas)}

    H = np.zeros((8, 8), dtype=complex)

    for a in alphas:
        i = alpha_idx[a]
        a1, a2, a3 = a

        for mu in range(3):
            # eta phase
            if mu == 0:
                eta = 1.0
            elif mu == 1:
                eta = (-1.0) ** a1
            else:  # mu == 2
                eta = (-1.0) ** (a1 + a2)

            # Target site: flip bit mu
            b = list(a)
            b[mu] = 1 - b[mu]
            b = tuple(b)
            j = alpha_idx[b]

            # Phase factor: if a[mu] == 0, hop forward within cell (factor 1)
            #               if a[mu] == 1, hop forward crosses boundary (factor e^{2iK_mu})
            # The hopping is anti-Hermitian: +eta/2 for forward, -eta/2 for backward
            # Forward hop from alpha:
            if a[mu] == 0:
                # Forward: alpha -> beta (within cell)
                phase = 1.0
            else:
                # Forward: alpha -> beta (crosses cell boundary)
                phase = np.exp(2j * K[mu])

            # The staggered Hamiltonian has anti-Hermitian hopping:
            # H[i,j] += (1/2) * eta * phase  (forward direction)
            H[i, j] += 0.5 * eta * phase

            # Backward hop: from beta to alpha
            # If b[mu] == 0: backward crosses boundary -> phase e^{-2iK_mu}
            # If b[mu] == 1: backward within cell -> phase 1
            # But b[mu] = 1 - a[mu], so:
            # If a[mu] == 0: b[mu] = 1, backward within cell, phase = 1
            # If a[mu] == 1: b[mu] = 0, backward crosses boundary, phase = e^{-2iK_mu}
            # The backward hop has -eta_mu(beta) * phase_backward

            # Actually, let me be more careful. The Hamiltonian is:
            # H = sum_{n,mu} eta_mu(n) * [ c^dag(n+e_mu) c(n) - c^dag(n) c(n+e_mu) ] / 2
            # which is anti-Hermitian by inspection (H^dag = -H for each term).
            # Wait, that's wrong. Let me use the standard convention:
            # H = sum_{n,mu} (i/2) * eta_mu(n) * [ c^dag(n+e_mu) c(n) - h.c. ]
            # No. The position-space code uses:
            # H[i,j] += t * eta / 2  (forward hop)
            # H[j,i] -= t * eta / 2  (backward hop) -- this makes it Hermitian if eta is real
            # So H[i,j] = +eta/2 * phase, H[j,i] = -eta/2 * phase^*

            # Let me redo this properly with the same convention as the existing code.

    # Let me rebuild from scratch using the convention from the existing code.
    H = np.zeros((8, 8), dtype=complex)

    for a in alphas:
        i = alpha_idx[a]
        a1, a2, a3 = a

        for mu in range(3):
            # eta phase at site alpha
            if mu == 0:
                eta = 1.0
            elif mu == 1:
                eta = (-1.0) ** a1
            else:
                eta = (-1.0) ** (a1 + a2)

            # Forward hop: alpha -> alpha XOR e_mu
            b = list(a)
            b[mu] = 1 - b[mu]
            b = tuple(b)
            j = alpha_idx[b]

            # Phase from Bloch theorem
            # If a[mu] = 0 and b[mu] = 1: hop within cell, no Bloch phase
            # If a[mu] = 1 and b[mu] = 0: hop crosses cell boundary (forward),
            #   Bloch phase = e^{i * k_cell * 1} where k_cell is the momentum
            #   conjugate to the supercell index.
            #   Since supercell has spacing 2, k_cell ranges over [-pi/2, pi/2],
            #   and the original momentum K = k_cell (in the reduced zone).
            #   But to get the BZ corners of the ORIGINAL lattice, we need to
            #   unfold: the original K relates to (k_cell, taste) where
            #   K = k_cell + pi * s / 1 for s in {0,1}^3.
            #   Actually, the reciprocal lattice of the supercell (spacing 2) has
            #   vectors pi * e_mu. So K and K + pi*e_mu are equivalent.
            #   The reduced BZ is [-pi/2, pi/2]^3.
            #   At k_cell = 0, the 8 eigenvalues correspond to K = pi*s for s in {0,1}^3.

            # For a general supercell momentum q (not K), the Bloch phase for
            # a hop crossing the cell boundary in direction mu is e^{i * q_mu * 2}
            # (since the cell has spacing 2 in lattice units).
            # But we want H(K) where K is in the ORIGINAL BZ.
            # The relation is q = K (since the physical hop is 1 lattice unit,
            # and the Bloch condition is c(n + 2*e_mu) = e^{i*q_mu*2} c(n)
            # in supercell coords, which means c(n + e_mu) picks up e^{i*K_mu}
            # where K_mu is the original BZ momentum).

            # Wait, let me think again. In the supercell picture:
            # Site n in the original lattice -> (N, alpha) where n = 2*N + alpha
            # Bloch condition: c(2*(N+E_mu) + alpha) = e^{i*q_mu} * c(2*N + alpha)
            # So for a forward hop from (N, alpha) in direction mu:
            #   target = (N, alpha') if alpha_mu = 0 (alpha' = alpha + e_mu, same supercell)
            #   target = (N + E_mu, alpha') if alpha_mu = 1 (alpha' = alpha - e_mu, next supercell)
            # In the second case, c(target) = e^{i*q_mu} * c(N, alpha')
            # So the Bloch phase is e^{i*q_mu} when alpha_mu = 1.

            # The original BZ momentum K relates to q by: the eigenvalues of H(q)
            # give energies at K = q + pi*s for all s in {0,1}^3 (the 8 tastes).
            # Actually no -- q directly IS the reduced momentum in [-pi/2, pi/2]^3,
            # and K = q when we label eigenvalues by their taste quantum number.

            # For our purposes: we want H(q) for q in the reduced BZ.
            # All 8 BZ corners of the original lattice fold to q = 0.
            # So we evaluate H(q=0) and get an 8x8 matrix.
            # The 8 eigenvalues give the energies at the 8 BZ corners.

            if a[mu] == 1:
                phase = np.exp(1j * K[mu])
            else:
                phase = 1.0

            # Convention from existing code:
            # H[i,j] += t * eta * 0.5   (forward: i->j, i.e., c^dag_j c_i)
            # H[j,i] -= t * eta * 0.5   (backward, making it anti-Hermitian in hopping)
            # Wait, re-reading the code:
            #   H[i, j] += t[0] * 0.5
            #   H[j, i] -= t[0] * 0.5
            # This means H[i,j] = +t/2 and H[j,i] = -t/2, so H is anti-Hermitian.
            # That's the correct form for the staggered Dirac operator (not Hermitian).

            # For the momentum-space version:
            H[i, j] += 0.5 * eta * phase
            H[j, i] -= 0.5 * eta * np.conj(phase)

    return H


def staggered_H_direct(K):
    """Build H(K) directly using the Kawamoto-Smit gamma matrices.

    In momentum space at the BZ corners, the staggered Hamiltonian
    factorizes into taste x momentum. At a BZ corner K = pi * s
    for s in {0,1}^3, the Hamiltonian on the 8-dim taste space is:

    H(K) = sum_mu  i * sin(K_mu) * Gamma_mu_KS

    where Gamma_mu_KS are the Kawamoto-Smit gamma matrices.

    Actually this is the naive/free-field form. Let me verify by
    direct computation.
    """
    I2 = np.eye(2, dtype=complex)
    sx = np.array([[0, 1], [1, 0]], dtype=complex)
    sy = np.array([[0, -1j], [1j, 0]], dtype=complex)
    sz = np.array([[1, 0], [0, -1]], dtype=complex)

    def kron3(A, B, C):
        return np.kron(A, np.kron(B, C))

    # Kawamoto-Smit gamma matrices
    G1 = kron3(sx, I2, I2)
    G2 = kron3(sz, sx, I2)
    G3 = kron3(sz, sz, sx)

    H = np.zeros((8, 8), dtype=complex)
    gammas = [G1, G2, G3]
    for mu in range(3):
        H += 1j * np.sin(K[mu]) * gammas[mu]

    return H


