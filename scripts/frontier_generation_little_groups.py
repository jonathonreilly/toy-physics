def find_staggered_symmetry_group_numerical():
    """Cross-check: find staggered symmetry group numerically.

    For each Oh element g, check if H(q) and H(g^{-1}.q) have the same
    spectrum for multiple generic q values. This is a necessary condition.
    We use enough q points to make false positives extremely unlikely.
    """
    Oh = generate_Oh()
    print(f"\nOh has {len(Oh)} elements")

    test_qs = [
        np.array([0.137, 0.259, 0.371]),
        np.array([0.743, 0.419, 0.157]),
        np.array([0.311, 0.823, 0.547]),
        np.array([1.103, 0.617, 0.929]),
        np.array([0.051, 0.983, 0.207]),
    ]

    symmetries = []

    for g in Oh:
        ginv = np.round(np.linalg.inv(g)).astype(int)
        is_sym = True

        for q in test_qs:
            H_q = staggered_H_momentum(q)
            H_gq = staggered_H_momentum(ginv @ q)

            eigs_q = np.sort(np.linalg.eigvalsh(1j * H_q))
            eigs_gq = np.sort(np.linalg.eigvalsh(1j * H_gq))

            if not np.allclose(eigs_q, eigs_gq, atol=1e-10):
                is_sym = False
                break

        if is_sym:
            symmetries.append(g)

    return symmetries


def find_staggered_symmetry_group_exact():
    """Determine staggered symmetry group by exact analysis of the eta phases.

    The staggered eta phases are:
      eta_1(n) = 1
      eta_2(n) = (-1)^{n_1}
      eta_3(n) = (-1)^{n_1+n_2}

    A point-group operation g: n -> g.n is a symmetry of the staggered
    Hamiltonian if it maps the eta phase structure to itself, i.e., for
    each pair of nearest neighbors (n, n+e_mu), the operation g maps this
    to another nearest-neighbor pair (g.n, g.n + e_nu) = (g.n, g.(n+e_mu))
    with the SAME eta phase.

    Equivalently, g is a symmetry iff: for all mu,
      eta_mu(n) = eta_{g^{-1}mu}(g^{-1}.n)  ... approximately.

    Actually, the condition is more nuanced because g permutes the
    directions. Let g be a 3x3 matrix. Direction mu maps to direction
    g.mu. The eta phase in direction mu at site n is eta_mu(n).
    After applying g, direction mu becomes direction nu where g * e_mu = +/- e_nu.
    The condition is:

      eta_mu(n) = eta_nu(g.n) * (sign from g)

    for all n and all mu.

    The "sign from g" accounts for the fact that g might reverse a direction
    (e.g., reflection), which conjugates the hopping.

    For the staggered Hamiltonian, the hopping in direction mu is:
      H += eta_mu(n) * [c^dag(n+e_mu) c(n) - c^dag(n) c(n+e_mu)] / 2

    Under g: n -> g.n, c(n) -> c(g.n), e_mu -> g.e_mu

    So the term becomes:
      eta_mu(g^{-1}.m) * [c^dag(m + g.e_mu) c(m) - h.c.] / 2

    For this to equal the original Hamiltonian, we need:
      For each mu, writing g.e_mu = +/- e_nu (with possible sign),
      eta_mu(g^{-1}.m) = +/- eta_nu(m)  for all m.

    Since the lattice is infinite and n ranges over all of Z^3, the
    condition is that eta_mu(g^{-1}.n) = eta_{perm(mu)}(n) up to an
    overall sign (which can be absorbed into the taste transformation).

    Actually the sign matters for the Hermiticity structure. If g maps
    direction mu to -e_nu, then the hopping direction is reversed, which
    flips the sign of the anti-symmetric part. Combined with possible sign
    of eta, the condition is:

      eta_mu(g^{-1}.n) * sign(g.e_mu)_nu = eta_{|g.e_mu|}(n)  for all n

    where sign and |.| extract the direction and sign of g.e_mu.

    This is getting complex. Let me just enumerate.

    For eta_1(n) = 1: constant, no constraint from mu=1.
    For eta_2(n) = (-1)^{n_1}: depends on n_1 mod 2.
    For eta_3(n) = (-1)^{n_1+n_2}: depends on (n_1+n_2) mod 2.

    I'll check each Oh element directly.
    """
    Oh = generate_Oh()

    # The eta phases as functions of n (mod 2):
    # eta_mu(n) for mu=0,1,2:
    #   mu=0: eta(n) = 1  (independent of n)
    #   mu=1: eta(n) = (-1)^{n[0]}
    #   mu=2: eta(n) = (-1)^{n[0]+n[1]}

    def eta(mu, n):
        """Staggered phase for direction mu at site n."""
        if mu == 0:
            return 1
        elif mu == 1:
            return (-1) ** (n[0] % 2)
        else:
            return (-1) ** ((n[0] + n[1]) % 2)

    symmetries = []
    symmetry_info = []

    for g in Oh:
        ginv = np.round(np.linalg.inv(g)).astype(int)

        is_sym = True

        # For each direction mu, determine what g does to it
        for mu in range(3):
            e_mu = np.zeros(3, dtype=int)
            e_mu[mu] = 1

            g_emu = g @ e_mu  # Where does direction mu go?

            # Find nu and sign such that g.e_mu = sign * e_nu
            nu = -1
            sign = 0
            for k in range(3):
                if abs(g_emu[k]) == 1 and all(g_emu[j] == 0 for j in range(3) if j != k):
                    nu = k
                    sign = g_emu[k]
                    break

            if nu == -1:
                # g.e_mu is not along a coordinate axis -- not possible for Oh
                is_sym = False
                break

            # Check: eta_mu(g^{-1}.n) must equal eta_nu(n) for all n
            # (The sign from direction reversal is handled separately -- it
            # affects the hopping sign but the anti-symmetric structure
            # absorbs it. What matters is whether the PHASE PATTERN matches.)

            # Test on all n mod 2 (only need n mod 2 since eta only depends on n mod 2)
            for n1 in range(2):
                for n2 in range(2):
                    for n3 in range(2):
                        n = np.array([n1, n2, n3], dtype=int)
                        ginv_n = ginv @ n  # Each component mod 2

                        eta_mu_ginvn = eta(mu, ginv_n)
                        eta_nu_n = eta(nu, n)

                        if eta_mu_ginvn != eta_nu_n:
                            is_sym = False
                            break
                    if not is_sym:
                        break
                if not is_sym:
                    break

            if not is_sym:
                break

        if is_sym:
            symmetries.append(g)
            tr, det = classify_element(g)
            symmetry_info.append((tr, det))

    return symmetries, symmetry_info


