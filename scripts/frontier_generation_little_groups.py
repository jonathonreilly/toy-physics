# Part 8: Irreducible representations at each corner
# =============================================================================

def irreps_at_corner(little_grp, K):
    """Analyze the representation structure at a BZ corner.

    The little group acts on the 8-dimensional taste space at K.
    We decompose this into irreps by computing characters.
    """
    H_K = staggered_H_momentum(K)
    iH = 1j * H_K
    eigs, vecs = np.linalg.eigh(iH)

    # For each eigenspace, compute the character of the little group representation
    tol = 1e-10
    unique_eigs = []
    eigenspaces = []

    for i, e in enumerate(eigs):
        found = False
        for j, ue in enumerate(unique_eigs):
            if abs(e - ue) < tol:
                eigenspaces[j].append(vecs[:, i])
                found = True
                break
        if not found:
            unique_eigs.append(e)
            eigenspaces.append([vecs[:, i]])

    return unique_eigs, eigenspaces


