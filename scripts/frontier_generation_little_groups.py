def analyze_bz_corners():
    """Compute the eigenvalues of H at each BZ corner and their degeneracies."""
    corners = {
        'Gamma': np.array([0.0, 0.0, 0.0]),
        'X1':    np.array([np.pi, 0.0, 0.0]),
        'X2':    np.array([0.0, np.pi, 0.0]),
        'X3':    np.array([0.0, 0.0, np.pi]),
        'M1':    np.array([np.pi, np.pi, 0.0]),
        'M2':    np.array([np.pi, 0.0, np.pi]),
        'M3':    np.array([0.0, np.pi, np.pi]),
        'R':     np.array([np.pi, np.pi, np.pi]),
    }

    print("\n" + "="*70)
    print("EIGENVALUES AT BZ CORNERS")
    print("="*70)

    results = {}
    for name, K in corners.items():
        H = staggered_H_momentum(K)
        # H is anti-Hermitian, so i*H is Hermitian
        iH = 1j * H
        eigs = np.sort(np.linalg.eigvalsh(iH))

        # Group by degeneracy
        unique_eigs = []
        degs = []
        tol = 1e-10
        for e in eigs:
            found = False
            for i, ue in enumerate(unique_eigs):
                if abs(e - ue) < tol:
                    degs[i] += 1
                    found = True
                    break
            if not found:
                unique_eigs.append(e)
                degs.append(1)

        hw = sum(1 for ki in K if abs(ki) > 0.1)  # Hamming weight
        print(f"\n  {name} = {K/np.pi}*pi  (hw={hw})")
        print(f"    Eigenvalues of iH: {eigs}")
        print(f"    Unique eigenvalues: {[f'{e:.6f}' for e in unique_eigs]}")
        print(f"    Degeneracies: {degs}")

        results[name] = {'eigs': eigs, 'unique': unique_eigs, 'degs': degs, 'hw': hw}

    return results


