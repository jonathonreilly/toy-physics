def test_part5_directional_non_unitary():
    print("\n=== Part 5: Directional measure is non-unitary (necessary obstruction) ===\n")

    L = 32
    a = 0.2

    print("\n  Unitarity defects across (beta, k) sweep:")
    print(f"  {'beta':>6} {'k':>6}    |M†M - I|_max")
    print("  " + "-" * 40)
    min_defect = np.inf
    for beta in [0.0, 0.4, 0.8, 1.6, 3.2]:
        for k in [0.0, 1.0, 5.0, 10.0]:
            M = directional_kernel_1d(L, beta, k, p_exp=2.0, scale=1.0, phi0=0.0)
            defect = np.max(np.abs(M.conj().T @ M - np.eye(L)))
            print(f"  {beta:>6.2f} {k:>6.2f}    {defect:.4f}")
            min_defect = min(min_defect, defect)

    check("Minimum unitarity defect across (beta, k) sweep > 0.05",
          min_defect > 0.05,
          f"min defect = {min_defect:.4f}")

    # Best-fit unitarity for *non-trivial* kernels: require a minimum amount
    # of off-diagonal weight so the optimizer cannot collapse to scale*identity
    # (which is trivially unitary at scale=1 but represents no propagation).
    L_test = 32
    def unit_obj_nontrivial(x):
        beta, k, p_exp, scale, phi0 = x
        if scale < 0.01 or scale > 50:
            return 1e6
        try:
            M = directional_kernel_1d(L_test, beta, k, p_exp, scale, phi0, max_d=8)
        except Exception:
            return 1e6
        # Off-diagonal weight: sum of |M_xy|^2 for x != y in row 0
        diag_w = abs(M[0, 0]) ** 2
        offdiag_w = sum(abs(M[0, y]) ** 2 for y in range(L_test) if y != 0)
        # Require non-trivial spread: off-diagonal weight comparable to diagonal
        if offdiag_w < 0.1 * diag_w:
            return 1e6  # exclude collapsed-to-identity solutions
        return np.max(np.abs(M.conj().T @ M - np.eye(L_test)))

    rng = np.random.default_rng(123)
    best_unit = np.inf
    for trial in range(30):
        x0 = np.array([
            rng.uniform(-2.0, 2.0),
            rng.uniform(0.0, 20.0),
            rng.uniform(0.5, 3.5),
            rng.uniform(0.1, 3.0),
            rng.uniform(-np.pi, np.pi),
        ])
        res = so.minimize(unit_obj_nontrivial, x0, method='Nelder-Mead',
                          options={'xatol': 1e-8, 'fatol': 1e-8, 'maxiter': 1000})
        if res.fun < best_unit:
            best_unit = res.fun

    check("Best-fit unitarity defect (non-trivial kernels only) > 0.01",
          best_unit > 0.01,
          f"min defect across non-trivial parameter space = {best_unit:.4f}")

    # Stone's theorem: only unitary kernels are heat-kernels of self-adjoint H.
    # Non-trivial directional kernels are non-unitary, hence excluded from
    # the heat-kernel class.
    check("Non-trivial directional measure excluded from heat-kernel class",
          best_unit > 0.01,
          "Stone's theorem: K = exp(-i t H) with H = H† iff K is unitary; "
          "non-trivial directional kernels fail")

    return True


