def lattice_momenta(L, a):
    """Return momenta in the first Brillouin zone: p ∈ [-π/a, π/a]."""
    p = 2.0 * np.pi * np.arange(L) / (L * a)
    return np.where(p > np.pi / a, p - 2.0 * np.pi / a, p)


def E_lat_1d(p, a, m):
    """Retained dispersion (EMERGENT_LORENTZ_INVARIANCE_NOTE): E^2 = m^2 + (4/a^2) sin^2(p a/2)."""
    return np.sqrt(m * m + (4.0 / (a * a)) * np.sin(p * a / 2.0) ** 2)


def canonical_evolution_1d(L, a, m, t):
    """Canonical heat-kernel U(t) = exp(-i t H_lat) with H_lat the retained
    lattice Hamiltonian. Built diagonally in momentum space (BZ uses spatial
    spacing `a`) and transformed to position space via DFT.

    Spatial lattice spacing `a` is fixed; temporal evolution time `t` is
    independent. This separation makes the group property
        U(t_1) U(t_2) = U(t_1 + t_2)
    manifest -- both sides use the same H_lat (same a) and Stone's theorem.
    """
    p = lattice_momenta(L, a)
    E = E_lat_1d(p, a, m)
    U_p_diag = np.exp(-1j * t * E)
    F = np.fft.fft(np.eye(L), axis=0) / np.sqrt(L)
    return F.conj().T @ np.diag(U_p_diag) @ F


def canonical_kernel_1d(L, a, m):
    """Per-step kernel: convention t = a (single lattice time step)."""
    return canonical_evolution_1d(L, a, m, t=a)


def directional_kernel_1d(L, beta, k, p_exp, scale, phi0, max_d=8):
    """Directional path-measure transition matrix (1+1D analogue).

        K_dir(δ; β, k, p_exp, scale, φ_0)
            = scale * exp(-β θ²) / L_edge^p_exp * exp(i (k S + φ_0)),
        θ = arctan(|δ|), L_edge = sqrt(1 + δ²), S = L_edge - 1.

    Parameters span the gravity-card empirical surface and beyond.
    """
    M = np.zeros((L, L), dtype=complex)
    for x in range(L):
        for d in range(-max_d, max_d + 1):
            theta = np.arctan(abs(d))
            L_edge = np.sqrt(1.0 + d * d)
            S = L_edge - 1.0
            w = np.exp(-beta * theta ** 2)
            amp = scale * w / L_edge ** p_exp * np.exp(1j * (k * S + phi0))
            M[(x + d) % L, x] += amp
    return M


