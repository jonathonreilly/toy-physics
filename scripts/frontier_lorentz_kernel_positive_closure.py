def test_part6_scheme_universality():
    print("\n=== Part 6: Universality of continuum limit across lattice schemes ===\n")

    m = 1.0
    p_test = 0.5

    # Compare standard Laplacian and Symanzik-improved schemes:
    # both should give the same continuum dispersion sqrt(m^2 + p^2),
    # but differ at finite a by their respective lattice corrections.
    a_values = [0.5, 0.2, 0.1, 0.05]

    print(f"  At p={p_test}, m={m}, comparing E_lat(p) for two schemes:")
    print(f"  {'a':>8} {'standard':>12} {'Symanzik':>12} {'continuum':>12}")
    print("  " + "-" * 52)
    diffs_std_cont = []
    diffs_imp_cont = []
    for a in a_values:
        E_std = E_lat_1d(p_test, a, m)
        E_imp = symanzik_E_lat_1d(p_test, a, m)
        E_cont = np.sqrt(m * m + p_test ** 2)
        diffs_std_cont.append(abs(E_std - E_cont))
        diffs_imp_cont.append(abs(E_imp - E_cont))
        print(f"  {a:>8.4g} {E_std:>12.8f} {E_imp:>12.8f} {E_cont:>12.8f}")

    check("Standard Laplacian -> continuum as a -> 0",
          diffs_std_cont[-1] < 1e-4,
          f"|E_std - E_cont| at a={a_values[-1]} = {diffs_std_cont[-1]:.2e}")

    check("Symanzik scheme -> continuum as a -> 0",
          diffs_imp_cont[-1] < 1e-4,
          f"|E_imp - E_cont| at a={a_values[-1]} = {diffs_imp_cont[-1]:.2e}")

    # Symanzik should converge faster (O(a^4) vs O(a^2)) at small a
    # At a=0.2, |E_std - E_cont| should be ~ O(a^2) ~ 0.01 * (a/sqrt(12))^2 * p^4 / E
    # while |E_imp - E_cont| should be ~ O(a^4)
    # So at a=0.05, ratio std/imp should be a^2 / a^4 = 1/a^2 = 400
    if diffs_imp_cont[-1] > 1e-12 and diffs_std_cont[-1] > 1e-12:
        ratio = diffs_std_cont[-1] / diffs_imp_cont[-1]
    else:
        ratio = 100.0
    check("Symanzik converges faster than standard (improvement)",
          ratio > 5.0 or diffs_imp_cont[-1] < 1e-10,
          f"std/imp at a={a_values[-1]}: {ratio:.1f} (improvement factor)")

    # Both schemes give same continuum -- universality
    rel_diff_continuum = abs(diffs_std_cont[-1] - diffs_imp_cont[-1]) / abs(np.sqrt(m * m + p_test ** 2))
    check("Both schemes converge to same continuum dispersion",
          rel_diff_continuum < 1e-3,
          f"|delta_std - delta_imp|/E_cont at a={a_values[-1]} = {rel_diff_continuum:.2e}")

    return True


