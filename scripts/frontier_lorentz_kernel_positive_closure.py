def symanzik_E_lat_1d(p, a, m):
    """Symanzik tree-improved Laplacian dispersion (4-point stencil).

    -∇²_NN  = (4/a²) sin²(pa/2)        [naive NN: O(a²) error]
    -∇²_NNN = (1/a²) sin²(pa)          [next-nearest-neighbor]
    -∇²_imp = (4/3)(-∇²_NN) - (1/3)(-∇²_NNN)

    Removes the O(a²) error of the naive Laplacian -- error is now O(a^4).
    This is the standard tree-level improvement (Symanzik 1983).

    Used here to verify lattice-scheme universality of the continuum limit:
    different choices of H_lat (NN vs improved) give different finite-`a`
    kernels but the same continuum kernel (Klein-Gordon).
    """
    minus_nabla2_NN = (4.0 / (a * a)) * np.sin(p * a / 2.0) ** 2
    minus_nabla2_NNN = (1.0 / (a * a)) * np.sin(p * a) ** 2
    minus_nabla2_imp = (4.0 / 3.0) * minus_nabla2_NN - (1.0 / 3.0) * minus_nabla2_NNN
    return np.sqrt(m * m + minus_nabla2_imp)


def symanzik_evolution_1d(L, a, m, t):
    """Heat-kernel built from the Symanzik-improved Laplacian dispersion."""
    p = lattice_momenta(L, a)
    E = symanzik_E_lat_1d(p, a, m)
    U_p_diag = np.exp(-1j * t * E)
    F = np.fft.fft(np.eye(L), axis=0) / np.sqrt(L)
    return F.conj().T @ np.diag(U_p_diag) @ F


