def test_part7_stone_uniqueness():
    print("\n=== Part 7: Stone's theorem -- canonical kernel is unique ===\n")

    m = 1.0
    L = 32
    a = 0.2

    # Build H_lat directly as a Hermitian matrix
    p = lattice_momenta(L, a)
    E = E_lat_1d(p, a, m)
    F = np.fft.fft(np.eye(L), axis=0) / np.sqrt(L)
    H_lat = F.conj().T @ np.diag(E) @ F  # diagonal in p-space, transformed to x-space
    # Note: this H_lat has eigenvalues E_lat(p), as required.

    # Verify H_lat is Hermitian (self-adjoint -- prerequisite for Stone)
    H_dagger_diff = np.max(np.abs(H_lat - H_lat.conj().T))
    check("H_lat is self-adjoint (Hermitian)",
          H_dagger_diff < 1e-12,
          f"|H_lat - H_lat†|_max = {H_dagger_diff:.2e}")

    # Stone's theorem: the unique strongly continuous one-parameter unitary
    # group with self-adjoint generator H_lat is U(t) = exp(-i t H_lat).
    # Build U directly via matrix exponential and compare with our canonical:
    U_via_expm = sla.expm(-1j * a * H_lat)
    U_via_FFT = canonical_kernel_1d(L, a, m)
    err = np.max(np.abs(U_via_expm - U_via_FFT))
    check("U via expm(-i a H_lat) = U via FFT diagonal",
          err < 1e-10,
          f"|U_expm - U_FFT|_max = {err:.2e}")

    # Group property: U(t_1) U(t_2) = U(t_1 + t_2)
    # (Both use the same H_lat with fixed lattice spacing a; only evolution
    # time t varies. This is the Stone group property in pure form.)
    t1, t2 = 0.1, 0.3
    U1 = canonical_evolution_1d(L, a, m, t1)
    U2 = canonical_evolution_1d(L, a, m, t2)
    U_sum = canonical_evolution_1d(L, a, m, t1 + t2)
    err = np.max(np.abs(U1 @ U2 - U_sum))
    check("Group property: U(t_1) U(t_2) = U(t_1 + t_2)",
          err < 1e-10,
          f"|U(0.1) U(0.3) - U(0.4)|_max = {err:.2e}")

    # Identity at t = 0
    U0 = canonical_evolution_1d(L, a, m, 0.0)
    err = np.max(np.abs(U0 - np.eye(L)))
    check("Identity: U(0) = I",
          err < 1e-12,
          f"|U(0) - I|_max = {err:.2e}")

    # Inverse: U(-t) = U(t)†
    t = 0.4
    U_pos = canonical_evolution_1d(L, a, m, t)
