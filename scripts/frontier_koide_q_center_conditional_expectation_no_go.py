#!/usr/bin/env python3
"""
Koide Q center conditional-expectation no-go.

Theorem attempt:
  Since the retained second-order Q carrier reduces to the C3 center
  generated by P_plus and P_perp, perhaps the canonical center/conditional
  expectation trace forces equal central labels and hence K_TL = 0.

Result:
  Negative.  The Hilbert-trace-preserving conditional expectation onto the
  center is explicit:

      E_H(X) = Tr(P_plus X)/Tr(P_plus) P_plus
             + Tr(P_perp X)/Tr(P_perp) P_perp.

  It is unital, idempotent, fixes the center, and preserves the ambient Hilbert
  trace.  Composed with the retained Hilbert trace, it inherits the ranks of
  the central projections:

      tau_H(P_plus) = 1/3,   tau_H(P_perp) = 2/3.

  Equal central-label weights are a valid state on the abstract two-atom
  center, but they are not the trace-preserving state induced by the retained
  three-dimensional carrier.  Choosing the abstract center-label trace is
  exactly the non-monoidal coarse-graining primitive isolated by the other
  no-go routes.

No PDG masses, target Koide value, K_TL pin, delta pin, or H_* pin is used.
"""

from __future__ import annotations

import sys

import sympy as sp


PASSES: list[tuple[str, bool, str]] = []


def record(name: str, ok: bool, detail: str = "") -> None:
    PASSES.append((name, ok, detail))
    status = "PASS" if ok else "FAIL"
    print(f"[{status}] {name}")
    if detail:
        for line in detail.splitlines():
            print(f"       {line}")


def section(title: str) -> None:
    print()
    print("=" * 88)
    print(title)
    print("=" * 88)


def q_from_weights(w_plus: sp.Expr, w_perp: sp.Expr) -> sp.Expr:
    r = sp.simplify(w_perp / w_plus)
    return sp.simplify((1 + r) / 3)


def ktl_from_weights(w_plus: sp.Expr, w_perp: sp.Expr) -> sp.Expr:
    r = sp.simplify(w_perp / w_plus)
    return sp.simplify((r**2 - 1) / (4 * r))


def hilbert_center_expectation(x: sp.Matrix, p_plus: sp.Matrix, p_perp: sp.Matrix) -> sp.Matrix:
    return sp.simplify(
        sp.trace(p_plus * x) / sp.trace(p_plus) * p_plus
        + sp.trace(p_perp * x) / sp.trace(p_perp) * p_perp
    )


def main() -> int:
    section("A. Center of the retained C3 carrier")

    C = sp.Matrix([[0, 0, 1], [1, 0, 0], [0, 1, 0]])
    I3 = sp.eye(3)
    J = sp.ones(3, 3)
    P_plus = J / 3
    P_perp = I3 - P_plus
    record(
        "A.1 central projectors have ranks 1 and 2",
        P_plus.rank() == 1 and P_perp.rank() == 2,
        f"rank(P_plus)={P_plus.rank()}, rank(P_perp)={P_perp.rank()}",
    )
    record(
        "A.2 P_plus and P_perp are fixed by C3 conjugation",
        sp.simplify(C * P_plus * C**2 - P_plus) == sp.zeros(3, 3)
        and sp.simplify(C * P_perp * C**2 - P_perp) == sp.zeros(3, 3),
        "They span the two-atom center of the retained real quotient.",
    )
    record(
        "A.3 central projectors are orthogonal and exhaustive",
        sp.simplify(P_plus * P_perp) == sp.zeros(3, 3)
        and sp.simplify(P_plus + P_perp) == I3,
        "P_plus P_perp=0 and P_plus+P_perp=I.",
    )

    section("B. Hilbert-trace-preserving conditional expectation")

    x_symbols = sp.symbols("x00:03 x10:13 x20:23")
    X = sp.Matrix(3, 3, x_symbols)
    E_X = hilbert_center_expectation(X, P_plus, P_perp)
    E_E_X = hilbert_center_expectation(E_X, P_plus, P_perp)
    z_plus, z_perp = sp.symbols("z_plus z_perp", real=True)
    Z = z_plus * P_plus + z_perp * P_perp
    E_Z = hilbert_center_expectation(Z, P_plus, P_perp)
    record(
        "B.1 E_H is unital, idempotent, and fixes the center",
        hilbert_center_expectation(I3, P_plus, P_perp) == I3
        and sp.simplify(E_E_X - E_X) == sp.zeros(3, 3)
        and sp.simplify(E_Z - Z) == sp.zeros(3, 3),
        "E_H(X)=Tr(P_plus X)P_plus/1 + Tr(P_perp X)P_perp/2.",
    )
    record(
        "B.2 E_H preserves the ambient Hilbert trace",
        sp.simplify(sp.trace(E_X) - sp.trace(X)) == 0,
        "Tr(E_H(X))=Tr(X) for a symbolic 3x3 matrix X.",
    )

    tau_h_plus = sp.simplify(sp.trace(P_plus) / 3)
    tau_h_perp = sp.simplify(sp.trace(P_perp) / 3)
    record(
        "B.3 normalized Hilbert trace gives rank weights 1:2",
        tau_h_plus == sp.Rational(1, 3) and tau_h_perp == sp.Rational(2, 3),
        f"tau_H(P_plus)={tau_h_plus}, tau_H(P_perp)={tau_h_perp}",
    )
    record(
        "B.4 Hilbert-trace center weights are off the equal-block leaf",
        q_from_weights(tau_h_plus, tau_h_perp) == 1
        and ktl_from_weights(tau_h_plus, tau_h_perp) == sp.Rational(3, 8),
        f"Q={q_from_weights(tau_h_plus, tau_h_perp)}, K_TL={ktl_from_weights(tau_h_plus, tau_h_perp)}",
    )

    section("C. Abstract center-label trace is a different state")

    tau_z_plus = sp.Rational(1, 2)
    tau_z_perp = sp.Rational(1, 2)
    record(
        "C.1 equal label trace on the abstract two-atom center lands on the Koide leaf",
        q_from_weights(tau_z_plus, tau_z_perp) == sp.Rational(2, 3)
        and ktl_from_weights(tau_z_plus, tau_z_perp) == 0,
        f"tau_Z=(1/2,1/2) -> Q={q_from_weights(tau_z_plus, tau_z_perp)}, K_TL={ktl_from_weights(tau_z_plus, tau_z_perp)}",
    )
    record(
        "C.2 equal label trace is not the Hilbert-trace-preserving center state",
        tau_z_plus != tau_h_plus and tau_z_perp != tau_h_perp,
        f"tau_Z - tau_H = ({tau_z_plus - tau_h_plus}, {tau_z_perp - tau_h_perp})",
    )
    density_label = sp.Rational(3, 2) * P_plus + sp.Rational(3, 4) * P_perp
    record(
        "C.3 equal label trace is a non-Hilbert central density on the retained carrier",
        sp.simplify(sp.trace(density_label) / 3) == 1
        and sp.simplify(sp.trace(density_label * P_plus) / 3) == tau_z_plus
        and sp.simplify(sp.trace(density_label * P_perp) / 3) == tau_z_perp
        and density_label != I3,
        "D_label=(3/2)P_plus+(3/4)P_perp, not the retained Hilbert density I.",
    )

    section("D. Family of center states exposes the residual choice")

    u, w = sp.symbols("u w", positive=True, real=True)
    density_u = sp.simplify(3 * u * P_plus + sp.Rational(3, 2) * (1 - u) * P_perp)
    state_u_plus = sp.simplify(sp.trace(density_u * P_plus) / 3)
    state_u_perp = sp.simplify(sp.trace(density_u * P_perp) / 3)
    record(
        "D.1 every central preparation is a positive central density choice",
        state_u_plus == u and state_u_perp == 1 - u,
        "D_u=3u P_plus + (3/2)(1-u)P_perp realizes tau(P_plus)=u.",
    )
    tau_w_plus = sp.simplify(1 / (1 + w))
    tau_w_perp = sp.simplify(w / (1 + w))
    record(
        "D.2 positive center states retain one free ratio w",
        sp.simplify(tau_w_plus + tau_w_perp) == 1,
        f"tau_w=(1/(1+w), w/(1+w)); ratio={w}",
    )
    record(
        "D.3 Hilbert trace and equal-label trace are two different ratio choices",
        tau_w_perp.subs(w, 2) == tau_h_perp and tau_w_perp.subs(w, 1) == tau_z_perp,
        "w=2 is rank/Hilbert; w=1 is equal central labels.",
    )
    record(
        "D.4 choosing w=1 is exactly the missing source-neutral coarse graining",
        sp.solve(sp.Eq(ktl_from_weights(tau_w_plus, tau_w_perp), 0), w) == [1],
        "K_TL(tau_w)=0 -> w=1.",
    )
    record(
        "D.5 choosing u=1/2 is the same residual in preparation coordinates",
        sp.solve(sp.Eq(ktl_from_weights(u, 1 - u), 0), u) == [sp.Rational(1, 2)]
        and ktl_from_weights(sp.Rational(1, 3), sp.Rational(2, 3)) == sp.Rational(3, 8),
        "K_TL(u)=0 -> u=1/2; retained Hilbert/rank state is u=1/3.",
    )

    section("E. Verdict")

    record(
        "E.1 center conditional-expectation route does not close Q",
        True,
        "Trace-preserving expectation gives rank weights; equal labels require a separate center-state principle.",
    )
    record(
        "E.2 Q remains open after center expectation audit",
        True,
        "Residual primitive: physical selection of abstract center-label trace over retained Hilbert/rank trace.",
    )

    print()
    n_pass = sum(1 for _, ok, _ in PASSES if ok)
    n_total = len(PASSES)
    print("=" * 88)
    print("Summary")
    print("=" * 88)
    print(f"PASSED: {n_pass}/{n_total}")
    for name, ok, _ in PASSES:
        print(f"  [{'PASS' if ok else 'FAIL'}] {name}")

    print()
    if n_pass == n_total:
        print("VERDICT: center conditional expectation does not close Q.")
        print("KOIDE_Q_CENTER_CONDITIONAL_EXPECTATION_NO_GO=TRUE")
        print("Q_CENTER_CONDITIONAL_EXPECTATION_CLOSES_Q=FALSE")
        print("RESIDUAL_SCALAR=center_trace_choice_rank_vs_label_equiv_K_TL")
        print("RESIDUAL_CENTER_STATE=w_center_label_minus_1_equiv_K_TL")
        print("RESIDUAL_DENSITY=central_density_D_u_not_fixed_by_retained_expectation")
        return 0

    print("VERDICT: center conditional-expectation audit has FAILs.")
    print("KOIDE_Q_CENTER_CONDITIONAL_EXPECTATION_NO_GO=FALSE")
    print("Q_CENTER_CONDITIONAL_EXPECTATION_CLOSES_Q=FALSE")
    print("RESIDUAL_SCALAR=center_trace_choice_rank_vs_label_equiv_K_TL")
    print("RESIDUAL_CENTER_STATE=w_center_label_minus_1_equiv_K_TL")
    print("RESIDUAL_DENSITY=central_density_D_u_not_fixed_by_retained_expectation")
    return 1


if __name__ == "__main__":
    sys.exit(main())
