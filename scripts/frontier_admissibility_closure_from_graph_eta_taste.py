def gamma_from_eta_construction(n: int) -> list[np.ndarray]:
    """Gamma_k on the 2^n-dim taste space, from the retained eta-phase/taste
    recipe on Z^n.

    At position k in 1..n:
        Gamma_k = sigma_y^(k-1) (x) sigma_x (x) sigma_0^(n-k)

    At n=3 this gives the retained G_1, G_2, G_3 of
    docs/NATIVE_GAUGE_CLOSURE_NOTE.md verbatim.
    """
    gammas: list[np.ndarray] = []
    for k in range(1, n + 1):
        factors = [SIGMA_Y] * (k - 1) + [SIGMA_X] + [SIGMA_0] * (n - k)
        gammas.append(kron_list(factors))
    return gammas


def verify_clifford(gammas: list[np.ndarray], tol: float = 1e-12) -> float:
    """Return max deviation from {Gamma_mu, Gamma_nu} = 2 delta_{mu,nu} I."""
    n = len(gammas)
    d = gammas[0].shape[0]
    I = np.eye(d, dtype=np.complex128)
    max_err = 0.0
    for mu in range(n):
        for nu in range(n):
            anti = gammas[mu] @ gammas[nu] + gammas[nu] @ gammas[mu]
            target = 2.0 * I if mu == nu else np.zeros_like(I)
            err = np.max(np.abs(anti - target))
            if err > max_err:
                max_err = err
    return max_err


