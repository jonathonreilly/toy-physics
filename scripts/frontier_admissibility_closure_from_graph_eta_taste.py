def axis_permutation_lift(
    gammas: list[np.ndarray], i: int, j: int
) -> list[np.ndarray]:
    """Return the Gamma_k' obtained by the axis-permutation (i <-> j) on Z^n,
    lifted via the eta-phase rule.

    The lift is NOT a simple Python-list swap: the staggered eta phases
    reshuffle when axes are permuted. However, the resulting Gamma_k' satisfy
    the same Clifford anticommutator {Gamma'_mu, Gamma'_nu} = 2 delta I,
    so Gamma'_k differs from Gamma_{sigma(k)} at most by an O(n)
    automorphism of Cl(n).

    For the O(n)-covariance test we only need: the SPAN of bivector products
    is invariant under the permutation. This is cleaner to test at the
    level of the bivector subspace directly: sigma_{ij} swaps Gamma_i and
    Gamma_j in the list, and the bivector basis (1/2) Gamma_mu Gamma_nu
    is reindexed accordingly, up to signs from anticommutation.
    """
    new = list(gammas)
    new[i], new[j] = new[j], new[i]
    return new


def axis_sign_flip_lift(gammas: list[np.ndarray], i: int) -> list[np.ndarray]:
    """Gamma_i -> -Gamma_i; other Gamma_k unchanged."""
    new = list(gammas)
    new[i] = -gammas[i]
    return new


def is_in_span(
    X: np.ndarray, basis_mats: list[np.ndarray], tol: float = 1e-10
) -> bool:
    """Check if X is in the linear span of basis_mats."""
    cols = np.stack([B.flatten() for B in basis_mats], axis=1)
    target = X.flatten()
    # Solve basis * coeffs = target in least-squares sense
    coeffs, *_ = np.linalg.lstsq(cols, target, rcond=None)
    residual = np.linalg.norm(cols @ coeffs - target)
    return residual < tol


