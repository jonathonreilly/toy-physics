def clifford_basis_monomials(gammas: list[np.ndarray]) -> dict[tuple[int, ...], np.ndarray]:
    """Return { I : Gamma_I } for I ranging over ordered subsets of {0,...,n-1}.

    Gamma_{} = I, Gamma_{i_1 < ... < i_k} = Gamma_{i_1} ... Gamma_{i_k}.
    These 2^n monomials form a basis of Cl(n) = End(C^{2^n}) (for even n)
    or a generating set for odd n (still 2^n-dimensional as a vector space).
    """
    n = len(gammas)
    d = gammas[0].shape[0]
    basis: dict[tuple[int, ...], np.ndarray] = {(): np.eye(d, dtype=np.complex128)}
    for k in range(1, n + 1):
        for I in itertools.combinations(range(n), k):
            M = np.eye(d, dtype=np.complex128)
            for i in I:
                M = M @ gammas[i]
            basis[I] = M
    return basis


def clifford_inner_trace(A: np.ndarray, B: np.ndarray) -> complex:
    """Normalized Clifford-trace pairing: <A, B> = Tr(A^dag B) / dim."""
    d = A.shape[0]
    return np.trace(A.conj().T @ B) / d


def grade_components(
    X: np.ndarray,
    basis: dict[tuple[int, ...], np.ndarray],
    n: int,
) -> dict[int, float]:
    """Return {k: ||X_k||^2_F-normalized} where X = sum_k X_k is the Clifford
    grade decomposition.

    X_k is the sum of components of X in the degree-k monomials. We measure
    the weight of each grade by summing |<X, Gamma_I>|^2 across |I| = k.
    The monomials are orthogonal under the normalized Clifford trace pairing
    (direct consequence of {Gamma_mu, Gamma_nu} = 2 delta_{mu,nu} I).
    """
    weights: dict[int, float] = {k: 0.0 for k in range(n + 1)}
    for I, GI in basis.items():
        # Normalize: <Gamma_I, Gamma_I> = 1 always (each Gamma^2 = I, orthonormal basis)
        c = clifford_inner_trace(GI, X)
        weights[len(I)] += abs(c) ** 2
    return weights


