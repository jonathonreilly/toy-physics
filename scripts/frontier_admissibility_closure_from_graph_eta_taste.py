def v_n_framework_basis(gammas: list[np.ndarray]) -> list[tuple[tuple[int, int], np.ndarray]]:
    """Return [((mu, nu), (1/2) Gamma_mu Gamma_nu) for mu < nu].

    This is the framework-native definition of V_n: the bivector subspace
    of Cl(n) generated by the retained eta-phase Gamma_k's. At n=3 these
    are the retained native-SU(2) generators (up to the conventional
    factor of i used in Hermitian form).
    """
    n = len(gammas)
    out = []
    for mu in range(n):
        for nu in range(mu + 1, n):
            B = 0.5 * (gammas[mu] @ gammas[nu])
            out.append(((mu, nu), B))
    return out


