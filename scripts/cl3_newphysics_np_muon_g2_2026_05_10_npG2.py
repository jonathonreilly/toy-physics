def section1_pauli_structural_anchor() -> None:
    """P1: retained Cl(3) Pauli irrep supplies the vertex algebra used in
    the Schwinger 1-loop amplitude.

    The Gordon-decomposed QED vertex contains sigma^{mu nu} =
    (i/2) [gamma^mu, gamma^nu]. Non-relativistic reduction to the
    magnetic-moment piece projects sigma^{ij} = i eps_ijk sigma_k
    (Pauli matrices). The 2-dim Cl(3) Pauli irrep is retained per
    CL3_PAULI_IRREP_UNIQUENESS_NARROW_THEOREM_NOTE_2026-05-10.
    """
    section_header(
        "Section 1: Pauli structural anchor (P1)"
    )

    try:
        import numpy as np
    except ImportError:
        report("S1.0 numpy import", False, "numpy required")
        return
    report("S1.0 numpy import", True, "numpy available")

    # Pauli matrices in canonical Cl(3) irrep
    sigma_1 = np.array([[0, 1], [1, 0]], dtype=complex)
    sigma_2 = np.array([[0, -1j], [1j, 0]], dtype=complex)
    sigma_3 = np.array([[1, 0], [0, -1]], dtype=complex)
    eye2 = np.eye(2, dtype=complex)
    sigma = [sigma_1, sigma_2, sigma_3]

    # P1.1 Cl(3) anticommutation: {sigma_i, sigma_j} = 2 delta_ij I
    anticomm_ok = True
    max_dev = 0.0
    for i in range(3):
        for j in range(3):
            ac = sigma[i] @ sigma[j] + sigma[j] @ sigma[i]
            target = 2 * (1 if i == j else 0) * eye2
            d = float(np.max(np.abs(ac - target)))
            max_dev = max(max_dev, d)
            if d > 1e-12:
                anticomm_ok = False
    report(
        "S1.1 P1 Cl(3) anticommutation {sigma_i, sigma_j} = 2 delta_ij I",
        anticomm_ok,
        f"max |dev| = {max_dev:.2e}",
    )

    # P1.2 sigma_i sigma_j = delta_ij I + i eps_ijk sigma_k (vertex closure)
    levi_civita = np.zeros((3, 3, 3))
    levi_civita[0, 1, 2] = 1
    levi_civita[1, 2, 0] = 1
    levi_civita[2, 0, 1] = 1
    levi_civita[2, 1, 0] = -1
    levi_civita[1, 0, 2] = -1
    levi_civita[0, 2, 1] = -1

    vertex_ok = True
    max_dev = 0.0
    for i in range(3):
        for j in range(3):
            prod = sigma[i] @ sigma[j]
            target = (1 if i == j else 0) * eye2
            for k in range(3):
                target = target + 1j * levi_civita[i, j, k] * sigma[k]
            d = float(np.max(np.abs(prod - target)))
            max_dev = max(max_dev, d)
            if d > 1e-12:
                vertex_ok = False
    report(
        "S1.2 P1 Pauli vertex algebra sigma_i sigma_j = delta_ij I + i eps_ijk sigma_k",
        vertex_ok,
        f"max |dev| = {max_dev:.2e}",
    )

    # P1.3 sigma^{ij} = -eps_ijk sigma_k (Gordon non-rel reduction)
    # Define sigma^{ij} := (i/2)[sigma^i, sigma^j].
    # From the commutator [sigma_i, sigma_j] = 2 i eps_ijk sigma_k we get
    #   (i/2) [sigma_i, sigma_j] = - eps_ijk sigma_k.
    # The non-relativistic magnetic-moment piece of the QED vertex picks up
    # this structure (up to a global Pauli/Dirac convention sign).
    gordon_ok = True
    max_dev = 0.0
    for i in range(3):
        for j in range(3):
            sigma_ij = (1j / 2) * (sigma[i] @ sigma[j] - sigma[j] @ sigma[i])
            target = np.zeros((2, 2), dtype=complex)
            for k in range(3):
                target = target + (-1) * levi_civita[i, j, k] * sigma[k]
            d = float(np.max(np.abs(sigma_ij - target)))
            max_dev = max(max_dev, d)
            if d > 1e-12:
                gordon_ok = False
    report(
        "S1.3 P1 Gordon non-rel reduction: sigma^{ij} = (i/2)[sigma_i,sigma_j] = -eps_ijk sigma_k",
        gordon_ok,
        f"max |dev| = {max_dev:.2e}",
    )

    # P1.4 Verify CL3_PAULI_IRREP_UNIQUENESS narrow theorem note is cited
    base = os.getcwd()
    pauli_path = os.path.join(
        base,
        "docs/CL3_PAULI_IRREP_UNIQUENESS_NARROW_THEOREM_NOTE_2026-05-10.md",
    )
    pauli_cited = os.path.exists(pauli_path)
    report(
        "S1.4 P1 narrow theorem note exists on retained surface",
        pauli_cited,
        f"path = CL3_PAULI_IRREP_UNIQUENESS_NARROW_THEOREM_NOTE_2026-05-10.md",
    )

    # P1.5 Pauli irrep has complex dimension 2 (canonical Cl(3))
    irrep_dim = sigma[0].shape[0]
    report(
        "S1.5 P1 retained Cl(3) Pauli irrep has dim_C = 2",
        irrep_dim == 2,
        f"dim = {irrep_dim}",
    )


