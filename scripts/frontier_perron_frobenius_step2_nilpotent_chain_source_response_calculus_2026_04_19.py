PASS_COUNT = 0
FAIL_COUNT = 0


def check(name: str, condition: bool, detail: str = "") -> bool:
    global PASS_COUNT, FAIL_COUNT
    status = "PASS" if condition else "FAIL"
    if condition:
        PASS_COUNT += 1
    else:
        FAIL_COUNT += 1
    line = f"  [{status}] {name}"
    if detail:
        line += f"  ({detail})"
    print(line)
    return condition


def live_from_response_pack(responses: list[float]) -> np.ndarray:
    triplet_e1 = 0.5 * (responses[1] - responses[2]) + 0.25 * (responses[3] - responses[5])
    triplet_e2 = responses[0] + 0.25 * (responses[3] + responses[5]) - 0.5 * (responses[1] + responses[2]) - 0.5 * responses[7]
    s12, s13 = projected_even_split_from_responses(np.array(responses, dtype=float))
    return np.array(
        [
            mass_from_projected_even_split(triplet_e2, s12, s13),
            delta_live_from_projected_even_split(triplet_e1, s12, s13),
            q_plus_from_projected_even_split(s12, s13),
        ],
        dtype=float,
    )


def main() -> int:
    print("=" * 104)
    print("PERRON-FROBENIUS STEP-2 NILPOTENT-CHAIN SOURCE-RESPONSE CALCULUS")
    print("=" * 104)
    print()
    print("Question:")
    print("  Once N_chain is given, are the Hermitian source paths it generates")
    print("  sufficient to recover the exact local chain-plane and projected-source")
    print("  response data?")

    n = canonical_n_chain()
    packet = chain_plane_packet_from_n(n)
    long = long_corner_packet_from_n(n)
    h_generic = generic_hermitian_target()
    model = build_synthetic_wilson_parent(h_generic)
    i_e = model["I_e"]
    s_w = model["S_W"]

    full_responses = np.array(response_pack_from_n_chain_sources(s_w, i_e, n), dtype=float)
    direct_responses = np.array(hermitian_linear_responses(h_generic), dtype=float)
    h_rec = reconstruct_h_from_responses(full_responses.tolist())
    check(
        "The exact first-variation formulas on the N_chain-generated full Hermitian basis reproduce the canonical projected response pack",
        np.linalg.norm(full_responses - direct_responses) < 1e-12,
        f"response_err={np.linalg.norm(full_responses - direct_responses):.2e}",
    )
    check(
        "Those same first variations reconstruct the generic charged Hermitian target exactly",
        np.linalg.norm(h_rec - h_generic) < 1e-12,
        f"reconstruction_err={np.linalg.norm(h_rec - h_generic):.2e}",
    )

    chain_plane_responses = np.array(
        [
            first_variation_from_resolvent(s_w, embedded_source(i_e, packet["X12"])),
            first_variation_from_resolvent(s_w, embedded_source(i_e, packet["Y12"])),
            first_variation_from_resolvent(s_w, embedded_source(i_e, packet["X23"])),
            first_variation_from_resolvent(s_w, embedded_source(i_e, packet["Y23"])),
        ],
        dtype=float,
    )
    chain_plane_direct = direct_responses[[3, 4, 7, 8]]
    long_corner_responses = np.array(
        [
            first_variation_from_resolvent(s_w, embedded_source(i_e, long["X13"])),
            first_variation_from_resolvent(s_w, embedded_source(i_e, long["Y13"])),
        ],
        dtype=float,
    )
    long_corner_direct = direct_responses[[5, 6]]

    check(
        "The nearest-neighbor Hermitian 4-packet generated from N_chain carries the exact local chain-plane response data",
        np.linalg.norm(chain_plane_responses - chain_plane_direct) < 1e-12,
        f"chain_plane_err={np.linalg.norm(chain_plane_responses - chain_plane_direct):.2e}",
    )
    check(
        "The downstream long-corner Hermitian pair generated from N_chain^2 carries the exact (S13,A13) response data",
        np.linalg.norm(long_corner_responses - long_corner_direct) < 1e-12,
        f"long_corner_err={np.linalg.norm(long_corner_responses - long_corner_direct):.2e}",
    )

    x, y, phase = solve_sparse_target_preimage()
    h_target = sparse_face_h(np.array([x[0], x[1], y[0], phase], dtype=float))
    model_target = build_synthetic_wilson_parent(h_target)
    responses_target = response_pack_from_n_chain_sources(model_target["S_W"], model_target["I_e"], n)
    s12, s13 = projected_even_split_from_responses(np.array(responses_target, dtype=float))
    live = live_from_response_pack(responses_target)

    check(
        "On the known exact sparse-face target preimage, the N_chain-generated source calculus reproduces the exact ordered projected-source even law (S12,S13)",
        np.linalg.norm(np.array([s12, s13], dtype=float) - np.array(hermitian_linear_responses(h_target), dtype=float)[[3, 5]]) < 1e-12,
        f"(S12,S13)=({s12:.12f},{s13:.12f})",
    )
    check(
        "Composing those responses forward through the already-closed reduced codomain reproduces the observed live point",
        np.linalg.norm(live - TARGET) < 1e-8,
        f"dist={np.linalg.norm(live - TARGET):.2e}",
    )

    print("\n" + "=" * 104)
    print("RESULT")
    print("=" * 104)
    print("  Exact calculus answer:")
    print("    - once N_chain is given, the Hermitian source paths it generates carry")
    print("      the full projected-response pack exactly")
    print("    - the 4-source chain-plane packet carries the nearest-neighbor part")
    print("    - the downstream long-corner pair from N_chain^2 carries the missing")
    print("      (S13,A13) channels")
    print("    - together they compose to the already-closed dW_e^H ordered-even law")
    print("      and to the observed live point on the known target preimage")
    print()
    print(f"PASS={PASS_COUNT} FAIL={FAIL_COUNT}")
    return 0 if FAIL_COUNT == 0 else 1


if __name__ == "__main__":
    sys.exit(main())
