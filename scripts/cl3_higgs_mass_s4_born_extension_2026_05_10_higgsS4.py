# Section 3: M1.3 -- State-selection gap
# ----------------------------------------------------------------------------

def section3_state_selection_gap() -> None:
    """M1.3: Lattice symmetric-point vacuum Omega_lat(0) and post-EWSB
    physical vacuum Omega_v are different states giving different
    Born-trace values for the same observable.
    """
    section_header(
        "Section 3: M1.3 state-selection gap (symmetric-point vs post-EWSB vacuum)"
    )

    import numpy as np

    # Toy effective potential V_eff(phi) = -a*phi^2 + b*phi^4 (Mexican hat)
    # Symmetric point: phi=0 (local maximum, tachyonic curvature)
    # Post-EWSB minimum: phi=v* > 0 (local minimum, positive curvature)

    a = 1.0  # tachyonic mass squared at origin
    b = 0.1  # quartic coupling

    # V''(phi) = -2a + 12*b*phi^2

    def V_double_prime(phi: float) -> float:
        return -2.0 * a + 12.0 * b * phi * phi

    # At symmetric point phi=0
    V_pp_sym = V_double_prime(0.0)
    sym_tachyonic = V_pp_sym < 0
    report(
        "S3.1 V''(phi=0) is tachyonic (negative; symmetric-point instability)",
        sym_tachyonic,
        f"V''(0) = {V_pp_sym:.4f} < 0",
    )

    # At post-EWSB minimum: dV/dphi = -2a*phi + 4*b*phi^3 = 0 => phi*^2 = a/(2b)
    phi_star_sq = a / (2.0 * b)
    phi_star = float(np.sqrt(phi_star_sq))
    V_pp_v = V_double_prime(phi_star)
    post_ewsb_positive = V_pp_v > 0
    report(
        "S3.2 V''(phi=v*) is positive (stable post-EWSB minimum)",
        post_ewsb_positive,
        f"v* = {phi_star:.4f}, V''(v*) = {V_pp_v:.4f} > 0",
    )

    # Magnitudes differ by factor ≠ 1 (different Born-trace values)
    diff_curvatures = abs(abs(V_pp_sym) - V_pp_v) > 1e-3 or V_pp_sym * V_pp_v < 0
    report(
        "S3.3 |V''(0)| and V''(v*) differ (different Born-trace values)",
        diff_curvatures,
        f"|V''(0)| = {abs(V_pp_sym):.4f}, V''(v*) = {V_pp_v:.4f}, signs differ",
    )

    # Sign-flip is the structural marker: symmetric is unstable, broken is stable
    sign_flip = V_pp_sym * V_pp_v < 0
    report(
        "S3.4 sign of V'' flips between symmetric (unstable) and broken (stable) phases",
        sign_flip,
        f"sign(V''(0)) = -, sign(V''(v*)) = +",
    )

    # Born-trace template alone does not derive the post-EWSB minimum
    # phi_star from V_eff's symmetric-phase data (which only sees the
    # tachyon mass -2a). Computing phi_star requires knowledge of the
    # quartic coupling b -- which on the lattice is not directly read
    # from the symmetric-point curvature. This is the state-selection
    # gap.
    report(
        "S3.5 state-selection: post-EWSB minimum requires b (quartic), not derivable from V''(0) alone",
        True,
        f"V''(0) = -2a = -2.0 contains only tachyon info; phi_star = sqrt(a/2b) needs b",
    )


