def section1_trace_template_generality() -> None:
    """M1.1: For any density operator rho_hat and Hermitian operator O,
    <O> = Tr(rho_hat * O). Verify on toy lattice with multiple
    observables.
    """
    section_header(
        "Section 1: M1.1 trace-template generality Tr(rho * O) = <O>"
    )

    try:
        import numpy as np
    except ImportError:
        report("S1.0 numpy import", False, "numpy required")
        return
    report("S1.0 numpy import", True, "numpy available")

    # Toy 4-site Hilbert space (Z^1 reduction; structure carries to Z^3)
    N = 4

    # Density operator: pure state with non-uniform amplitudes
    psi = np.array([0.6, 0.5, 0.4, 0.3], dtype=complex)
    psi /= np.linalg.norm(psi)
    rho = np.outer(psi, psi.conj())

    # Multiple Hermitian observables on the same Hilbert space:

    # (a) Position projector at x=2 (G2's M_hat(x=2))
    M2 = np.zeros((N, N), dtype=complex)
    M2[2, 2] = 1.0

    # (b) Position projector at x=1
    M1 = np.zeros((N, N), dtype=complex)
    M1[1, 1] = 1.0

    # (c) Diagonal observable: sigma_x = diag(0,1,2,3)
    sx_op = np.diag(np.array([0.0, 1.0, 2.0, 3.0], dtype=complex))

    # (d) Curvature-like observable on the same space:
    # O_curv = -|grad|^2 (discrete Laplacian, Hermitian)
    O_curv = np.zeros((N, N), dtype=complex)
    for i in range(N):
        O_curv[i, i] = 2.0
        O_curv[i, (i + 1) % N] = -1.0
        O_curv[i, (i - 1) % N] = -1.0

    # Check trace-operator template for each observable:
    for label, op in [("M_hat(x=2)", M2), ("M_hat(x=1)", M1), ("sigma_x", sx_op), ("O_curv", O_curv)]:
        # <O> = Tr(rho * O)
        trace_value = np.trace(rho @ op).real
        # <O> = <psi|O|psi> for pure state
        direct_value = float((psi.conj() @ op @ psi).real)
        match = abs(trace_value - direct_value) < 1e-10
        report(
            f"S1.1 {label}: <O> = Tr(rho * O) = <psi|O|psi>",
            match,
            f"Tr value = {trace_value:.6f}, direct = {direct_value:.6f}",
        )

    # Hermiticity verification:
    for label, op in [("M_hat(x=2)", M2), ("sigma_x", sx_op), ("O_curv", O_curv)]:
        herm = np.allclose(op, op.conj().T)
        report(
            f"S1.2 {label}: Hermitian",
            herm,
            f"max |op - op^dagger| = {float(np.max(np.abs(op - op.conj().T))):.2e}",
        )


