# Section 2: M1.2 -- Distinct observables
# ----------------------------------------------------------------------------

def section2_distinct_observables() -> None:
    """M1.2: M_hat(x) (rank-1 projector) and O_{V''} (curvature operator)
    are structurally distinct observables sharing only the trace template.
    """
    section_header(
        "Section 2: M1.2 distinct observables (position projector vs curvature operator)"
    )

    import numpy as np

    N = 4

    # Position projector at x=2
    M2 = np.zeros((N, N), dtype=complex)
    M2[2, 2] = 1.0

    # Discrete Laplacian (curvature-like) on Z^1 ~ a curvature operator
    O_curv = np.zeros((N, N), dtype=complex)
    for i in range(N):
        O_curv[i, i] = 2.0
        O_curv[i, (i + 1) % N] = -1.0
        O_curv[i, (i - 1) % N] = -1.0

    # M2 is rank 1, idempotent, PSD
    M2_rank = int(np.linalg.matrix_rank(M2))
    M2_idempotent = np.allclose(M2 @ M2, M2)
    M2_eigs = np.linalg.eigvalsh(M2)
    M2_psd = bool(np.all(M2_eigs >= -1e-12))

    report(
        "S2.1 M_hat(x=2) is rank-1 (projector property)",
        M2_rank == 1,
        f"rank(M_hat) = {M2_rank}",
    )
    report(
        "S2.2 M_hat(x=2) is idempotent (M^2 = M)",
        M2_idempotent,
        "rank-1 projector",
    )
    report(
        "S2.3 M_hat(x=2) is PSD",
        M2_psd,
        f"min eig = {float(np.min(M2_eigs)):.6e}",
    )

    # O_curv is rank > 1 (NOT a rank-1 projector), NOT idempotent
    # On periodic Z_4, the discrete Laplacian has a zero mode (constant
    # function) so rank = N - 1 = 3 in this toy example. The relevant
    # property is rank > 1, distinguishing it from M_hat (rank-1).
    O_rank = int(np.linalg.matrix_rank(O_curv))
    O_idempotent = np.allclose(O_curv @ O_curv, O_curv)
    O_eigs = np.linalg.eigvalsh(O_curv)
    # O_curv is a Laplacian; positive semidefinite (with one zero mode for periodic boundary)
    O_psd = bool(np.all(O_eigs >= -1e-12))

    report(
        "S2.4 O_curv is rank > 1 (NOT a rank-1 projector)",
        O_rank > 1,
        f"rank(O_curv) = {O_rank} (N-1 = {N-1} for Z_{N} periodic Laplacian)",
    )
    report(
        "S2.5 O_curv is NOT idempotent (NOT a projector)",
        not O_idempotent,
        f"max |O^2 - O| = {float(np.max(np.abs(O_curv @ O_curv - O_curv))):.6f}",
    )

    # Different ranks => different observables
    diff_rank = M2_rank != O_rank
    report(
        "S2.6 M_hat(x) and O_curv are structurally distinct (different ranks)",
        diff_rank,
        f"rank(M) = {M2_rank}, rank(O_curv) = {O_rank}",
    )

    # Different expectations on the same state
    psi = np.array([0.6, 0.5, 0.4, 0.3], dtype=complex)
    psi /= np.linalg.norm(psi)
    rho = np.outer(psi, psi.conj())

    exp_M = float((psi.conj() @ M2 @ psi).real)
    exp_O = float((psi.conj() @ O_curv @ psi).real)
    diff_expectation = abs(exp_M - exp_O) > 1e-3
    report(
        "S2.7 different expectations on the same state (M_hat vs O_curv)",
        diff_expectation,
        f"<M_hat(x=2)> = {exp_M:.4f}, <O_curv> = {exp_O:.4f}",
    )


