def build_clifford_gammas():
    """Build Cl(3) Gamma matrices in the 2^3 = 8 dim taste space."""
    G1 = np.kron(np.kron(SIGMA_X, I2), I2)
    G2 = np.kron(np.kron(SIGMA_Y, SIGMA_X), I2)
    G3 = np.kron(np.kron(SIGMA_Y, SIGMA_Y), SIGMA_X)
    return [G1, G2, G3]


def build_su2_generators(gammas):
    """SU(2) spin generators from Cl(3): S_k = -(i/2) eps_{ijk} G_i G_j."""
    S1 = -0.5j * gammas[1] @ gammas[2]
    S2 = -0.5j * gammas[2] @ gammas[0]
    S3 = -0.5j * gammas[0] @ gammas[1]
    return [S1, S2, S3]


def commutator(A, B):
    return A @ B - B @ A


def anticommutator(A, B):
    return A @ B + B @ A


def is_hermitian(M, tol=1e-10):
    return np.linalg.norm(M - M.conj().T) < tol


def frobenius_inner(A, B):
    """Tr(A^dag B), normalized."""
    return np.trace(A.conj().T @ B)


def algebra_dimension(generators, max_iter=10):
    """Compute the dimension of the Lie algebra generated by commutator closure."""
    basis = []
    for g in generators:
        gn = np.linalg.norm(g)
        if gn < 1e-12:
            continue
        residual = g / gn
        for b in basis:
            ov = frobenius_inner(b, residual) / frobenius_inner(b, b)
            residual = residual - ov * b
        if np.linalg.norm(residual) > 0.1:
            basis.append(residual / np.linalg.norm(residual))

    for iteration in range(max_iter):
        new_els = []
        current = len(basis)
        for i in range(current):
            for j in range(i + 1, current):
                comm = commutator(basis[i], basis[j])
                cn = np.linalg.norm(comm)
                if cn < 1e-10:
                    continue
                comm = comm / cn
                residual = comm.copy()
                for b in basis + new_els:
                    bn = frobenius_inner(b, b).real
                    if bn < 1e-12:
                        continue
                    ov = frobenius_inner(b, residual) / bn
                    residual = residual - ov * b
                if np.linalg.norm(residual) > 0.1:
                    residual = residual / np.linalg.norm(residual)
                    new_els.append(residual)
        if not new_els:
            break
        basis.extend(new_els)

    return len(basis), basis


def check_su_n_closure(generators, n):
    """Check if generators close to su(n). Returns (closes, max_error)."""
    expected_dim = n * n - 1
    dim, basis = algebra_dimension(generators)
    # Check commutator closure error
    max_err = 0.0
    for i in range(len(basis)):
        for j in range(i + 1, len(basis)):
            comm = commutator(basis[i], basis[j])
            residual = comm.copy()
            for b in basis:
                bn = frobenius_inner(b, b).real
                if bn < 1e-12:
                    continue
                ov = frobenius_inner(b, residual) / bn
                residual = residual - ov * b
            err = np.linalg.norm(residual)
            max_err = max(max_err, err)
    closes = (dim == expected_dim) and (max_err < 0.1)
    return closes, max_err, dim


