def attack2_confinement(side=8, beta=2.0, n_configs=50):
    """
    On the lattice, SU(3) confines (area law) while U(1) does not (perimeter law).
    Build Wilson loops using Cl(3) generators restricted to different subgroups
    and test for confinement.

    Method:
    - Generate random gauge configurations for SU(2) and SU(3) link variables
    - Build Wilson loops of various sizes
    - Fit to area law vs perimeter law
    - Compare string tension for different subgroups
    """
    print("\n" + "=" * 80)
    print("ATTACK 2: CONFINEMENT SELECTS SU(3)")
    print("=" * 80)

    results = {}

    # --- Wilson loop measurement ---
    # W(R, T) = <Tr P exp(i g integral A_mu dx^mu)>
    # Area law: W ~ exp(-sigma * R * T)
    # Perimeter law: W ~ exp(-mu * 2(R+T))

    # Generate SU(2) random gauge field on 3D lattice
    print("\n  Generating gauge configurations...")
    n = side ** 3

    def random_su2():
        """Random SU(2) matrix near identity, parametrized by Pauli matrices."""
        eps = 0.3 / beta  # coupling-dependent spread
        a = np.random.randn(3) * eps
        norm = np.sqrt(np.sum(a ** 2))
        if norm > 1e-10:
            a0 = np.sqrt(max(0, 1 - norm ** 2))
            U = a0 * I2 + 1j * (a[0] * SIGMA_X + a[1] * SIGMA_Y + a[2] * SIGMA_Z)
        else:
            U = I2.copy()
        # Ensure unitarity
        U, _ = np.linalg.qr(U)
        if np.linalg.det(U).real < 0:
            U[:, 0] *= -1
        return U

    def random_su3():
        """Random SU(3) matrix near identity."""
        eps = 0.3 / beta
        # Use Gell-Mann parametrization
        a = np.random.randn(8) * eps
        H = sum(a[i] * GELLMANN[i] for i in range(8))
        U = np.eye(3, dtype=complex) + 1j * H - 0.5 * H @ H
        # Project to SU(3) via QR
        U, R = np.linalg.qr(U)
        phase = np.diag(R)
        phase = phase / np.abs(phase)
        U = U @ np.diag(phase)
        det = np.linalg.det(U)
        U = U / (det ** (1.0 / 3))
        return U

    def random_u1():
        """Random U(1) phase."""
        eps = 0.3 / beta
        return np.exp(1j * np.random.randn() * eps)

    # Measure Wilson loops for each group
    for group_name, random_link, dim in [("U(1)", random_u1, 1),
                                          ("SU(2)", random_su2, 2),
                                          ("SU(3)", random_su3, 3)]:
        print(f"\n  --- Wilson loops for {group_name} (dim={dim}) ---")

        # Generate link variables
        # links[x, y, z, mu] = group element on link (x,y,z) -> (x+mu_hat)
        links = {}
        for x in range(side):
            for y in range(side):
                for z in range(side):
                    for mu in range(3):
                        links[(x, y, z, mu)] = random_link()

        def get_link(x, y, z, mu, forward=True):
            """Get link variable, using periodicity."""
            x, y, z = x % side, y % side, z % side
            U = links[(x, y, z, mu)]
            if forward:
                return U
            else:
                if dim == 1:
                    return np.conj(U)
                else:
                    return U.conj().T

        # Measure Wilson loops W(R, T) in the (mu=0, nu=1) plane
        # Average over spatial starting points and configurations
        wilson_data = []
        max_R = min(side // 2, 4)

        for R in range(1, max_R + 1):
            for T in range(1, max_R + 1):
                W_sum = 0.0
                W_count = 0

