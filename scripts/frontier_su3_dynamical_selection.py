                    for key in links:
                        links[key] = random_link()

                    for x0 in range(side):
                        for y0 in range(side):
                            z0 = 0
                            # Build plaquette path: right R, up T, left R, down T
                            if dim == 1:
                                prod = 1.0 + 0j
                            else:
                                prod = np.eye(dim, dtype=complex)

                            # Bottom edge (mu=0 direction, length R)
                            for r in range(R):
                                prod = prod @ get_link(x0 + r, y0, z0, 0) if dim > 1 else prod * get_link(x0 + r, y0, z0, 0)

                            # Right edge (mu=1 direction, length T)
                            for t in range(T):
                                U = get_link(x0 + R, y0 + t, z0, 1)
                                prod = prod @ U if dim > 1 else prod * U

