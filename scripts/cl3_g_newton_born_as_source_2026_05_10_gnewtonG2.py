    rho_coherent_diag = np.diag(rho_coherent_op).real

    # Same diagonal as rho_b_mixed
    diag_match = np.allclose(rho_coherent_diag, rho_b_mixed)
    report(
        "S2.2 coherent superposition |+> = (|0>+|1>)/sqrt(2) has same diag as mixed -- diagonal alone underdetermines the state",
        diag_match,
        f"diag(|+><+|) = {rho_coherent_diag.tolist()} vs diag(mixed) = {rho_b_mixed.tolist()}",
    )

    # But rho_coherent_op != rho_mixed_op (off-diagonal differs)
    states_differ = not np.allclose(rho_coherent_op, rho_mixed_op)
    report(
        "S2.3 coherent superposition state != mixed state (off-diagonals differ)",
        states_differ,
        f"|rho_coherent - rho_mixed|_max = {float(np.max(np.abs(rho_coherent_op - rho_mixed_op))):.6f}",
    )

    # The prior probe's "divergence" reduces to: MAP_a (applied to a pure state
    # that LOOKS LIKE the mixed state's diagonal) gives the same answer as MAP_b
    # applied to the mixed state. So the maps don't "diverge" -- they agree
