    N = 2

    # Pure state on 2-site lattice
    psi_pure = np.array([1.0, 0.0], dtype=complex)
    rho_pure_op = np.outer(psi_pure, psi_pure.conj())

    # Mixed state: 0.5|0><0| + 0.5|1><1|
