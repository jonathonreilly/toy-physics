    norm_ok_mixed = abs(norm_mixed - 1.0) < 1e-12
    report(
        "S1.6 P1.5 mixed-state normalization sum_x rho_grav(x) = Tr rho_hat = 1",
        norm_ok_mixed,
        f"sum = {norm_mixed:.12f}",
    )

    # --- P1.1 Linearity in rho_hat ---
