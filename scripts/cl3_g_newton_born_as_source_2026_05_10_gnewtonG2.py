    pure_agree = np.allclose(rho_a_pure, rho_b_pure)
    report(
        "S2.1 prior probe S4.1 confirmed: MAP_a and MAP_b agree on pure states",
        pure_agree,
        f"|psi|^2 = {rho_a_pure.tolist()}, diag(rho) = {rho_b_pure.tolist()}",
    )

    # MAP_b applied to mixed state
    rho_b_mixed = np.diag(rho_mixed_op).real

    # The prior probe computed rho_b_mixed = [0.5, 0.5] (S4.2) and noted
    # that NO single psi has |psi|^2 = [0.5, 0.5] (would require |psi_0|=|psi_1|
