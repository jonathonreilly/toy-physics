    rho_grav_pure_again = np.diag(rho_pure_op).real
    rho_grav_pure_check = np.diag(rho_pure_op).real
    no_ambiguity_pure = np.allclose(rho_grav_pure_again, rho_grav_pure_check)
    report(
        "S2.4 unified map is well-defined on each fixed state (pure)",
        no_ambiguity_pure,
        "deterministic function of rho_hat",
    )

    rho_grav_mixed_again = np.diag(rho_mixed_op).real
    rho_grav_mixed_check = np.diag(rho_mixed_op).real
    no_ambiguity_mixed = np.allclose(rho_grav_mixed_again, rho_grav_mixed_check)
    report(
        "S2.5 unified map is well-defined on each fixed state (mixed)",
        no_ambiguity_mixed,
        "deterministic function of rho_hat",
    )

    # P2.4: The prior probe's "divergence" was MAP_a vs MAP_b on different
    # states -- a category error. The unified map resolves this.
    # Specifically: MAP_a is undefined on mixed states (no single psi), so
    # comparing it to MAP_b on mixed states is comparing an undefined object
    # to a defined one. The unified map MAP_c = <x|rho_hat|x> is defined for
    # all density operators and reduces to MAP_a on pure states.

    # The category error: prior probe used MAP_a as the "Born map" but
    # gravity sources include mixed states (statistical ensembles). MAP_a
