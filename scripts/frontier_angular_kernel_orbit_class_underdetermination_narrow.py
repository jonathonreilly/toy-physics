from __future__ import annotations

import math
import sys
from itertools import product

import numpy as np
import sympy as sp

PASS_COUNT = 0
FAIL_COUNT = 0


def check(name, condition, detail="", kind="EXACT"):
    global PASS_COUNT, FAIL_COUNT
    status = "PASS" if condition else "FAIL"
    if condition:
        PASS_COUNT += 1
    else:
        FAIL_COUNT += 1
    tag = f" [{kind}]" if kind != "EXACT" else ""
    msg = f"  [{status}]{tag} {name}"
    if detail:
        msg += f"  ({detail})"
    print(msg)
    return condition


# =============================================================================
# Setup: forward-cone neighbour set R(d_max) and D_4 orbit classes O(d_max)
# =============================================================================


def forward_neighbour_set(d_max: int):
    """R(d_max) = { (1, dy, dz) : |dy|, |dz| <= d_max } on Z^3.

    Returned as the list of (dy, dz) layer offsets (dx = +1 implicit).
    """
    return [(dy, dz) for dy in range(-d_max, d_max + 1)
                     for dz in range(-d_max, d_max + 1)]


def orbit_class(dy: int, dz: int):
    """D_4 orbit class identifier (axis-perm + sign-flip).

    The D_4 action on (dy, dz) generated by 90-degree rotation
    (dy, dz) -> (-dz, dy) and reflection (dy, dz) -> (dy, -dz) has
    orbits indexed by the unordered multiset { |dy|, |dz| }.
    """
    a, b = abs(dy), abs(dz)
    return (min(a, b), max(a, b))


def orbit_classes(d_max: int):
    """Set of D_4 orbit classes appearing in R(d_max)."""
    return {orbit_class(dy, dz) for (dy, dz) in forward_neighbour_set(d_max)}


# =============================================================================
# Part A. Orbit-class enumeration
# =============================================================================


def test_part_a_orbit_enumeration():
    print("\n=== Part A: D_4 orbit-class enumeration on R(d_max) ===\n")

    sizes = []
    for d_max in [1, 2, 3, 4, 5]:
        classes = orbit_classes(d_max)
        sizes.append((d_max, len(classes)))
        print(f"  d_max = {d_max}: |R| = {(2*d_max+1)**2:3d}, "
              f"|O(d_max)| = {len(classes):3d}")

    # |O(d_max)| >= 2 for all d_max >= 1
    for d_max, n in sizes:
        check(f"|O(d_max={d_max})| >= 2",
              n >= 2,
              f"got {n} orbit classes")

    # Linear growth: |O(d_max)| <= (d_max+1)(d_max+2)/2 (combinatorial bound)
    for d_max, n in sizes:
        bound = (d_max + 1) * (d_max + 2) // 2
        check(f"|O(d_max={d_max})| matches combinatorial bound (d_max+1)(d_max+2)/2 = {bound}",
              n == bound,
              f"|O| = {n}, expected {bound}")

    # |O(d_max)| is strictly increasing in d_max
    for i in range(1, len(sizes)):
        check(f"|O({sizes[i][0]})| > |O({sizes[i-1][0]})|",
              sizes[i][1] > sizes[i-1][1],
              f"{sizes[i][1]} > {sizes[i-1][1]}")

    return sizes


# =============================================================================
# Part B. Free-parameter count via sympy symbolic algebra
# =============================================================================


def test_part_b_free_parameters():
    print("\n=== Part B: Free-parameter count of cubic-symmetric weight family ===\n")

    d_max = 3
    classes = sorted(orbit_classes(d_max))
    n_classes = len(classes)
    print(f"  d_max = {d_max}: {n_classes} orbit classes: {classes}")

    # Symbolic variables: one weight per orbit class.
    syms = sp.symbols(f"k0:{n_classes}", nonnegative=True)
    sym_lookup = dict(zip(classes, syms))

    # For each (dy, dz) in R, look up the orbit weight symbol.
    R = forward_neighbour_set(d_max)
    w_table = {(dy, dz): sym_lookup[orbit_class(dy, dz)] for (dy, dz) in R}

    # Symbolic total mass
    total = sum(w_table.values())
    print(f"  Symbolic total mass (sum over R of orbit-weighted): "
          f"degree-1 polynomial in {n_classes} symbols.")

    # Verify: total is linear in the n_classes symbols
    poly = sp.Poly(total, *syms)
    check("Symbolic total mass is degree-1 in the orbit-class symbols",
          poly.total_degree() == 1,
          f"degree = {poly.total_degree()}")

    # Verify: each orbit class symbol appears in the total with multiplicity
    # equal to the orbit size (number of (dy, dz) in R with that orbit).
    orbit_size = {}
    for (dy, dz) in R:
        cls = orbit_class(dy, dz)
        orbit_size[cls] = orbit_size.get(cls, 0) + 1
    coeff_map = {sym: poly.coeff_monomial(sym) for sym in syms}
    for cls, sym in sym_lookup.items():
        expected = orbit_size[cls]
        actual = coeff_map[sym]
        check(f"Orbit {cls} contributes {expected} terms to total",
              actual == expected,
              f"coeff of {sym} = {actual}, expected {expected}")

    # The unit-norm constraint sum_R k = 1 is one linear equation in n_classes
    # symbols, so the affine solution space is (n_classes - 1)-dimensional.
    check(f"Free-parameter dimension = |O(d_max=3)| - 1 = {n_classes - 1}",
          n_classes - 1 >= 1,
          f"affine kernel-parameter dim = {n_classes - 1}")

    return classes, syms, sym_lookup


