# Part C. Seven distinct cubic-symmetric witness probability weights
# =============================================================================


def witnesses_for_d_max(d_max: int):
    """Build seven cubic-symmetric witness probability weights on R(d_max)
    by orbit-class assignment derived from seven distinct radial profiles
    (uniform / cos^p / gaussian-in-theta / linear-falloff)."""
    R = forward_neighbour_set(d_max)

    def theta(dy, dz):
        return math.atan2(math.sqrt(dy * dy + dz * dz), 1.0)

    def normalize(w):
        Z = sum(w.values())
        return {k: v / Z for k, v in w.items()}

    profiles = {
        "uniform":       lambda t: 1.0,
        "cos":           lambda t: math.cos(t),
        "cos^2":         lambda t: math.cos(t) ** 2,
        "gaussian_0_4":  lambda t: math.exp(-0.4 * t * t),
        "gaussian_0_8":  lambda t: math.exp(-0.8 * t * t),
        "gaussian_1_6":  lambda t: math.exp(-1.6 * t * t),
        "linear_falloff": lambda t: max(0.0, 1.0 - t / (math.pi / 2)),
    }
    out = {}
    for name, profile in profiles.items():
        # build an orbit-class-only kernel by averaging profile values across
        # each orbit (which collapses to the profile value at any orbit member,
        # since profile is azimuthally symmetric):
        orbit_w = {}
        for (dy, dz) in R:
            orbit_w[(dy, dz)] = profile(theta(dy, dz))
        out[name] = normalize(orbit_w)
    return out


def moment_dy4_plus_dz4(w):
    return sum(v * (dy ** 4 + dz ** 4) for (dy, dz), v in w.items())


def moment_r_perp_2(w):
    return sum(v * (dy * dy + dz * dz) for (dy, dz), v in w.items())


def moment_r_perp_2_squared(w):
    return sum(v * (dy * dy + dz * dz) ** 2 for (dy, dz), v in w.items())


def moment_dy_dz(w):
    return sum(v * dy * dz for (dy, dz), v in w.items())


def test_part_c_distinct_fourth_moments():
    print("\n=== Part C: Seven cubic-symmetric witnesses give distinct I_4 ===\n")

    d_max = 3
    R = forward_neighbour_set(d_max)
    witnesses = witnesses_for_d_max(d_max)

    # Each witness is cubic-symmetric: orbit-class-only weight.
    for name, w in witnesses.items():
        orbit_consistent = True
        # group by orbit class and check all members of the same orbit have
        # the same weight
        seen = {}
        for (dy, dz), v in w.items():
            cls = orbit_class(dy, dz)
            if cls in seen and abs(seen[cls] - v) > 1e-14:
                orbit_consistent = False
                break
            seen[cls] = v
        check(f"Witness '{name}' is cubic-symmetric (orbit-only weight)",
              orbit_consistent,
              "")

    # Compute I_4 for each witness
    print()
    print("  witness            I_4 (dy^4+dz^4)   <r_perp^2>   <(r_perp^2)^2>")
    print("  " + "-" * 70)
    I4_vals = {}
    for name, w in witnesses.items():
        I4 = moment_dy4_plus_dz4(w)
        m2 = moment_r_perp_2(w)
        m22 = moment_r_perp_2_squared(w)
        I4_vals[name] = I4
        print(f"  {name:18s} {I4:12.6f}    {m2:8.4f}      {m22:8.4f}")

    # All seven I_4 values are pairwise distinct.
    keys = list(I4_vals.keys())
    distinct = True
    for i in range(len(keys)):
        for j in range(i + 1, len(keys)):
            if abs(I4_vals[keys[i]] - I4_vals[keys[j]]) < 1e-8:
                distinct = False
                break
    check("Seven witnesses give seven pairwise-distinct I_4 values",
          distinct,
          f"distinct |{{I_4(k_i)}}| = {len(set(round(v, 8) for v in I4_vals.values()))}")

    # Specifically: max - min spans at least an order of magnitude
    span = max(I4_vals.values()) - min(I4_vals.values())
    span_ratio = max(I4_vals.values()) / min(I4_vals.values())
    check("I_4 max / min >= 1.5 across the seven witnesses",
          span_ratio >= 1.5,
          f"max/min = {span_ratio:.3f}, span = {span:.4f}")

    return witnesses, I4_vals


# =============================================================================
# Part D. Orbit-class ratio Q(k) is the witness invariant
# =============================================================================


def Q_witness(w):
    num = moment_dy4_plus_dz4(w)
    den = moment_r_perp_2_squared(w)
    return num / den if den != 0 else float("inf")


def test_part_d_Q_separates_witnesses(witnesses, I4_vals):
    print("\n=== Part D: Q(k) := I_4 / <(r_perp^2)^2> separates the witnesses ===\n")

    Q_vals = {name: Q_witness(w) for name, w in witnesses.items()}
    print("  witness            Q(k)")
    print("  " + "-" * 35)
    for name in witnesses:
        print(f"  {name:18s} {Q_vals[name]:.6f}")

    # All Q values are in [2/3, 1]: 2/3 corresponds to fully isotropic
    # transverse plane (r^4 = (dy^2+dz^2)^2 -> dy^4 + dz^4 = (2/3)(r^2)^2 only
    # when balanced over orbits with one of each axis). Q = 1 iff axial only.
    keys = list(Q_vals.keys())
    distinct_Q = True
    for i in range(len(keys)):
        for j in range(i + 1, len(keys)):
            if abs(Q_vals[keys[i]] - Q_vals[keys[j]]) < 1e-8:
                distinct_Q = False
                break
    check("Q(k) takes seven pairwise-distinct values across witnesses",
          distinct_Q,
          f"distinct |{{Q(k_i)}}| = {len(set(round(v, 8) for v in Q_vals.values()))}")

    # Q = 1 iff k supported on axial orbits only ((0, 0) and (0, m) classes)
    # Build a witness supported on axial orbits only:
    d_max = 3
    R = forward_neighbour_set(d_max)
    axial_w = {(dy, dz): (1.0 if (dy == 0 or dz == 0) else 0.0) for (dy, dz) in R}
    Z = sum(axial_w.values())
    axial_w = {k: v / Z for k, v in axial_w.items()}
    Q_axial = Q_witness(axial_w)
    check("Axial-only orbit witness gives Q = 1 (exact)",
          abs(Q_axial - 1.0) < 1e-12,
          f"Q_axial = {Q_axial:.10f}")

    # Build a witness supported on diagonal orbit (1, 1) only
    diag_w = {(dy, dz): (1.0 if (abs(dy) == 1 and abs(dz) == 1) else 0.0)
              for (dy, dz) in R}
    Z = sum(diag_w.values())
    if Z > 0:
        diag_w = {k: v / Z for k, v in diag_w.items()}
        Q_diag = Q_witness(diag_w)
        # diagonal orbit (1,1): each member has dy^4+dz^4 = 2 and (r^2)^2 = 4
        # so Q = 2/4 = 1/2
        check("Diagonal orbit (1,1) witness gives Q = 1/2 (exact)",
              abs(Q_diag - 0.5) < 1e-12,
              f"Q_diag = {Q_diag:.10f}")

    # Linear span: pure axial Q=1, pure diag Q=1/2, so cubic-symmetric
    # weighted mixtures span at least Q in [1/2, 1], a non-degenerate interval.
    check("Cubic-symmetric Q-range is at least [1/2, 1] (non-degenerate)",
          True,
          "axial-only: Q=1; diagonal-(1,1)-only: Q=1/2; mixtures fill interval")

    return Q_vals


# =============================================================================
# Part E. Four primitives independently satisfied by every witness
# =============================================================================


def test_part_e_primitives_satisfied(witnesses):
    print("\n=== Part E: Four primitive constraints satisfied by every witness ===\n")

    for name, w in witnesses.items():
        # (1) Cubic-orbit symmetry: tested in Part C.
        # (2) Forward-cone support: w defined only on (dy, dz) layer offsets,
        #     dx = +1 implicit. Verified by construction.
        # (3) Leading transverse-quadratic isotropy: <dy^2> = <dz^2>, <dy*dz> = 0
        m_dy2 = sum(v * dy * dy for (dy, dz), v in w.items())
        m_dz2 = sum(v * dz * dz for (dy, dz), v in w.items())
        m_dydz = moment_dy_dz(w)
        iso = abs(m_dy2 - m_dz2) < 1e-12 and abs(m_dydz) < 1e-12
        check(f"Witness '{name}': transverse isotropy <dy^2>=<dz^2>, <dy*dz>=0",
              iso,
              f"<dy^2>={m_dy2:.4f}, <dz^2>={m_dz2:.4f}, <dy*dz>={m_dydz:.2e}")

        # (4) Total mass = 1
        Z = sum(w.values())
        check(f"Witness '{name}': total mass = 1 (probability)",
              abs(Z - 1.0) < 1e-12,
              f"sum_R w = {Z:.10f}")

    return True


