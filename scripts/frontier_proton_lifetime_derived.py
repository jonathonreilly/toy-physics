    print("STEP 3: LEPTOQUARK OPERATORS IN THE FULL Cl(3) ALGEBRA")
    print("=" * 78)

    paulis = [I2, SIGMA_X, SIGMA_Y, SIGMA_Z]
    pauli_names = ['0', 'x', 'y', 'z']

    P_quark = P_T1 + P_T2
    P_lepton = P_S0 + P_S3

    n_leptoquark = 0
    n_gauge_like = 0
    n_total = 0
    lq_examples = []

    for i1, p1 in enumerate(paulis):
        for i2, p2 in enumerate(paulis):
            for i3, p3 in enumerate(paulis):
                n_total += 1
                name = f"{pauli_names[i1]}{pauli_names[i2]}{pauli_names[i3]}"
                op = np.kron(np.kron(p1, p2), p3)

                # Off-diagonal (quark<->lepton) block norms
                ql = np.linalg.norm(P_quark @ op @ P_lepton)
                lq = np.linalg.norm(P_lepton @ op @ P_quark)

                if ql > 1e-10 or lq > 1e-10:
                    n_leptoquark += 1
                    if len(lq_examples) < 5:
                        lq_examples.append((name, ql, lq))
                else:
                    n_gauge_like += 1

    print(f"\n  Total operators in (sigma_a x sigma_b x sigma_c): {n_total}")
    print(f"  Operators mixing quark <-> lepton subspaces:       {n_leptoquark}")
    print(f"  Operators preserving subspaces (gauge-like):       {n_gauge_like}")

    check(n_leptoquark > 0,
          f"Leptoquark operators exist ({n_leptoquark} found)")
    check(n_gauge_like > 0,
          f"Gauge-like (subspace-preserving) operators exist ({n_gauge_like} found)")

    print(f"\n  Example leptoquark operators (first 5):")
    for name, ql, lq in lq_examples:
        print(f"    {name}:  ||quark->lepton|| = {ql:.4f}, ||lepton->quark|| = {lq:.4f}")

    print(f"\n  These operators connect quarks (T1/T2) to leptons (S0/S3).")
    print(f"  They are the framework's analog of GUT leptoquark (X, Y) bosons.")
    print(f"  They exist in the full Cl(3) algebra but NOT in the gauge sector.")

    return n_leptoquark


