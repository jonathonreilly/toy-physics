def taste_vector(s):
    """Map taste state (s1,s2,s3) to 8-dim basis vector in (C^2)^3."""
    v = np.array([1.0, 0.0]) if s[0] == 0 else np.array([0.0, 1.0])
    for k in (1, 2):
        vk = np.array([1.0, 0.0]) if s[k] == 0 else np.array([0.0, 1.0])
        v = np.kron(v, vk)
    return v.astype(complex)


def projector(states):
    """Build projector onto span of taste vectors for given states."""
    P = np.zeros((8, 8), dtype=complex)
    for s in states:
        v = taste_vector(s)
        P += np.outer(v, v.conj())
    return P


