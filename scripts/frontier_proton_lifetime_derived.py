def step2_gauge_preserves_subspaces(P_S0, P_T1, P_T2, P_S3):
    """
    Verify that SU(3) generators act within T1 (and T2) and that SU(2)
    generators act within the taste space in a way that preserves the
    Hamming-weight grading when restricted to gauge interactions.
    """
    print("\n" + "=" * 78)
    print("STEP 2: GAUGE ALGEBRA PRESERVES HAMMING-WEIGHT SUBSPACES")
    print("=" * 78)

    P_quark = P_T1 + P_T2   # quark sector (6-dim)
    P_lepton = P_S0 + P_S3  # lepton sector (2-dim)

    # Build SU(3) generators in the T1 subspace (Gell-Mann-like)
    t1v = [taste_vector(s) for s in T1]
    su3_gens = []
    for i in range(3):
        for j in range(i + 1, 3):
            su3_gens.append(np.outer(t1v[i], t1v[j].conj()) +
                            np.outer(t1v[j], t1v[i].conj()))
            su3_gens.append(-1j * np.outer(t1v[i], t1v[j].conj()) +
                            1j * np.outer(t1v[j], t1v[i].conj()))
    su3_gens.append(np.outer(t1v[0], t1v[0].conj()) -
                    np.outer(t1v[1], t1v[1].conj()))
    su3_gens.append((np.outer(t1v[0], t1v[0].conj()) +
                     np.outer(t1v[1], t1v[1].conj()) -
                     2 * np.outer(t1v[2], t1v[2].conj())) / np.sqrt(3))

    print(f"\n  SU(3) generators (8 Gell-Mann-like on T1):")
    su3_ok = True
    for k, G in enumerate(su3_gens):
        # G should have zero lepton block
        off = np.linalg.norm(P_lepton @ G @ P_quark) + np.linalg.norm(P_quark @ G @ P_lepton)
        if off > 1e-12:
            su3_ok = False
    check(su3_ok, "All 8 SU(3) generators have zero quark<->lepton block")

    # Build the baryon number operator
    B_op = np.zeros((8, 8), dtype=complex)
    for s in TASTE_STATES:
        idx = TASTE_STATES.index(s)
        if s in T1 or s in T2:
            B_op[idx, idx] = 1.0 / 3.0

    # Check [B, SU(3)] = 0
    commutes_su3 = all(
        np.linalg.norm(B_op @ G - G @ B_op) < 1e-12 for G in su3_gens
    )
    check(commutes_su3, "[B, SU(3)_a] = 0  for all a = 1..8")

    return B_op, su3_gens


