omega = np.exp(2j * np.pi / 3)
omega_bar = omega.conjugate()

# C_3 cyclic shift on C^3: C maps e_i -> e_{i+1 mod 3}
C = np.zeros((3, 3), dtype=complex)
C[1, 0] = C[2, 1] = C[0, 2] = 1.0
C2 = C @ C
I3 = np.eye(3, dtype=complex)

# Pauli matrices (Cl(3) per-site generators)
sigma_0 = np.eye(2, dtype=complex)
sigma_1 = np.array([[0, 1], [1, 0]], dtype=complex)
sigma_2 = np.array([[0, -1j], [1j, 0]], dtype=complex)
sigma_3 = np.array([[1, 0], [0, -1]], dtype=complex)


def hermitian_circulant(a: float, b: complex) -> np.ndarray:
    """H = aI + bC + b̄C^2 (Hermitian circulant on C^3)."""
    return a * I3 + b * C + np.conj(b) * C2


def conjugation(X: np.ndarray, U: np.ndarray) -> np.ndarray:
    """Algebra-automorphism action: alpha_U(X) = U X U^*."""
    return U @ X @ U.conj().T


def conditional_expectation_C3(X: np.ndarray) -> np.ndarray:
    """E(X) = (1/3) sum_g alpha_{C^g}(X) — projection onto circulants."""
    return (X + C @ X @ C.conj().T + C2 @ X @ C2.conj().T) / 3.0


def is_circulant(X: np.ndarray, tol: float = 1e-10) -> bool:
    """Check if X is in span{I, C, C^2}."""
    a = np.trace(X) / 3.0
    b = np.trace(X @ C2) / 3.0
    bbar = np.trace(X @ C) / 3.0
    reconstr = a * I3 + b * C + bbar * C2
    return bool(np.allclose(X, reconstr, atol=tol))


def is_hermitian(X: np.ndarray, tol: float = 1e-10) -> bool:
    return bool(np.allclose(X, X.conj().T, atol=tol))


def commutes_with_C3(U: np.ndarray, tol: float = 1e-10) -> bool:
    return bool(np.allclose(U @ C, C @ U, atol=tol))


def fourier_basis() -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    """Eigenbasis of C: |chi_1>, |chi_omega>, |chi_omega_bar>.
    C |chi_chi> = chi |chi_chi>."""
    v1 = np.array([1, 1, 1], dtype=complex) / np.sqrt(3)
    vw = np.array([1, omega, omega_bar], dtype=complex) / np.sqrt(3)
    vwb = np.array([1, omega_bar, omega], dtype=complex) / np.sqrt(3)
    return v1, vw, vwb


