def section7_structural_obstruction() -> None:
    section("Section 7: Structural obstruction theorem")

    # Theorem (proven inline):
    #
    # Claim: ANY continuous one-parameter unitary group {U(theta)} acting on
    # H_hw=1 = C^3 lifts to End(H_hw=1) = M_3(C) ONLY by conjugation
    # alpha_{U(theta)}(X) = U(theta) · X · U(theta)^*. There is no non-
    # conjugation lift accessible from a single state-space unitary group.
    #
    # Proof: Let phi: U(1) -> Aut(M_3(C)) be a continuous group homomorphism
    # induced from a single Hilbert-space U(1) action. Then phi(theta) acts
    # on M_3(C) as a *-algebra automorphism (preserving products and adjoints).
    # By the Skolem-Noether theorem (every *-automorphism of M_n(C) is inner),
    # phi(theta)(X) = U_alpha(theta) X U_alpha(theta)^* for some unitary
    # U_alpha(theta) on C^3, depending continuously on theta.
    #
    # If the action is induced by a single Hilbert-space U(theta), then
    # U_alpha(theta) = U(theta) up to a phase. The phase ambiguity does not
    # affect the conjugation action.
    #
    # Hence: any single-Hilbert-space-U(1) lift to M_3(C) is conjugation.
    # Probe 14 covers conjugation by C_3-commuting U: trivial on A^{C_3}.
    # So no closure of A1.
    #
    # The "non-conjugation" lifts attempted (V2 left-module, V3 twisted
    # conjugation, V4 pseudoscalar twist, V5 spatial phase) either:
    #   (a) fail to be group homomorphisms (V2: not multiplicative);
    #   (b) fail to preserve the algebra structure of M_3(C) (V3: not unital,
    #       not multiplicative, not Hermiticity-preserving);
    #   (c) collapse to scalar action / trivial conjugation (V4: pseudoscalar
    #       central; V5: C_3-symmetrized spatial = scalar);
    #   (d) require a structure that is NOT supplied by retained per-site
    #       qubit U(1) on H_lat (V1: site-by-site C_3-character grading is
    #       NOT a retained operation if we demand C_3-equivariance).

    # Verification 7.1: Skolem-Noether holds on M_3(C).
    # Pick a random *-automorphism by conjugation, verify it equals U X U^*.
    np.random.seed(17)
    Q, _ = np.linalg.qr(np.random.randn(3, 3) + 1j * np.random.randn(3, 3))
    X = hermitian_circulant(1.0, 0.5 + 0.3j)
    Y = hermitian_circulant(0.7, 0.2 + 0.6j)
    aut_X = Q @ X @ Q.conj().T
    aut_Y = Q @ Y @ Q.conj().T
    aut_XY = Q @ (X @ Y) @ Q.conj().T
    check("7.1a Conjugation by Q preserves multiplication: aut(XY) = aut(X) aut(Y)",
          np.allclose(aut_XY, aut_X @ aut_Y))
    check("7.1b Conjugation by Q preserves adjoint: aut(X^*) = aut(X)^*",
          np.allclose(Q @ X.conj().T @ Q.conj().T, (Q @ X @ Q.conj().T).conj().T))

    # Verification 7.2: The Skolem-Noether constraint forces the lift.
    # If we require:
    #   (A) phi(theta) is a *-algebra automorphism of M_3(C),
    #   (B) phi(theta) is continuous in theta,
    #   (C) phi forms a 1-parameter group: phi(s+t) = phi(s) phi(t),
    # then phi(theta) = conjugation by some U(theta) in U(3)/U(1)_phase.

    # Verification 7.3: Probe 14 covers U(theta) commuting with C_3.
    # Build the maximal C_3-commuting U(1) inside U(3): the Fourier-diagonal
    # subgroup. Its restriction to A^{C_3} acts as scalar multiplication on
    # each character isotype (= 3 independent phases on chi_1, chi_omega,
    # chi_omega_bar).
    F = np.column_stack(fourier_basis())
    # An arbitrary C_3-commuting U(theta) = F · diag(e^{i a theta}, e^{i b theta},
    # e^{i c theta}) · F^*.
    a_p, b_p, c_p = 0.0, 0.7, -0.4
    theta = 0.6
    U_C3eq = F @ np.diag(np.exp(1j * theta * np.array([a_p, b_p, c_p]))) @ F.conj().T
    check("7.3a U_C3eq commutes with C",
          commutes_with_C3(U_C3eq))
    check("7.3b U_C3eq is unitary",
          np.allclose(U_C3eq @ U_C3eq.conj().T, I3))

    # Verification 7.4: Conjugation by U_C3eq ON CIRCULANTS = TRIVIAL,
    # because U_C3eq is itself in the circulant algebra A^{C_3} (which is
    # commutative as an algebra over C).
    H = hermitian_circulant(1.0, 0.5 + 0.3j)
    H_after = conjugation(H, U_C3eq)
    check("7.4 Conjugation by C_3-commuting U is TRIVIAL on circulants (A^{C_3} commutative)",
          np.allclose(H_after, H))

    # Verification 7.5: Conjugation by NON-C_3-commuting U breaks A^{C_3}.
    # Pick U not commuting with C, conjugate H, show result is non-circulant.
    np.random.seed(18)
    Mat = np.random.randn(3, 3) + 1j * np.random.randn(3, 3)
    Q2, _ = np.linalg.qr(Mat)
    while commutes_with_C3(Q2):
        Mat = np.random.randn(3, 3) + 1j * np.random.randn(3, 3)
        Q2, _ = np.linalg.qr(Mat)
    H_break = conjugation(H, Q2)
    check("7.5 Conjugation by NON-C_3-commuting Q breaks A^{C_3} (not circulant)",
          not is_circulant(H_break))

    # Verification 7.6: The U(1)_b symmetry (b -> e^{i phi} b, a -> a) on
    # circulants H = aI + bC + b̄C² acts in Fourier basis as:
    #   diag(a + 2 Re(b),
    #        a + b·omega + b̄·omega_bar,
    #        a + b·omega_bar + b̄·omega)
    # Under b -> e^{i phi} b:
    #   lambda_1 -> a + 2 Re(e^{i phi} b) = a + 2|b|cos(phi + arg b)
    #   lambda_w -> a + e^{i phi} b·omega + e^{-i phi} b̄·omega_bar
    #             = 2 Re(a/2 + e^{i phi} b · omega)
    #   lambda_wb -> similar
    # KEY OBSERVATION: U(1)_b acts NON-LINEARLY on the eigenvalues
    # (lambda_1, lambda_w, lambda_wb). It is NOT a linear *-automorphism
    # of M_3(C). It IS a linear automorphism of A^{C_3} as a 4-real-dim
    # vector space (parametrized by (a, Re b, Im b)).

    # Test directly: U(1)_b on (a, b)
    a_val = 1.0
    b_val = 0.5 + 0.3j
    phi = 0.7
    H_orig = hermitian_circulant(a_val, b_val)
    H_rot = hermitian_circulant(a_val, b_val * np.exp(1j * phi))
    eigs_orig = sorted(np.linalg.eigvals(H_orig).real)
    eigs_rot = sorted(np.linalg.eigvals(H_rot).real)
    print(f"  diag info: U(1)_b rotation by phi={phi}: ")
    print(f"    eigs(H_orig) = {eigs_orig}")
    print(f"    eigs(H_rot)  = {eigs_rot}")
    # The eigenvalues CHANGE (different multiset), confirming U(1)_b is
    # NOT a unitary similarity (which would preserve the spectrum).
    check("7.6 U(1)_b on (a, b) does NOT preserve the spectrum of H",
          not np.allclose(eigs_orig, eigs_rot))

