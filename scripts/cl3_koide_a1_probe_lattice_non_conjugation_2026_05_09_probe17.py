    # "pseudoscalar-twisted" combination omega_x ⊗ omega_y ⊗ ... = i^N
    # = ±1 or ±i. In particular, omega^N is in the global Z_4 center of
    # the per-site Pauli group, NOT a continuous U(1).
    #
    # Combining omega with continuous per-site phase gives:
    #   exp(i theta omega/2) on per-site C^2 = exp(i theta · i / 2) · I
    #     = exp(-theta/2) · I (NOT unitary unless theta = 0!)
    # Because omega = i·I has imaginary "value", the exponentiation gives
    # a real-decay factor, not a phase.
    #
    # To make a unitary, we'd need omega-ITSELF, not its scalar value:
    #   exp(i theta omega) where omega is treated as the algebra element
    #   omega = i·I. Then exp(i theta · i I) = exp(-theta) · I, again non-unitary.

    # Test 5.3: Pseudoscalar twist of conjugation: alpha_{omega^k}(X) = omega^k X omega^{-k}.
    # Since omega = i·I is central, omega^k X omega^{-k} = X for all k.
