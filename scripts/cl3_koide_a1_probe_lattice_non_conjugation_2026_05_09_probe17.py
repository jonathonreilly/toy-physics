    # Note: actually on H_hw=1 (3-dim), omega's representation is determined by
    # the 3-dim faithful representation of Cl(3) -- but Cl(3) has no faithful
    # 3-dim irrep (only 2-dim Pauli or its parity conjugate). So omega on H_hw=1
    # acts as a SCALAR (since hw=1 is 1-fermion-number sector, with definite
    # chirality / pseudoscalar charge).
    X = hermitian_circulant(1.0, 0.5 + 0.3j)
    check("5.3 Pseudoscalar conjugation alpha_omega(X) = omega X omega^{-1} = X (trivial since omega scalar)",
          np.allclose(omega_lifted @ X @ omega_lifted.conj().T / np.abs(np.linalg.det(omega_lifted))**(2/3),
                      X))

    # More directly: omega_lifted = i·I commutes with everything, so conjugation
    # is trivial.
    check("5.4 omega·X = X·omega (omega is scalar, hence central)",
          np.allclose(omega_lifted @ X, X @ omega_lifted))

