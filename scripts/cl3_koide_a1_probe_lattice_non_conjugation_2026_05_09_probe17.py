    # one site in direction d. On corner |X_k> = |hw=1 mode k>, T_d |X_k>
    # acts as a phase e^{i k · d} = e^{i pi n_k,d}. Per the M_3(C) generation
    # in THREE_GENERATION_OBSERVABLE_THEOREM_NOTE:
    #   T_x: diag(-1, +1, +1), T_y: diag(+1, -1, +1), T_z: diag(+1, +1, -1)
    T_x = np.diag([-1.0, 1.0, 1.0]).astype(complex)
    T_y = np.diag([1.0, -1.0, 1.0]).astype(complex)
    T_z = np.diag([1.0, 1.0, -1.0]).astype(complex)
    check("6.1a T_x is unitary",
          np.allclose(T_x @ T_x.conj().T, I3))
    check("6.1b T_x · T_y · T_z = -I (sublattice parity character at hw=1)",
          np.allclose(T_x @ T_y @ T_z, -I3))

    # Test 6.2: The FRACTIONAL/CONTINUOUS-theta version exp(i theta · n_x · sigma_3 / 2)
    # at lattice site x acts as exp(i theta · k · n_x) on momentum-eigenstate |k>.
