    # SL(2,R) hyperbolic flow, not a U(1).
    theta = 0.4
    twist_op = np.cos(1j * theta / 2) * sigma_0 + np.sin(1j * theta / 2) * sigma_3
    # Equivalent: cos(i theta/2) = cosh(theta/2), sin(i theta/2) = i sinh(theta/2)
    expected_twist = np.cosh(theta / 2) * sigma_0 - np.sinh(theta / 2) * sigma_3
    # WAIT — let me recompute. exp(-theta sigma_3 / 2):
    expected_twist_correct = np.diag([np.exp(-theta / 2), np.exp(theta / 2)]).astype(complex)
    check("5.5 Pseudoscalar-twisted per-site U(1) is NON-UNITARY (hyperbolic flow)",
          not np.allclose(expected_twist_correct @ expected_twist_correct.conj().T, sigma_0))

    print("  >> V4 verdict: pseudoscalar omega = e_1 e_2 e_3 = i·I is CENTRAL")
    print("     and SCALAR on the framework's faithful Cl(3) irreps. Pseudoscalar-")
    print("     twisted conjugation is therefore TRIVIAL (central elements commute).")
    print("     Pseudoscalar-twisted exponentiation gives non-unitary hyperbolic flow,")
    print("     not a U(1). Either way, V4 cannot supply U(1)_b.")


