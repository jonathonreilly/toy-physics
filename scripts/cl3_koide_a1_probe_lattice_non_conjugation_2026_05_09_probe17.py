    # for example.
    # Translating the WHOLE lattice by theta in direction n_0 = e_1 gives
    # |k> -> e^{i k_1 theta} |k>, which is JUST a phase on momentum eigenstates.
    # Restricted to hw=1 this gives:
    #   |X_1 = (pi,0,0)> -> e^{i pi theta} |X_1>  (IF this stays in hw=1)
    #   |X_2 = (0,pi,0)> -> e^{0} |X_2> = |X_2>
    #   |X_3 = (0,0,pi)> -> e^{0} |X_3> = |X_3>
    # So the action U_theta = diag(e^{i pi theta}, 1, 1) on hw=1 = C^3.

    # Test 6.2: This U_theta does NOT commute with C_3.
    theta = 0.5
    U_shift = np.diag([np.exp(1j * np.pi * theta), 1.0, 1.0]).astype(complex)
    check("6.2a U_shift is unitary",
          np.allclose(U_shift @ U_shift.conj().T, I3))
    check("6.2b U_shift does NOT commute with C_3",
          not commutes_with_C3(U_shift))

    # Test 6.3: Conjugation by U_shift on circulants takes them OUT of A^{C_3}.
    H = hermitian_circulant(1.0, 0.5 + 0.3j)
    H_after_shift = conjugation(H, U_shift)
    check("6.3 conjugation by U_shift on circulant breaks A^{C_3} structure",
          not is_circulant(H_after_shift))

