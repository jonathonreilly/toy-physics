def section6_V5_spatial_phase_lift() -> None:
    section("Section 6: V5 — Spatial site-position-dependent phase")

    # Proposal: per-site phase exp(i theta · n) with n indexing site location
    # gives a "shift" rather than conjugation. On lattice fermion modes,
    # this is a momentum-shift gauge transformation:
    #   |k> -> |k + theta·n_0> for some direction n_0.
    #
    # On the FINITE BZ corner space {(n_1 pi, n_2 pi, n_3 pi) : n_i in {0,1}},
    # there are only 8 corners. A continuous-theta shift either:
    #   (i) preserves all corners (= integer multiple of pi shift, theta = m pi):
    #       discrete Z_2 action per direction, NOT a U(1).
    #   (ii) takes corners off the discrete set:
    #       not a well-defined action on H_hw=1 = span{X_1, X_2, X_3}.

