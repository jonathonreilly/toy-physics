    # all three direction shifts symmetrically: U_full(theta) = T_x^theta T_y^theta T_z^theta.
    # In matrix form on hw=1: T_x · T_y · T_z = diag(-1, -1, -1) = -I (already
    # verified at 6.1b). For continuous theta:
    #   U_full(theta) = diag((-1)^theta, (-1)^theta, (-1)^theta) = (-1)^theta · I.
    # This is a SCALAR. Trivial on M_3(C).

    # Verify:
    sign_theta = np.exp(1j * np.pi * theta)  # (-1)^theta = e^{i pi theta}
    U_full = sign_theta * I3
    H_after_full = conjugation(H, U_full)
    check("6.4 C_3-symmetrized spatial shift = scalar = trivial conjugation on M_3(C)",
          np.allclose(H_after_full, H))

    # Test 6.5: No matter how we choose "site indexing function n", the
    # combined per-site phase U(theta) = ⊗_x exp(i theta · n(x) · sigma_3 / 2)
    # restricted to hw=1 acts as a UNITARY on C^3. Any unitary on C^3 that
    # is C_3-EQUIVARIANT (commutes with C) is itself in the circulant
    # algebra A^{C_3} (Schur). Conjugation by such a unitary is TRIVIAL on
    # A^{C_3} (Probe 14). So V5 collapses back to Probe 14's domain.

    print("  >> V5 verdict: spatial-site-position phase gives a unitary on")
    print("     H_hw=1 = C^3. Either C_3-equivariant (-> circulant unitary ->")
    print("     trivial conjugation on A^{C_3}) or non-equivariant (-> breaks")
    print("     A^{C_3} structure, action is not on circulants). Cannot supply U(1)_b.")


