# Section 4: V3 — Twisted conjugation X -> UXV^* with U != V
# ----------------------------------------------------------------------


def section4_V3_twisted_conjugation() -> None:
    section("Section 4: V3 — Twisted conjugation with U != V")

    # Twisted conjugation: alpha_{U,V}(X) = U · X · V^*.
    # For U = V this is conjugation. For U != V it is generally NOT an
    # algebra-automorphism.
    #
    # On A^{C_3}, alpha_{U,V}(I) = U V^*. For the action to fix the unit
    # element (i.e., be unital), we need U V^* = I, i.e., U = V. That's
    # conjugation.
    #
    # If we DROP the unitality requirement, alpha_{U,V} is a linear map
    # M_3(C) -> M_3(C) but not an algebra-action; in particular, it's not
    # a group homomorphism for the U(1) group structure. So it doesn't
    # define a U(1) ACTION.

    # Test 4.1: For the twisted action to define a 1-parameter group,
    # we need U_theta V_theta^* = (U_1 V_1^*)^theta = something with
    # group structure. Setting W := U V^*, the action X -> W · X · I = W X
    # is just LEFT multiplication by W (the V^* on the right cancels with
    # the requirement that each action is X -> something X something^*).
    # This is just V2 (left-module) again.

    # Symbolic statement: alpha_{U,V}(X) = U X V^*. For this to be a
    # group action of theta -> alpha_{U(theta), V(theta)}, we need
    # alpha_{U(theta_1), V(theta_1)} ∘ alpha_{U(theta_2), V(theta_2)}
    #   = alpha_{U(theta_1+theta_2), V(theta_1+theta_2)}.
    # Compute LHS:
    #   alpha_{U_1,V_1}(alpha_{U_2,V_2}(X))
    #   = U_1 · (U_2 X V_2^*) · V_1^*
    #   = (U_1 U_2) X (V_1 V_2)^*
    # So composition gives multiplicative U_1 U_2 and V_1 V_2, which IS a
    # group action provided U(theta) and V(theta) are 1-parameter subgroups
    # of U(3) (which they are, individually).
    # Verify numerically:
    theta1, theta2 = 0.4, 0.7
    # Define an example U,V acting in the eigenbasis of C
    F = np.column_stack(fourier_basis())
    # U(theta) = diag(e^{i a theta}, e^{i b theta}, e^{i c theta}) in Fourier basis
    a_U, b_U, c_U = 0.0, 1.0, -1.0  # so U(theta) = diag(1, e^{i theta}, e^{-i theta})
    a_V, b_V, c_V = 0.0, -1.0, 1.0  # so V(theta) = diag(1, e^{-i theta}, e^{i theta})

    def U_theta(t: float) -> np.ndarray:
        return F @ np.diag(np.exp(1j * t * np.array([a_U, b_U, c_U]))) @ F.conj().T

    def V_theta(t: float) -> np.ndarray:
        return F @ np.diag(np.exp(1j * t * np.array([a_V, b_V, c_V]))) @ F.conj().T

    def twisted(X: np.ndarray, t: float) -> np.ndarray:
        return U_theta(t) @ X @ V_theta(t).conj().T

    X = hermitian_circulant(1.0, 0.5 + 0.3j)
    LHS = twisted(twisted(X, theta2), theta1)
    RHS = twisted(X, theta1 + theta2)
    check("4.1 Twisted conjugation IS a 1-parameter group action (associative)",
          np.allclose(LHS, RHS))

    # Test 4.2: The fixed-element property — does twisted conjugation send
    # I to itself? alpha_{U,V}(I) = U I V^* = U V^*.
    # For our (a_U, b_U, c_U) = (0, 1, -1) and (a_V, b_V, c_V) = (0, -1, 1):
    # U V^* in Fourier basis = diag(1, e^{i theta}, e^{-i theta}) ·
    #                          diag(1, e^{i theta}, e^{-i theta}) = diag(1, e^{2i theta}, e^{-2i theta})
    # which is NOT the identity for theta != 0.
    UV_dag = U_theta(0.5) @ V_theta(0.5).conj().T
    check("4.2 Twisted action does NOT preserve I (alpha_{U,V}(I) = UV^* != I)",
          not np.allclose(UV_dag, I3))

    # Test 4.3: The twisted action does NOT preserve the algebra product:
    # alpha_{U,V}(AB) = U A B V^*
    # alpha_{U,V}(A) · alpha_{U,V}(B) = U A V^* · U B V^* = U A (V^* U) B V^*
    # These are equal iff V^* U commutes with B for all B, i.e., V^* U = scalar.
    # That requires U = lambda V for some scalar lambda — back to conjugation
    # times scalar.
    A = hermitian_circulant(0.7, 0.4 + 0.1j)
    B = hermitian_circulant(0.3, 0.2 - 0.5j)
    lhs = twisted(A @ B, 0.6)
    rhs = twisted(A, 0.6) @ twisted(B, 0.6)
    check("4.3 Twisted action is NOT multiplicative (NOT an algebra-automorphism)",
          not np.allclose(lhs, rhs))

    # Test 4.4: Whether twisted conjugation acts as U(1)_b on (a, b).
    # On A^{C_3} ≅ C^3 (Fourier basis), twisted by our example:
    #   U(theta) = diag(1, e^{i theta}, e^{-i theta})
    #   V^*(theta) = diag(1, e^{i theta}, e^{-i theta})  (since V = its conjugate)
    # In Fourier basis, X (a circulant) is diag(lambda_1, lambda_w, lambda_wb).
    # Then twisted(X, theta) in Fourier basis = U X V^*
    #   = diag(lambda_1, lambda_w · e^{2i theta}, lambda_wb · e^{-2i theta})
    # This MULTIPLIES eigenvalues at characters omega, omega_bar by phases.
    # But these are EIGENVALUES of H, not the (a, b) coordinates.

    # The (a, b) coordinates of a circulant relate to eigenvalues via:
    #   lambda_1 = a + 2 Re(b)  (real)
    #   lambda_w = a + 2 Re(b omega) = a - Re(b) - sqrt(3) Im(b)
    #   lambda_wb = a + 2 Re(b omega_bar) = a - Re(b) + sqrt(3) Im(b)
    #
    # Multiplying lambda_w by e^{2i theta} produces a COMPLEX eigenvalue,
    # which means the resulting X = U X V^* is NO LONGER HERMITIAN.

    H_after_twist = twisted(hermitian_circulant(1.0, 0.5 + 0.3j), 0.4)
    check("4.4a Twisted conjugation does NOT preserve Hermiticity",
          not is_hermitian(H_after_twist))

    # Test 4.5: Even ignoring Hermiticity loss, the twisted action multiplies
    # (lambda_w, lambda_wb) by phases (e^{2i theta}, e^{-2i theta}). This is
    # PHASE-rotation-on-eigenvalues, NOT phase-rotation-on-(b, b̄)-coefficients.
    # It is NOT the U(1)_b symmetry on the b-doublet.

    # Construct a candidate "twisted" lift that DOES rotate b by phase:
    # We want alpha(aI + bC + b̄C²) = aI + e^{i theta} b C + e^{-i theta} b̄ C².
    # In Fourier: in basis (chi_1, chi_omega, chi_omega_bar):
    #   X = a · diag(1, 1, 1)              ← from aI
    #     + b · diag(1, omega, omega_bar) ← from bC; coefficient of diag(0,1,0) is b·omega
    # Hmm, the Fourier diagonalization of bC is more subtle. Let me re-derive:
    # C acts on the Fourier basis by:
    #   C |chi_1> = |chi_1>, C |chi_omega> = omega |chi_omega>, C |chi_obar> = obar |chi_obar>
    # So C in Fourier basis is diag(1, omega, omega_bar).
    # Then aI + bC + b̄C² in Fourier is
    #   diag(a, a, a) + b·diag(1, omega, omega_bar) + b̄·diag(1, omega², omega_bar²)
    #   = diag(a + b + b̄, a + b·omega + b̄·omega², a + b·omega_bar + b̄·omega_bar²)
    #   = diag(a + 2 Re(b),  a + b·omega + b̄·omega_bar, a + b·omega_bar + b̄·omega)
    # (using omega² = omega_bar)
    # So the (a, b) -> (a, e^{i phi} b) action would change:
    #   lambda_1: a + 2 Re(b) -> a + 2 Re(e^{i phi} b) = a + 2 Re(b)cos(phi) - 2 Im(b)sin(phi)
    #   lambda_w: a + b omega + b̄ omega_bar -> a + e^{i phi} b omega + e^{-i phi} b̄ omega_bar
    #   lambda_wb: similar with omega_bar
    #
    # CRITICAL: The transformation b -> e^{i phi} b changes ALL THREE
    # eigenvalues of H (since each eigenvalue depends on Re(b), Im(b)
    # combinations). It is NOT a purely-eigenvalue-multiplicative transformation.
    # So no twisted conjugation in U(3) x U(3) (acting linearly in Fourier basis)
    # can implement U(1)_b on (a, b) → (a, e^{i phi} b).

    print("  >> V3 verdict: twisted conjugation is a group action but is not")
    print("     unital, not multiplicative, AND does not preserve Hermiticity.")
    print("     It acts on EIGENVALUES of H (not on (a, b) coefficients of H),")
    print("     so it cannot implement the b -> e^{i phi} b U(1)_b symmetry.")


