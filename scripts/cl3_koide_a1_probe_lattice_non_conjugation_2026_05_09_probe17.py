def section2_V1_character_graded_phase() -> None:
    section("Section 2: V1 — C_3-character-graded per-site phase")

    # The proposal: for the three sites supporting hw=1, apply
    #   per-site phase exp(i theta * chi_k * sigma_3 / 2)
    # with chi_k in {0, +1, -1} = C_3-character at corner k.
    #
    # The action on H_hw=1 = C^3:
    #   |X_k> -> exp(i theta * chi_k / 2) |X_k>
    # i.e., a diagonal unitary U_theta = diag(e^{i theta * chi_1 / 2}, ...)
    #
    # On M_3(C) this acts by conjugation A -> U_theta A U_theta^*.
    # The induced action on circulants A^{C_3} is generated by the
    # commutator with diag(0, +1, -1)/2 (the generator of U_theta).

    # Build the generator G_chi = diag(0, +1, -1) (rescaled out the 1/2)
    G_chi = np.diag([0.0, 1.0, -1.0]).astype(complex)

    # Test 2.1: U_theta = exp(i theta G_chi / 2) is unitary
    theta = 0.7
    U_theta = np.diag(np.exp(1j * theta * np.array([0, 1, -1]) / 2.0)).astype(complex)
    check("2.1 U_theta(character-graded) is unitary",
          np.allclose(U_theta @ U_theta.conj().T, I3))

    # Test 2.2: U_theta does NOT commute with C_3 (G_chi has a C_3 character
    # +1 only on the trivial component, so it commutes with C iff it is
    # constant — but G_chi has nonzero (chi=±1) entries that get permuted
    # by C, so [U_theta, C] != 0).
    check("2.2 U_theta(character-graded) does NOT commute with C",
          not np.allclose(U_theta @ C, C @ U_theta))

    # Test 2.3: The action A -> U_theta A U_theta^* on circulants does NOT
    # preserve circulant structure — it kicks A^{C_3} OUT of A^{C_3}.
    H_test = hermitian_circulant(1.0, 0.5 + 0.3j)
    H_after = U_theta @ H_test @ U_theta.conj().T
    check("2.3 U_theta·circulant·U_theta^* is NOT circulant (action breaks A^{C_3})",
          not is_circulant(H_after))

    # Test 2.4: Per-site phase NOT being symmetric under C_3 means the lift
    # of "per-site qubit U(1)" cannot be uniformly applied across sites
    # without breaking C_3-equivariance. The "C_3-character-graded" angle
    # IS retained per-site qubit U(1), but the per-site choice of angle is
    # NOT a retained operation.
    #
    # The site-by-site phase choice involves ASSIGNING characters to sites.
    # But "site index" on the BZ-corner triplet is C_3-permuted; assigning
    # different angles by character means BREAKING C_3 — exactly what the
    # action MUST preserve to be a candidate for U(1)_b on A^{C_3}.

    # Verify: any C_3-equivariant per-site phase choice must give the SAME
    # angle to all three corners. That collapses U_theta to a global scalar
    # = trivial on M_3(C).

    # Test 2.5: If we demand the per-site phase be C_3-equivariant
    # (i.e., the angle assignment to sites is preserved by C_3 cyclic shift),
    # then all three angles are equal: theta_1 = theta_2 = theta_3 = theta.
    # The resulting action is exp(i theta) * I = scalar on H_hw=1.
    U_uniform = np.exp(1j * theta) * I3
    check("2.5 C_3-equivariant per-site phase = uniform = scalar on H_hw=1",
          np.allclose(U_uniform, np.exp(1j * theta) * I3) and
          commutes_with_C3(U_uniform))
    check("2.6 Uniform scalar acts trivially on M_3(C) (conjugation is identity)",
          np.allclose(conjugation(hermitian_circulant(1.0, 0.5 + 0.3j), U_uniform),
                      hermitian_circulant(1.0, 0.5 + 0.3j)))

    # Conclusion 2.7: V1 fails by C_3-equivariance constraint.
    # Either the per-site angle is C_3-equivariant (=> trivial scalar on H_hw=1),
    # or it is non-equivariant (=> breaks C_3, taking us OUT of the C_3-fixed
    # frame where A^{C_3} is defined).
    print("  >> V1 verdict: character-graded per-site phase either reduces")
    print("     to a scalar (trivial) or breaks C_3 equivariance.")
    print("     CANNOT supply U(1)_b on A^{C_3}.")


