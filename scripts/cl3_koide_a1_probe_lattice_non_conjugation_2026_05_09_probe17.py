# Section 3: V2 — Bimodule action on M_3(C)
# ----------------------------------------------------------------------


def section3_V2_bimodule_action() -> None:
    section("Section 3: V2 — Bimodule action distinct from conjugation")

    # Bimodule structure: M_3(C) acts on itself by left and right
    # multiplication. A "left-module-only" action L_U: A -> U·A is NOT
    # an algebra automorphism (it doesn't preserve products: L_U(AB) =
    # U·A·B ≠ L_U(A)·L_U(B) = U·A·U·B in general).
    #
    # Question: does retained per-site qubit U(1) act as a left-module
    # action L_U on M_3(C) on hw=1 that gives U(1)_b on the b-doublet?

    theta = 0.5
    # Pick a U commuting with C_3 (to preserve A^{C_3}):
    # U = e^{i theta} * I (only such U commuting with C is scalar on hw=1
    # if we want U(1) action; or U could be a circulant unitary).

    # Test 3.1: The "trivial" left-module action U = e^{i theta} * I on M_3(C)
    # gives L_U(A) = e^{i theta} * A. This is NOT an algebra automorphism
    # (preserves linearity but not multiplication — except for U = I).
    U = np.exp(1j * theta) * I3
    A = hermitian_circulant(1.0, 0.5 + 0.3j)
    B = hermitian_circulant(0.5, 0.2 - 0.1j)
    L_U_A = U @ A
    L_U_B = U @ B
    L_U_AB = U @ (A @ B)
    not_aut = not np.allclose(L_U_AB, L_U_A @ L_U_B)
    check("3.1 Left-module L_U: A -> U·A is NOT an algebra-automorphism",
          not_aut)

    # Test 3.2: But L_U is a LINEAR map on M_3(C). Its restriction to A^{C_3}
    # acts as multiplication by U on the left. For U = e^{i theta} * I,
    # L_U(aI + bC + b̄C²) = a·e^{i theta} I + b·e^{i theta} C + b̄·e^{i theta} C².
    # This is a UNIFORM phase on (a, b, b̄), NOT a U(1)_b action that
    # rotates b alone (preserving a).
    H_a = hermitian_circulant(1.0, 0.5 + 0.3j)
    L_U_H = U @ H_a
    # Check it's still in span{I, C, C²} but with all coefficients phase-shifted
    # by U:
    a_new = np.trace(L_U_H) / 3.0
    b_new = np.trace(L_U_H @ C2) / 3.0
    expected_a = 1.0 * np.exp(1j * theta)
    expected_b = (0.5 + 0.3j) * np.exp(1j * theta)
    check("3.2 L_U on circulant (U=scalar) gives uniform phase on (a, b)",
          np.isclose(a_new, expected_a) and np.isclose(b_new, expected_b))

    # The left-module action with scalar U does NOT separate the trivial
    # isotype (a-coefficient) from the doublet (b-coefficient). Both get
    # the SAME phase, so this is NOT U(1)_b (which acts only on b).

    # Test 3.3: Try non-scalar U commuting with C_3 (e.g., U = unitary in
    # circulant subalgebra). The most general U commuting with C is itself
    # circulant: U = u_0 I + u_1 C + u_2 C² with |u_0|^2 + |u_1|^2 + |u_2|^2
    # appropriately normalized.
    # In the Fourier basis (eigenbasis of C), U is diag(lambda_1, lambda_omega,
    # lambda_omega_bar) where lambda_chi = u_0 + u_1 chi + u_2 chi^2 and each
    # |lambda_chi| = 1.
    #
    # As a U(1) subgroup, the maximal compact U(1) inside circulant unitaries
    # has 3 independent angles (u(1) x u(1) x u(1) = 3-torus). Each angle is
    # a different per-character phase.
    lambda_1, lambda_w, lambda_wb = np.exp(1j * 0.3), np.exp(1j * 0.5), np.exp(1j * (-0.8))
    # Build U as circulant by inverse Fourier:
    # U_circ in Fourier basis = diag(lambda_1, lambda_w, lambda_wb)
    F = np.column_stack(fourier_basis())
    U_circ = F @ np.diag([lambda_1, lambda_w, lambda_wb]) @ F.conj().T
    check("3.3a U_circ (circulant unitary) commutes with C_3",
          commutes_with_C3(U_circ))
    check("3.3b U_circ is unitary",
          np.allclose(U_circ @ U_circ.conj().T, I3))

    # Now check: left-module action L_{U_circ} on a circulant H = aI + bC + b̄C².
    # Result: U_circ · H is a product of two circulants in M_3(C), hence again
    # a circulant. So L preserves A^{C_3} only when U is itself in A^{C_3}.
    H = hermitian_circulant(1.0, 0.5 + 0.3j)
    L_H = U_circ @ H
    check("3.3c L_{U_circ}(circulant) is circulant (since U_circ in A^{C_3})",
          is_circulant(L_H))

    # Now restricted to A^{C_3} ≅ C^3 (via Fourier), L_{U_circ} acts as
    # diag(lambda_1, lambda_w, lambda_wb). This is a 3-torus T^3 action on
    # the three characters separately. NOT specifically U(1)_b which is the
    # SO(2) phase rotation on (Re b, Im b) = real isotype rotation in the
    # (chi_omega, chi_omega_bar) plane.

    # Test 3.4: For L_{U_circ} to act as U(1)_b on the b-doublet, we need
    # lambda_omega and lambda_omega_bar to be CONJUGATE (so that real
    # parametrization (Re b, Im b) gets rotated). i.e.,
    # lambda_omega_bar = (lambda_omega)*. Combined with lambda_1 = 1 (no
    # action on a), this gives:
    #
    #   U_b(theta) := F · diag(1, e^{i theta}, e^{-i theta}) · F^*
    #
    # in M_3(C). This U_b is NOT a SCALAR on H_hw=1 — it's a specific 1D
    # subgroup of the 3-torus of circulant unitaries.
    # Build it:
    theta_b = 0.7
    U_b_diag = np.diag([1.0, np.exp(1j * theta_b), np.exp(-1j * theta_b)]).astype(complex)
    U_b = F @ U_b_diag @ F.conj().T
    check("3.4a U_b(theta) commutes with C_3",
          commutes_with_C3(U_b))
    check("3.4b U_b(theta) is unitary",
          np.allclose(U_b @ U_b.conj().T, I3))

    # CRUCIAL TEST: does conjugation by U_b act as U(1)_b on (a, b)?
    # alpha_{U_b}(aI + bC + b̄C²) should give aI + e^{i theta_b} bC + e^{-i theta_b} b̄C².
    H_ab = hermitian_circulant(1.0, 0.5 + 0.3j)
    H_after_U_b = conjugation(H_ab, U_b)
    a_after = np.trace(H_after_U_b) / 3.0
    b_after = np.trace(H_after_U_b @ C2) / 3.0
    bbar_after = np.trace(H_after_U_b @ C) / 3.0
    print(f"  diag info: U_b conjugation on (a, b) = (a={1.0}, b={0.5+0.3j}) ->")
    print(f"             a_after = {a_after:.6f}")
    print(f"             b_after = {b_after:.6f}")
    print(f"             expected a = 1.0, b = (0.5 + 0.3j) * e^{{i theta_b}} = ", end="")
    print(f"{(0.5 + 0.3j) * np.exp(1j * theta_b):.6f}")

    # WAIT: U_b commutes with C_3, so conjugation by U_b PRESERVES the
    # circulant subspace AND fixes a (the trivial-character coefficient).
    # By Probe 14, conjugation by C_3-commuting unitary is TRIVIAL on
    # circulant subspace.
    #
    # Let's verify: alpha_{U_b}(aI) = U_b · aI · U_b^* = a · U_b · U_b^* = aI.
    # alpha_{U_b}(C) = U_b · C · U_b^*. Since [U_b, C] = 0, this = C.
    # So alpha_{U_b}(aI + bC + b̄C²) = aI + bC + b̄C². TRIVIAL.
    expected_trivial = H_ab
    check("3.4c Conjugation by U_b acts TRIVIALLY on circulants (Probe 14)",
          np.allclose(H_after_U_b, expected_trivial))

    # Test 3.5: The LEFT-MODULE action L_{U_b}(H) = U_b · H is NOT trivial
    # (it's multiplication of circulants in M_3(C)). But this is NOT an
    # algebra-automorphism — it's a left-module action.
    L_U_b_H = U_b @ H_ab
    check("3.5a Left-module L_{U_b}(H) = U_b · H is non-trivial on circulants",
          not np.allclose(L_U_b_H, H_ab))
    check("3.5b L_{U_b}(H) is still a circulant (product of two circulants)",
          is_circulant(L_U_b_H))

    # Decompose L_{U_b}(H) into (a, b)-coefficients: L_{U_b} acts on A^{C_3} ≅ C³
    # via Fourier as element-wise multiplication by (1, e^{i theta_b}, e^{-i theta_b}).
    # So in (a, b, b̄) parametrization:
    #   L_{U_b}(aI + bC + b̄C²) corresponds to multiplying the trivial-character
    #   eigenvalue (= a + 2 Re b) by 1, the omega-character eigenvalue
    #   (= a + 2 Re(b omega)) by e^{i theta_b}, and the omega-bar eigenvalue
    #   (= a + 2 Re(b omega-bar)) by e^{-i theta_b}.
    # This is NOT a clean (a, b) -> (a, e^{i theta_b} b) action — it MIXES a and b.

    # Verify: the a-coefficient of L_{U_b}(H) is NOT equal to the a-coefficient of H.
    a_orig = np.trace(H_ab) / 3.0
    a_after_L = np.trace(L_U_b_H) / 3.0
    print(f"  diag info: L_{{U_b}}(H): a_before = {a_orig:.6f}, a_after = {a_after_L:.6f}")
    check("3.5c Left-module action MIXES a and b (NOT a clean U(1)_b)",
          not np.isclose(a_orig, a_after_L))

    print("  >> V2 verdict: bimodule (left-module) action by U_circ in A^{C_3}")
    print("     mixes a and b coefficients. NOT a clean U(1)_b on b-doublet.")
    print("     Conjugation by same U_circ is trivial on A^{C_3} (Probe 14).")
    print("     Neither pure left-module nor conjugation gives U(1)_b.")


