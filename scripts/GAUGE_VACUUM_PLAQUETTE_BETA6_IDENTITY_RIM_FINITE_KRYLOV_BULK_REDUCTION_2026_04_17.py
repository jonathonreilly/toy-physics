#!/usr/bin/env python3
"""
Finite-Krylov bulk reduction after the plaquette beta=6 identity-rim reduction.

This sharpens the remaining operator-side bulk front in two exact ways:

1. for fixed propagation depth d, explicit class-sector closure only sees the
   finite Krylov compression generated by eta_6(e), not the whole bulk operator
   and not even the whole eta-cyclic object;
2. even fixed eta_6(e) together with the exact propagated retained triple does
   not determine the first finite Krylov bulk matrix-element block on the
   current bank.
"""

from __future__ import annotations

import cmath
import math
from pathlib import Path

import numpy as np


ROOT = Path(__file__).resolve().parents[1]

THEOREM_PASS = 0
SUPPORT_PASS = 0
FAIL = 0

DEPTH = 3
SAMPLES = {
    "W_A": (-13 * math.pi / 16.0, 5 * math.pi / 8.0),
    "W_B": (-5 * math.pi / 16.0, -7 * math.pi / 16.0),
    "W_C": (7 * math.pi / 16.0, -11 * math.pi / 16.0),
}
ORBITS = [(0, 2), (0, 3), (0, 4), (0, 5)]
ETA_FIXED = np.ones(4, dtype=float) / 2.0
BASE_TARGET = np.array([1.2, 0.9, 1.1, 0.8], dtype=float)
KERNEL_STEP = 0.4


def check(name: str, condition: bool, detail: str = "", bucket: str = "THEOREM") -> None:
    global THEOREM_PASS, SUPPORT_PASS, FAIL
    status = "PASS" if condition else "FAIL"
    if condition:
        if bucket == "SUPPORT":
            SUPPORT_PASS += 1
        else:
            THEOREM_PASS += 1
    else:
        FAIL += 1
    print(f"  [{status}] [{bucket}] {name}")
    if detail:
        print(f"         {detail}")


def read(rel_path: str) -> str:
    return (ROOT / rel_path).read_text()


def normalize(vec: np.ndarray) -> np.ndarray:
    return vec / np.linalg.norm(vec)


def dim_su3(p: int, q: int) -> int:
    return (p + 1) * (q + 1) * (p + q + 2) // 2


def su3_character(p: int, q: int, theta1: float, theta2: float) -> complex:
    x = [
        cmath.exp(1j * theta1),
        cmath.exp(1j * theta2),
        cmath.exp(-1j * (theta1 + theta2)),
    ]
    lam = [p + q, q, 0]
    num = np.array([[x[i] ** (lam[j] + 2 - j) for j in range(3)] for i in range(3)], dtype=complex)
    den = np.array([[x[i] ** (2 - j) for j in range(3)] for i in range(3)], dtype=complex)
    return complex(np.linalg.det(num) / np.linalg.det(den))


def orbit_sample_row(p: int, q: int) -> np.ndarray:
    d = dim_su3(p, q)
    row = []
    for theta1, theta2 in SAMPLES.values():
        ch = su3_character(p, q, theta1, theta2)
        row.append(float(np.real_if_close(2.0 * (d * ch).real)))
    return np.array(row, dtype=float)


def sample_matrix() -> np.ndarray:
    return np.column_stack([orbit_sample_row(*orbit) for orbit in ORBITS])


def krylov_columns(op: np.ndarray, eta: np.ndarray, depth: int) -> np.ndarray:
    return np.column_stack([np.linalg.matrix_power(op, n) @ eta for n in range(depth + 1)])


def orthonormal_basis(columns: np.ndarray) -> np.ndarray:
    qmat, _ = np.linalg.qr(columns)
    rank = int(np.linalg.matrix_rank(columns))
    return qmat[:, :rank]


def projector_from_basis(basis: np.ndarray) -> np.ndarray:
    return basis @ basis.T


def complement_basis(basis: np.ndarray) -> np.ndarray:
    ambient_dim = basis.shape[0]
    out: list[np.ndarray] = []
    for i in range(ambient_dim):
        vec = np.zeros(ambient_dim, dtype=float)
        vec[i] = 1.0
        for j in range(basis.shape[1]):
            vec -= float(np.dot(basis[:, j], vec)) * basis[:, j]
        for col in out:
            vec -= float(np.dot(col, vec)) * col
        norm = np.linalg.norm(vec)
        if norm > 1.0e-12:
            out.append(vec / norm)
        if len(out) == ambient_dim - basis.shape[1]:
            break
    if not out:
        return np.zeros((ambient_dim, 0), dtype=float)
    return np.column_stack(out)


def demo_bulk_operator() -> np.ndarray:
    amat = np.array(
        [
            [1.2, 0.1, 0.0, 0.2, 0.0, 0.1],
            [0.3, 1.1, 0.2, 0.0, 0.1, 0.0],
            [0.0, 0.2, 1.0, 0.1, 0.2, 0.0],
            [0.1, 0.0, 0.3, 0.9, 0.0, 0.2],
            [0.0, 0.1, 0.0, 0.2, 0.8, 0.3],
            [0.2, 0.0, 0.1, 0.0, 0.2, 0.7],
        ],
        dtype=float,
    )
    return amat @ amat.T


def depth_target_operator(v_target: np.ndarray, eta: np.ndarray, depth: int) -> np.ndarray:
    unit = normalize(v_target)
    lam = (np.linalg.norm(v_target) ** 2 / float(np.dot(v_target, eta))) ** (1.0 / depth)
    return lam * np.outer(unit, unit)


def first_krylov_block(op: np.ndarray, eta: np.ndarray) -> np.ndarray:
    q0 = normalize(eta)
    alpha0 = float(q0 @ op @ q0)
    residual = op @ q0 - alpha0 * q0
    beta1 = float(np.linalg.norm(residual))
    if beta1 < 1.0e-12:
        alpha1 = 0.0
    else:
        q1 = residual / beta1
        alpha1 = float(q1 @ op @ q1)
    return np.array([[alpha0, beta1], [beta1, alpha1]], dtype=float)


def main() -> int:
    identity_rim_note = read("docs/GAUGE_VACUUM_PLAQUETTE_BETA6_IDENTITY_RIM_REDUCTION_NOTE_2026-04-17.md")
    cyclic_bulk_note = read(
        "docs/GAUGE_VACUUM_PLAQUETTE_BETA6_IDENTITY_RIM_CYCLIC_BULK_REDUCTION_NOTE_2026-04-17.md"
    )
    operator_nonclosure_note = read(
        "docs/GAUGE_VACUUM_PLAQUETTE_FIRST_PROPAGATED_RETAINED_TRIPLE_OPERATOR_SIDE_NONCLOSURE_NOTE_2026-04-17.md"
    )
    pf_boundary_note = read("docs/PERRON_FROBENIUS_SELECTION_AXIOM_BOUNDARY_NOTE_2026-04-17.md")

    demo_op = demo_bulk_operator()
    eta_demo = normalize(np.array([1.0, 0.6, 0.4, 0.2, 0.3, 0.5], dtype=float))
    demo_columns = krylov_columns(demo_op, eta_demo, DEPTH)
    demo_basis = orthonormal_basis(demo_columns)
    demo_proj = projector_from_basis(demo_basis)
    demo_compressed = demo_proj @ demo_op @ demo_proj
    projected_orbit_errors = [
        float(
            np.max(
                np.abs(
                    np.linalg.matrix_power(demo_op, n) @ eta_demo
                    - np.linalg.matrix_power(demo_compressed, n) @ eta_demo
                )
            )
        )
        for n in range(DEPTH + 1)
    ]

    complement = complement_basis(demo_basis)
    demo_core = demo_basis.T @ demo_op @ demo_basis
    demo_a = demo_basis @ demo_core @ demo_basis.T + complement @ np.diag([0.6, 1.1]) @ complement.T
    demo_b = demo_basis @ demo_core @ demo_basis.T + complement @ np.diag([1.7, 2.4]) @ complement.T
    matched_orbit_gaps = [
        float(
            np.max(
                np.abs(
                    np.linalg.matrix_power(demo_a, n) @ eta_demo
                    - np.linalg.matrix_power(demo_b, n) @ eta_demo
                )
            )
        )
        for n in range(DEPTH + 1)
    ]
    complement_gap = float(np.max(np.abs(demo_a - demo_b)))
    demo_eig_floor = min(float(np.min(np.linalg.eigvalsh(demo_a))), float(np.min(np.linalg.eigvalsh(demo_b))))

    e3 = sample_matrix()
    _, _, vh = np.linalg.svd(e3)
    kernel = vh[-1]
    kernel_residual = float(np.max(np.abs(e3 @ kernel)))

    v_p = BASE_TARGET.copy()
    v_q = BASE_TARGET + KERNEL_STEP * kernel
    target_gap = float(np.max(np.abs(v_p - v_q)))
    min_target_entry = min(float(np.min(v_p)), float(np.min(v_q)))
    triple_p = e3 @ v_p
    triple_q = e3 @ v_q
    triple_gap = float(np.max(np.abs(triple_p - triple_q)))

    s_p = depth_target_operator(v_p, ETA_FIXED, DEPTH)
    s_q = depth_target_operator(v_q, ETA_FIXED, DEPTH)
    depth_vec_p = np.linalg.matrix_power(s_p, DEPTH) @ ETA_FIXED
    depth_vec_q = np.linalg.matrix_power(s_q, DEPTH) @ ETA_FIXED
    depth_map_gap = max(
        float(np.max(np.abs(depth_vec_p - v_p))),
        float(np.max(np.abs(depth_vec_q - v_q))),
    )
    triple_depth_gap = float(np.max(np.abs(e3 @ depth_vec_p - e3 @ depth_vec_q)))
    eig_floor = min(float(np.min(np.linalg.eigvalsh(s_p))), float(np.min(np.linalg.eigvalsh(s_q))))

    block_p = first_krylov_block(s_p, ETA_FIXED)
    block_q = first_krylov_block(s_q, ETA_FIXED)
    block_gap = float(np.max(np.abs(block_p - block_q)))

    print("=" * 124)
    print("GAUGE-VACUUM PLAQUETTE BETA=6 IDENTITY-RIM FINITE-KRYLOV BULK REDUCTION")
    print("=" * 124)
    print()
    print("Fixed-depth Krylov reduction witness")
    print(f"  propagation depth d                       = {DEPTH}")
    print(f"  demo bulk dimension / Krylov rank         = {demo_op.shape[0]} / {demo_basis.shape[1]}")
    print(f"  projected-orbit errors                    = {[f'{x:.3e}' for x in projected_orbit_errors]}")
    print(f"  complement-variation orbit gaps           = {[f'{x:.3e}' for x in matched_orbit_gaps]}")
    print(f"  orthogonal-complement operator gap        = {complement_gap:.12f}")
    print(f"  PSD eigenvalue floor under variation      = {demo_eig_floor:.12e}")
    print()
    print("First finite Krylov-block nonclosure witness")
    print(f"  higher-orbit slice                        = {ORBITS}")
    print(f"  sample-matrix shape / rank                = {e3.shape} / {np.linalg.matrix_rank(e3)}")
    print(f"  kernel direction                          = {np.array2string(kernel, precision=10)}")
    print(f"  kernel residual max-norm                  = {kernel_residual:.12e}")
    print(f"  fixed identity rim eta                    = {np.array2string(ETA_FIXED, precision=10)}")
    print(f"  propagated target v_P                     = {np.array2string(v_p, precision=10)}")
    print(f"  propagated target v_Q                     = {np.array2string(v_q, precision=10)}")
    print(f"  target gap / min entry                    = {target_gap:.12f} / {min_target_entry:.12f}")
    print(f"  exact propagated triple                   = {np.array2string(triple_p, precision=10)}")
    print(f"  propagated triple gap                     = {triple_gap:.12e}")
    print(f"  depth-target realization error            = {depth_map_gap:.12e}")
    print(f"  propagated triple gap after depth map     = {triple_depth_gap:.12e}")
    print(f"  positive bulk witness eigenvalue floor    = {eig_floor:.12e}")
    print()
    print("First 2x2 Krylov/Lanczos bulk blocks")
    print(f"  J_1^P                                     = {np.array2string(block_p, precision=10)}")
    print(f"  J_1^Q                                     = {np.array2string(block_q, precision=10)}")
    print(f"  block gap                                 = {block_gap:.12f}")
    print()

    check(
        "The identity-rim reduction note already reduces explicit class-sector beta=6 closure to bulk data together with eta_6(e), with generic W dependence already downstream",
        "bulk operator together with the" in identity_rim_note
        and "`eta_6(e)`" in identity_rim_note
        and "generic marked-holonomy dependence is already carried by the universal" in identity_rim_note,
        bucket="SUPPORT",
    )
    check(
        "The cyclic bulk reduction note already says the live upstream bulk object is the eta_6(e)-cyclic compression rather than the whole operator",
        "`eta_6(e)`-cyclic compression of `S_6^env`" in cyclic_bulk_note
        and "not the whole operator" in cyclic_bulk_note,
        bucket="SUPPORT",
    )
    check(
        "The propagated-triple nonclosure note already says the propagated retained triple is the right finite target but still not an operator-side closure datum",
        "right next evaluative target" in operator_nonclosure_note
        and "still **not** the whole operator-side closure datum" in operator_nonclosure_note,
        bucket="SUPPORT",
    )
    check(
        "The PF selection boundary note already locates the plaquette gap at explicit bulk kernel data together with the rim map to eta_6",
        "earliest missing constructive datum on that lane is" in pf_boundary_note
        and "explicit orthogonal-slice kernel `K_6^env` together with the rim map to" in pf_boundary_note
        and "`eta_6`" in pf_boundary_note,
        bucket="SUPPORT",
    )

    check(
        "For fixed depth d, the finite Krylov compression generated by eta reproduces every orbit vector S^n eta up to n=d",
        max(projected_orbit_errors) < 1.0e-12,
        detail=f"max projected-orbit error={max(projected_orbit_errors):.3e}",
    )
    check(
        "Two positive bulk operators with the same finite Krylov compression but different orthogonal-complement blocks induce the same depth-d eta orbit",
        max(matched_orbit_gaps) < 1.0e-12 and complement_gap > 1.0e-1 and demo_eig_floor > 0.0,
        detail=f"max orbit gap={max(matched_orbit_gaps):.3e}, complement gap={complement_gap:.3f}",
    )
    check(
        "So fixed-depth class-sector beta=6 closure depends only on the finite eta_6(e)-generated Krylov bulk block, not on the whole compressed operator",
        max(projected_orbit_errors) < 1.0e-12 and max(matched_orbit_gaps) < 1.0e-12,
        detail=f"depth={DEPTH}, Krylov rank={demo_basis.shape[1]}",
    )
    check(
        "On the explicit four-orbit slice there are two distinct nonnegative propagated targets with the same exact propagated retained triple",
        kernel_residual < 1.0e-10 and min_target_entry > 0.0 and target_gap > 1.0e-2 and triple_gap < 1.0e-10,
        detail=f"target gap={target_gap:.6f}, triple gap={triple_gap:.3e}",
    )
    check(
        "Positive self-adjoint bulk witnesses with one fixed identity rim eta realize those two targets at the same propagation depth",
        depth_map_gap < 1.0e-12 and triple_depth_gap < 1.0e-10 and eig_floor > -1.0e-12,
        detail=f"depth-map error={depth_map_gap:.3e}, triple gap after mapping={triple_depth_gap:.3e}",
    )
    check(
        "Yet the first finite Krylov bulk matrix-element block already differs, so the current propagated retained triple does not determine even that first block",
        block_gap > 1.0e-3 and triple_depth_gap < 1.0e-10,
        detail=f"max |J_1^P - J_1^Q| = {block_gap:.6f}",
    )

    print()
    print(f"THEOREM PASS={THEOREM_PASS} SUPPORT={SUPPORT_PASS} FAIL={FAIL}")
    return 0 if FAIL == 0 else 1


if __name__ == "__main__":
    raise SystemExit(main())
