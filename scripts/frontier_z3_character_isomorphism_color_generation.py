def part4_decomposition_irreducibles() -> None:
    """Verify the decomposition V ≅ chi_0 + chi_omega + chi_omega^2 (regular rep)."""
    print()
    print("=" * 78)
    print("PART 4: DECOMPOSITION INTO IRREDUCIBLE T_3 CHARACTERS")
    print("=" * 78)

    # The irreducible characters of T_3 = Z/3Z:
    #   chi_0:     e -> 1, c -> 1, c^2 -> 1     (trivial)
    #   chi_omega: e -> 1, c -> omega, c^2 -> omega^2
    #   chi_omega^2: e -> 1, c -> omega^2, c^2 -> omega
    omega = sp.exp(2 * sp.pi * sp.I / 3)
    omega2 = sp.exp(-2 * sp.pi * sp.I / 3)  # = omega^2

    irreducibles = {
        "chi_0":   {"e": 1, "c": 1, "c^2": 1},
        "chi_omega": {"e": 1, "c": omega, "c^2": omega ** 2},
        "chi_omega^2": {"e": 1, "c": omega ** 2, "c^2": omega ** 4},  # omega^4 = omega
    }

    # Verify irreducible character orthogonality: 1/|T_3| Sum chi(g) bar chi(g) = 1
    # for each irreducible character.
    for name, chi in irreducibles.items():
        sum_norm = sp.Rational(1, 3) * sum(
            chi[g] * sp.conjugate(chi[g]) for g in ("e", "c", "c^2")
        )
        check(
            f"Irreducible {name}: 1/|T_3| Sum |chi(g)|^2 = 1 (orthogonality)",
            sp.simplify(sum_norm - 1) == 0,
            f"|{name}|^2 sum = {sp.simplify(sum_norm)}",
        )

    # Decompose V_color (character (3, 0, 0)) into irreducibles
    # Multiplicity of irreducible chi_k in V_color is:
    #   m_k = 1/|G| Sum_g chi_V_color(g) bar chi_k(g)
    V_color_char = {"e": 3, "c": 0, "c^2": 0}
    multiplicities = {}
    for name, chi in irreducibles.items():
        m = sp.Rational(1, 3) * sum(
            V_color_char[g] * sp.conjugate(chi[g]) for g in ("e", "c", "c^2")
        )
        multiplicities[name] = sp.simplify(m)

    for name, m in multiplicities.items():
        check(
            f"V_color contains irreducible {name} with multiplicity 1",
            m == 1,
            f"m({name}) = {m}",
        )

    # Confirm V_color decomposes as the regular rep: chi_0 + chi_omega + chi_omega^2
    decomposition_str = " + ".join(name for name in irreducibles.keys())
    check(
        "V_color ≅ chi_0 + chi_omega + chi_omega^2 (regular representation)",
        all(m == 1 for m in multiplicities.values()),
        f"V_color = {decomposition_str}",
    )

    # Same decomposition for V_gen (same character vector)
    check(
        "V_gen ≅ chi_0 + chi_omega + chi_omega^2 (same decomposition as V_color)",
        True,
        "by character equality from Part 3",
    )


