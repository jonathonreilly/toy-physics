# Eigenvalues of the 3-cycle: cube roots of unity
# ============================================================================

def part2_eigenvalues_cube_roots() -> None:
    """Eigenvalues of P are 1, omega, omega^2 (cube roots of unity)."""
    print()
    print("=" * 78)
    print("PART 2: EIGENVALUES OF P ARE CUBE ROOTS OF UNITY")
    print("=" * 78)

    # Sympy can compute eigenvalues symbolically
    P = sp.Matrix([
        [0, 0, 1],
        [1, 0, 0],
        [0, 1, 0],
    ])

    eigenvalues_dict = P.eigenvals()  # {eigenvalue: multiplicity}
    eigenvalues = []
    for ev, mult in eigenvalues_dict.items():
        eigenvalues.extend([ev] * mult)

    # Check the characteristic polynomial: x^3 - 1 = 0
    x = sp.Symbol("x")
    char_poly = P.charpoly(x)
    expected_char_poly = x ** 3 - 1
    check(
        "Characteristic polynomial of P is x^3 - 1",
        sp.simplify(char_poly.as_expr() - expected_char_poly) == 0,
        f"char_poly = {char_poly.as_expr()}",
    )

    # The eigenvalues are the 3 cube roots of unity. Sympy returns them
    # in Cartesian form; verify numerically using complex arithmetic
    # that each eigenvalue satisfies x^3 = 1.
    eigenvalues_numeric = [complex(ev.evalf()) for ev in eigenvalues]
    for ev in eigenvalues_numeric:
        ev_cubed = ev ** 3
        check(
            f"Eigenvalue {ev:.4f} satisfies x^3 = 1 (math.isclose on real and imag)",
            math.isclose(ev_cubed.real, 1.0, abs_tol=1e-10)
            and math.isclose(ev_cubed.imag, 0.0, abs_tol=1e-10),
            f"({ev:.4f})^3 = {ev_cubed:.4f}",
        )

    # Numerical: omega = e^{2π i / 3} ~ -1/2 + (sqrt(3)/2) i
    omega_numeric = complex(np.exp(2j * np.pi / 3))
    omega_real = -0.5
    omega_imag = math.sqrt(3) / 2
    check(
        "Numerical: omega = e^{2π i / 3} ~ -0.5 + 0.866 i (math.isclose)",
        math.isclose(omega_numeric.real, omega_real, abs_tol=1e-12)
        and math.isclose(omega_numeric.imag, omega_imag, abs_tol=1e-12),
        f"omega = {omega_numeric}",
    )

    # Sum of cube roots is 0 numerically
    sum_numeric = sum(eigenvalues_numeric)
    check(
        "Sum of cube roots of unity is 0 (math.isclose on real + imag)",
        math.isclose(sum_numeric.real, 0.0, abs_tol=1e-10)
        and math.isclose(sum_numeric.imag, 0.0, abs_tol=1e-10),
        f"1 + omega + omega^2 = {sum_numeric:.6f} (numerically)",
    )

    # Sum symbolically via Vieta: the coefficient of x^2 in x^3 - 1 = 0
    # (Vieta: -sum of roots = coefficient of x^2 / leading coefficient)
    vieta_sum = -sp.Poly(x ** 3 - 1, x).all_coeffs()[1]  # coefficient of x^2 = 0
    check(
        "Vieta: sum of roots of x^3 - 1 = 0 (= -coeff of x^2)",
        vieta_sum == 0,
        f"sum = -coefficient of x^2 = {vieta_sum}",
    )


