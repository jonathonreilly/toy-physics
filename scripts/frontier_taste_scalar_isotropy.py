def part1_shift_algebra():
    """Construct and verify the commuting involution structure of S_i."""
    print("\n" + "=" * 72)
    print("PART 1: Shift operator algebra on C^8 = (C^2)^{otimes 3}")
    print("=" * 72)

    I2 = np.eye(2, dtype=complex)
    sx = np.array([[0, 1], [1, 0]], dtype=complex)

    S = [
        np.kron(sx, np.kron(I2, I2)),
        np.kron(I2, np.kron(sx, I2)),
        np.kron(I2, np.kron(I2, sx)),
    ]
    I8 = np.eye(8, dtype=complex)

    for i in range(3):
        diff = np.max(np.abs(S[i] @ S[i] - I8))
        check(f"S_{i+1}^2 = I (involution)",
              diff < 1e-14, f"max|S^2 - I| = {diff:.1e}")

    for i in range(3):
        for j in range(i + 1, 3):
            comm = np.max(np.abs(S[i] @ S[j] - S[j] @ S[i]))
            check(f"[S_{i+1}, S_{j+1}] = 0 (commutativity)",
                  comm < 1e-14, f"max|[S_i,S_j]| = {comm:.1e}")

    for i in range(3):
        herm = np.max(np.abs(S[i] - S[i].conj().T))
        check(f"S_{i+1} = S_{i+1}^dag (Hermiticity)",
              herm < 1e-14)

    return S


