def part2_eigenvalues(S):
    """Verify H(phi) eigenvalues are lambda_s = sum_i phi_i (-1)^{s_i}."""
    print("\n" + "=" * 72)
    print("PART 2: Eigenvalue structure of H(phi) = sum phi_i S_i")
    print("=" * 72)

    def H(phi):
        return phi[0] * S[0] + phi[1] * S[1] + phi[2] * S[2]

    def exact_eigs(phi):
        return sorted(
            sum(phi[i] * (-1) ** s[i] for i in range(3))
            for s in [(a, b, c) for a in range(2) for b in range(2) for c in range(2)]
        )

    tests = [(1, 0, 0), (0, 1, 0), (0, 0, 1), (1, 0.5, 0.3), (V_EW, 0, 0)]
    for phi in tests:
        num = sorted(np.linalg.eigvalsh(H(phi)))
        ana = exact_eigs(phi)
        diff = max(abs(n - a) for n, a in zip(num, ana))
        check(f"H{phi}: eigenvalues match analytic",
              diff < 1e-10, f"max|diff| = {diff:.1e}")

    # At the EWSB minimum: all |lambda_s| = v
    eigs_vev = np.linalg.eigvalsh(H((V_EW, 0, 0)))
    check("At phi=(v,0,0): all |lambda_s| = v",
          all(abs(abs(e) - V_EW) < 1e-8 for e in eigs_vev))

    return H


