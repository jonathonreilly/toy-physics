def _key(M: np.ndarray) -> tuple:
    return tuple(np.round(M.flatten(), 9))


def enumerate_group(max_len: int = 6) -> dict[tuple, str]:
    """Enumerate the group generated by <C3, TX, TY, TZ> up to word length max_len."""
    gens = [C3, TX, TY, TZ]
    names = ["C3", "TX", "TY", "TZ"]

    seen: dict[tuple, str] = {}
    seen[_key(I3)] = "I"
    frontier = [(I3, "I")]
    for _ in range(max_len):
        new = []
        for M, nm in frontier:
            for g, gn in zip(gens, names):
                Mg = M @ g
                k = _key(Mg)
                if k not in seen:
                    seen[k] = (nm + "*" + gn) if nm != "I" else gn
                    new.append((Mg, seen[k]))
        frontier = new
    return seen


def part1_group_enumeration() -> dict[tuple, str]:
    print("\n" + "=" * 88)
    print("PART 1: ENUMERATION OF RETAINED DISCRETE ACTIONS")
    print("=" * 88)

    group = enumerate_group(max_len=6)
    check(
        "Retained unitary group <C3, TX, TY, TZ> has exactly 24 distinct elements",
        len(group) == 24,
        f"|G| = {len(group)}",
    )
    # Verify closure: products of any two elements stay in the set.
    closed = True
    for k1, _ in group.items():
        M1 = np.array(k1).reshape(3, 3)
        for k2, _ in group.items():
            M2 = np.array(k2).reshape(3, 3)
            if _key(M1 @ M2) not in group:
                closed = False
                break
        if not closed:
            break
    check("Group is closed under multiplication (axiom-native closure)", closed)
    return group


