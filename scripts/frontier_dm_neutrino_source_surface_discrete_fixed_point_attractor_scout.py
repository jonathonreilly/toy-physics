I3 = np.eye(3, dtype=complex)


# ---------------------------------------------------------------------------
# Chart projection and induced map
# ---------------------------------------------------------------------------


def chart_proj(M: np.ndarray) -> np.ndarray:
    """Orthogonal projection (HS inner product) onto the chart
    H_base + span{T_m, T_delta, T_q}, returning (m, delta, q_+) coefficients."""
    G = [T_M, T_D, T_Q]
    gram = np.array(
        [[np.real(np.trace(Gi.conj().T @ Gj)) for Gj in G] for Gi in G]
    )
    delta0 = M - H_BASE
    rhs = np.array([np.real(np.trace(Gi.conj().T @ delta0)) for Gi in G])
    return np.linalg.solve(gram, rhs)


def induced_map(S: np.ndarray) -> tuple[np.ndarray, np.ndarray]:
    """Return (A, b) s.t. F_S(v) = A v + b for v = (m, delta, q_+)."""
    Sinv = S.conj().T

    def F(v: np.ndarray) -> np.ndarray:
        m, d, q = v
        H_p = S @ (H_BASE + m * T_M + d * T_D + q * T_Q) @ Sinv
        return chart_proj(H_p)

    b = F(np.zeros(3))
    cols = []
    for i in range(3):
        e = np.zeros(3)
        e[i] = 1.0
        cols.append(F(e) - b)
    A = np.column_stack(cols)
    return A, b


