def part2_chart_fixed_point_scan(group: dict[tuple, str]) -> None:
    print("\n" + "=" * 88)
    print("PART 2: CHART FIXED-POINT SCAN — UNIQUE-FIXED-POINT OPERATORS")
    print("=" * 88)

    unique_fp_count = 0
    on_diagonal_count = 0
    on_pin_count = 0
    contracting_count = 0

    for k, name in group.items():
        S = np.array(k).reshape(3, 3)
        A, b = induced_map(S)
        IA = I3[:3, :3].real.astype(float) if False else (np.eye(3) - A)
        det_ia = np.linalg.det(IA)
        if abs(det_ia) < 1e-10:
            continue
        fp = np.linalg.solve(IA, b)
        eigs = np.linalg.eigvals(A)
        unique_fp_count += 1
        if max(abs(e) for e in eigs) < 1.0 - 1e-9:
            contracting_count += 1
        if abs(fp[1] - fp[2]) < 1e-6:
            on_diagonal_count += 1
        if (
            abs(fp[1] - TARGET_DELTA) < 1e-6
            and abs(fp[2] - TARGET_Q) < 1e-6
        ):
            on_pin_count += 1

    check(
        "At least one retained unitary has a unique chart fixed point",
        unique_fp_count > 0,
        f"{unique_fp_count} of {len(group)} have det(I - A) != 0",
    )
    check(
        "At least one retained unitary is a strict contractive attractor",
        contracting_count > 0,
        f"{contracting_count} contracting operators",
    )
    check(
        "Some retained unitaries have chart fixed point on the δ = q_+ diagonal",
        on_diagonal_count > 0,
        f"{on_diagonal_count} on diagonal",
    )
    check(
        "NO retained unitary has chart fixed point at the Koide pin (δ=q_+=sqrt(6)/3)",
        on_pin_count == 0,
        f"operators hitting pin = {on_pin_count}",
    )


