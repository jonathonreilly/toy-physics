def part4_joint_fixed_locus() -> None:
    print("\n" + "=" * 88)
    print("PART 4: JOINT FIXED-LOCUS SYSTEM IS INCONSISTENT")
    print("=" * 88)

    ops = {
        "C3": C3,
        "C3^2": C3 @ C3,
        "TX": TX,
        "TY": TY,
        "TZ": TZ,
    }
    big_A = []
    big_b = []
    for _name, S in ops.items():
        A, b = induced_map(S)
        big_A.append(np.eye(3) - A)
        big_b.append(b)
    big_A = np.vstack(big_A)
    big_b = np.concatenate(big_b)

    rank_A = np.linalg.matrix_rank(big_A, tol=1e-9)
    rank_aug = np.linalg.matrix_rank(
        np.column_stack([big_A, big_b]), tol=1e-9
    )

    check(
        "Joint chart-fixed-locus matrix has rank 3 (columns)",
        rank_A == 3,
        f"rank(A) = {rank_A}",
    )
    check(
        "Joint chart-fixed-locus augmented matrix has rank 4 (inconsistent)",
        rank_aug == 4,
        f"rank([A|b]) = {rank_aug}",
    )
    check(
        "Therefore NO joint chart fixed point exists under <C3, TX, TY, TZ>",
        rank_aug > rank_A,
        "over-determined inconsistent system",
    )


