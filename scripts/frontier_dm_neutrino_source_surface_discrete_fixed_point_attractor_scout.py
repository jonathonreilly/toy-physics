# Part 5: Z_3 averaging (block-spin surrogate) does not close on the chart
# ---------------------------------------------------------------------------


def part5_z3_averaging() -> None:
    print("\n" + "=" * 88)
    print("PART 5: Z_3 AVERAGING (BLOCK-SPIN SURROGATE) DOES NOT CLOSE ON CHART")
    print("=" * 88)

    def z3_avg(M: np.ndarray) -> np.ndarray:
        return (
            M + C3 @ M @ C3_INV + C3 @ C3 @ M @ C3_INV @ C3_INV
        ) / 3.0

    # Test on a range of chart points.
    residuals = []
    deltas_out = []
    for (m, d, q) in [
        (0.0, 0.0, 0.0),
        (0.5, 0.3, 0.2),
        (-0.3, 0.8, 0.5),
        (1.0, -0.4, 0.9),
        (TARGET_DELTA, TARGET_DELTA, TARGET_Q),
    ]:
        H = H_BASE + m * T_M + d * T_D + q * T_Q
        H_avg = z3_avg(H)
        coeffs = chart_proj(H_avg)
        recon = H_BASE + coeffs[0] * T_M + coeffs[1] * T_D + coeffs[2] * T_Q
        resid = np.linalg.norm(H_avg - recon)
        residuals.append(resid)
        deltas_out.append(coeffs[1])

    check(
        "Z_3-average of a chart H does NOT live in the chart (off-chart residual > 0)",
        all(r > 1e-3 for r in residuals),
        f"residuals = {[round(r, 4) for r in residuals]}",
    )

    # The projected δ component is a constant 4 sqrt(6) / 9, independent of input.
    expected_delta = 4.0 * math.sqrt(6.0) / 9.0
    check(
        "Z_3-averaging projects δ → 4 sqrt(6)/9 (a constant, NOT the Koide pin sqrt(6)/3)",
        all(abs(d - expected_delta) < 1e-9 for d in deltas_out),
        f"mean projected δ = {np.mean(deltas_out):.6f}, 4√6/9 = {expected_delta:.6f}",
    )
    check(
        "Z_3-averaging constant projected δ DIFFERS from Koide pin δ = sqrt(6)/3",
        abs(expected_delta - TARGET_DELTA) > 1e-3,
        f"|4√6/9 - √6/3| = {abs(expected_delta - TARGET_DELTA):.6f}",
    )


