    check(
        "C3*TZ is strictly contracting (all eigenvalues of A have |λ| < 1)",
        maxe < 1.0 - 1e-9,
        f"max |eig(A)| = {maxe:.6f}",
    )
    check(
        "C3*TZ has fixed point (m,δ,q_+) = (2(√2+√6)/9, 2(√2+√6)/9, (√2+√6)/9)",
        abs(fp[0] - exact_m) < 1e-9
        and abs(fp[1] - exact_d) < 1e-9
        and abs(fp[2] - exact_q) < 1e-9,
        f"fp numerical = {tuple(round(x, 6) for x in fp)}",
    )
    check(
        "C3*TZ fixed point has δ = m = 2 q_+ (NOT δ = q_+)",
        abs(fp[1] - fp[0]) < 1e-9 and abs(fp[1] - 2.0 * fp[2]) < 1e-9,
        "δ/q_+ = 2",
    )
    check(
        "C3*TZ fixed point MISSES the Koide pin (δ=q_+=sqrt(6)/3)",
        abs(fp[1] - TARGET_DELTA) > 1e-3,
        f"|δ_fp - sqrt(6)/3| = {abs(fp[1]-TARGET_DELTA):.6f}",
    )

    # Verify iteration converges:
    v = np.array([0.0, 0.0, 0.0])
    for _ in range(200):
        v = A @ v + b
    check(
        "Iteration from (0,0,0) converges to the C3*TZ attractor",
        np.linalg.norm(v - fp) < 1e-9,
        f"||v_inf - fp|| = {np.linalg.norm(v-fp):.2e}",
    )


