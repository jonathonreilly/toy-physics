    new_err = np.linalg.norm(new_part - (a * jx + b * jy))

    check("J_z is Frobenius-orthogonal to J_x", ortho_xz < 1e-12,
          f"<J_x,J_z>={ortho_xz:.2e}")
    check("J_z is Frobenius-orthogonal to J_y", ortho_yz < 1e-12,
          f"<J_y,J_z>={ortho_yz:.2e}")
    check("J_z remains the exact charge-zero normal direction", charge_z < 1e-12,
          f"charge error={charge_z:.2e}")
    check("The transverse pairing seed carries charge -2/+2 through J_-/J_+", charge_ann < 1e-12 and charge_cre < 1e-12,
          f"ann error={charge_ann:.2e}, cre error={charge_cre:.2e}")
