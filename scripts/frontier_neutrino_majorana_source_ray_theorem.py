    u = unitary_from_generator(jz, theta)

    rot_x = u @ jx @ u.conj().T
    rot_y = u @ jy @ u.conj().T

    coeffs_x = frob_coeffs(rot_x, [jx, jy, jz])
    coeffs_y = frob_coeffs(rot_y, [jx, jy, jz])

    target_x = np.array([math.cos(theta), math.sin(theta), 0.0], dtype=complex)
    target_y = np.array([-math.sin(theta), math.cos(theta), 0.0], dtype=complex)
    err_x = np.linalg.norm(coeffs_x - target_x)
    err_y = np.linalg.norm(coeffs_y - target_y)

    check("Rephasing about J_z rotates J_x inside the transverse plane", err_x < 1e-12,
          f"rotation error={err_x:.2e}")
