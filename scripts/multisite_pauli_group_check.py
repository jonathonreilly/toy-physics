"""Multi-site Pauli group P_N on N-site Cl(3) Fock space has order 4^{N+1}.

By axiom_first_cl3_per_site_uniqueness (retained), each per-site Hilbert is
Pauli C². The multi-site Pauli group P_N is generated by the per-site σ_i,x
acting at each site x ∈ {1, ..., N}:

    P_N  =  ⟨σ_{i, x} : i ∈ {1, 2, 3}, x ∈ {1, ..., N}⟩

By the per-site Pauli group result (R8 Block 03), each site contributes a
factor of P_1 / Z(P_1) ≅ Z_2 × Z_2 (4 cosets), and the central scalar
phases ±1, ±i are global (4 phase elements).

Total order:  |P_N|  =  4 · 4^N  =  4^{N+1}.

(4 from the central Z_4 scalar phase ⊗ 4^N from per-site (Z_2 × Z_2)
matrix choices.)

For N = 1: |P_1| = 4 · 4 = 16 ✓ (matches R8 Block 03)
For N = 2: |P_2| = 4 · 16 = 64
For N = 3: |P_3| = 4 · 64 = 256

Tests:
  (T1) For N = 1, group has order 16 (recover R8 Block 03)
  (T2) For N = 2, group has order 64
  (T3) For N = 3, group has order 256
  (T4) Order formula |P_N| = 4^{N+1} matches numerical enumeration
  (T5) Center Z(P_N) = {±I_total, ±i I_total} of order 4 (scalar phases)
  (T6) Quotient P_N / Z(P_N) ≅ (Z_2 × Z_2)^N has order 4^N
"""
from __future__ import annotations

from itertools import product as iproduct

import numpy as np


def kron_chain(matrices: list[np.ndarray]) -> np.ndarray:
    result = matrices[0]
    for M in matrices[1:]:
        result = np.kron(result, M)
    return result


def at_site(local_op: np.ndarray, x: int, N: int) -> np.ndarray:
    I2 = np.eye(2, dtype=complex)
    factors = [local_op if i == x else I2 for i in range(N)]
    return kron_chain(factors)


def matrix_in_set(M: np.ndarray, set_: list[np.ndarray]) -> bool:
    for E in set_:
        if np.linalg.norm(M - E) < 1e-10:
            return True
    return False


def generate_group(generators: list[np.ndarray], dim: int, max_iter: int = 100) -> int:
    """BFS-generate the multiplicative group, return its order."""
    Iden = np.eye(dim, dtype=complex)
    elements = [Iden.copy()]
    new = True
    iter_count = 0
    while new and iter_count < max_iter:
        new = False
        for E in elements[:]:
            for g in generators:
                product = E @ g
                if not matrix_in_set(product, elements):
                    elements.append(product)
                    new = True
        iter_count += 1
    return len(elements), elements


def main() -> None:
    print("=" * 72)
    print("MULTI-SITE PAULI GROUP P_N HAS ORDER 4^{N+1}")
    print("=" * 72)
    print()

    s1 = np.array([[0, 1], [1, 0]], dtype=complex)
    s2 = np.array([[0, -1j], [1j, 0]], dtype=complex)
    s3 = np.array([[1, 0], [0, -1]], dtype=complex)

    results = {}

    for N in [1, 2, 3]:
        dim = 2 ** N
        print(f"  Generating P_{N} on {N}-site Hilbert (dim = {dim})...")
        # Generators: σ_{i, x} for i ∈ {1, 2, 3}, x ∈ {0, ..., N-1}
        generators = []
        for x in range(N):
            for sigma in [s1, s2, s3]:
                generators.append(at_site(sigma, x, N))
        order, _ = generate_group(generators, dim)
        results[N] = order
        expected = 4 ** (N + 1)
        print(f"    P_{N}: observed order = {order}, expected 4^{N+1} = {expected}")
        print()

    # ----- Test 1: N=1 (recover R8 Block 03) -----
    print("-" * 72)
    print("TEST 1: |P_1| = 16  (recovers R8 Block 03 for N = 1)")
    print("-" * 72)
    print(f"  observed: {results[1]}, expected: 16")
    t1_ok = results[1] == 16
    print(f"  STATUS: {'PASS' if t1_ok else 'FAIL'}")
    print()

    # ----- Test 2: N=2 -----
    print("-" * 72)
    print("TEST 2: |P_2| = 64")
    print("-" * 72)
    print(f"  observed: {results[2]}, expected: 64")
    t2_ok = results[2] == 64
    print(f"  STATUS: {'PASS' if t2_ok else 'FAIL'}")
    print()

    # ----- Test 3: N=3 -----
    print("-" * 72)
    print("TEST 3: |P_3| = 256")
    print("-" * 72)
    print(f"  observed: {results[3]}, expected: 256")
    t3_ok = results[3] == 256
    print(f"  STATUS: {'PASS' if t3_ok else 'FAIL'}")
    print()

    # ----- Test 4: General formula |P_N| = 4^{N+1} -----
    print("-" * 72)
    print("TEST 4: Formula |P_N| = 4^{N+1} matches all observed orders")
    print("-" * 72)
    all_match = all(results[N] == 4 ** (N + 1) for N in [1, 2, 3])
    print(f"  N = 1: {results[1]} = 4^2 = {4**2}")
    print(f"  N = 2: {results[2]} = 4^3 = {4**3}")
    print(f"  N = 3: {results[3]} = 4^4 = {4**4}")
    t4_ok = all_match
    print(f"  STATUS: {'PASS' if t4_ok else 'FAIL'}")
    print()

    # ----- Test 5: Center for N = 2 -----
    print("-" * 72)
    print("TEST 5: Center Z(P_2) = {±I, ±iI} of order 4 (scalar phases only)")
    print("-" * 72)
    N = 2
    dim = 4
    generators = []
    for x in range(N):
        for sigma in [s1, s2, s3]:
            generators.append(at_site(sigma, x, N))
    _, elements = generate_group(generators, dim)
    Iden = np.eye(dim, dtype=complex)
    center = []
    for E in elements:
        is_central = all(np.linalg.norm(E @ g - g @ E) < 1e-10 for g in generators)
        if is_central:
            center.append(E)
    print(f"  |Z(P_2)| = {len(center)}")
    expected_center = [Iden, -Iden, 1j * Iden, -1j * Iden]
    center_match = all(matrix_in_set(C, expected_center) for C in center)
    print(f"  Center = {{±I, ±iI}}: {center_match}")
    t5_ok = len(center) == 4 and center_match
    print(f"  STATUS: {'PASS' if t5_ok else 'FAIL'}")
    print()

    # ----- Test 6: Quotient P_N / Z(P_N) has order 4^N -----
    print("-" * 72)
    print("TEST 6: P_N / Z(P_N) has order 4^N (the (Z_2 × Z_2)^N coset structure)")
    print("-" * 72)
    quotient_orders = {N: results[N] // 4 for N in [1, 2, 3]}
    expected_quotient = {N: 4 ** N for N in [1, 2, 3]}
    print(f"  N = 1: |P_1 / Z| = {quotient_orders[1]} = 4^1 = {expected_quotient[1]}")
    print(f"  N = 2: |P_2 / Z| = {quotient_orders[2]} = 4^2 = {expected_quotient[2]}")
    print(f"  N = 3: |P_3 / Z| = {quotient_orders[3]} = 4^3 = {expected_quotient[3]}")
    t6_ok = all(quotient_orders[N] == expected_quotient[N] for N in [1, 2, 3])
    print(f"  STATUS: {'PASS' if t6_ok else 'FAIL'}")
    print()

    print("=" * 72)
    print(f"  Test 1 (|P_1| = 16):                              {'PASS' if t1_ok else 'FAIL'}")
    print(f"  Test 2 (|P_2| = 64):                              {'PASS' if t2_ok else 'FAIL'}")
    print(f"  Test 3 (|P_3| = 256):                             {'PASS' if t3_ok else 'FAIL'}")
    print(f"  Test 4 (formula |P_N| = 4^{{N+1}}):                {'PASS' if t4_ok else 'FAIL'}")
    print(f"  Test 5 (Z(P_N) = scalar phases, order 4):         {'PASS' if t5_ok else 'FAIL'}")
    print(f"  Test 6 (quotient order = 4^N):                    {'PASS' if t6_ok else 'FAIL'}")
    all_ok = all([t1_ok, t2_ok, t3_ok, t4_ok, t5_ok, t6_ok])
    print(f"  OVERALL: {'PASS' if all_ok else 'FAIL'}")
    if not all_ok:
        raise SystemExit(1)


if __name__ == "__main__":
    main()
