section("Part 4: no proper subspace is D_3- and C_3-invariant")
# ============================================================================
# D_3-invariant subspaces of C^3 are direct sums of {C·e_1, C·e_2, C·e_3}
# (since each P_i is rank-1 projecting onto e_i, the only D_3-invariant
# subspaces are spans of subsets of {e_1, e_2, e_3}).
# So D_3-invariant subspaces are: {0}, span(e_1), span(e_2), span(e_3),
# span(e_1, e_2), span(e_1, e_3), span(e_2, e_3), C^3.
# That's 8 = 2^3 subspaces.

# For each NON-trivial proper subspace, check if it's also C_3-invariant.
# C_3 cycles e_1 -> e_2 -> e_3 -> e_1.
# So C_3-orbits of {e_1, e_2, e_3} are the full set or empty.
# A subset S ⊂ {1,2,3} is C_3-invariant iff S is the full orbit, i.e. S ∈ {∅, {1,2,3}}.

# Enumerate all subsets of {1,2,3} and check both invariances:
subsets = [
    frozenset(),
    frozenset({0}),
    frozenset({1}),
    frozenset({2}),
    frozenset({0, 1}),
    frozenset({0, 2}),
    frozenset({1, 2}),
    frozenset({0, 1, 2}),
]
def cycle(S):
    return frozenset((i + 1) % 3 for i in S)

D3_C3_invariant = []
for S in subsets:
    # Always D_3-invariant by construction
    # Check C_3-invariant
    if cycle(S) == S:
        D3_C3_invariant.append(S)

check("the only D_3- AND C_3-invariant subspaces are {0} and ℂ³",
      D3_C3_invariant == [frozenset(), frozenset({0, 1, 2})],
      detail=f"observed: {[sorted(S) for S in D3_C3_invariant]}")
check("therefore no PROPER subspace is invariant under both D_3 and C_3",
      len(D3_C3_invariant) == 2 and (frozenset() in D3_C3_invariant) and (frozenset({0,1,2}) in D3_C3_invariant))


