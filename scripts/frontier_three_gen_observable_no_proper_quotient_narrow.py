section("Part 2: diagonal projectors + cyclic permutation generate M_3(C)")
# ============================================================================
# Build P_X1, P_X2, P_X3, C_3 as 3x3 matrices over Q
P = [zeros(3, 3) for _ in range(3)]
for i in range(3):
    P[i][i, i] = Rational(1)

# Mutually orthogonal projectors summing to I_3
check("P_X_i are projectors (P_i² = P_i)",
      all(P[i] * P[i] == P[i] for i in range(3)))
check("P_X_i are mutually orthogonal (P_i P_j = 0 for i ≠ j)",
      all(P[i] * P[j] == zeros(3, 3) for i in range(3) for j in range(3) if i != j))
check("Σ P_X_i = I_3",
      sum(P, zeros(3, 3)) == eye(3))

# C_3 cyclic permutation: X_1 -> X_2 -> X_3 -> X_1
C3 = Matrix([[0, 0, 1],
             [1, 0, 0],
             [0, 1, 0]])
check("C₃³ = I_3", C3**3 == eye(3))
# C_3 is unitary: C_3 C_3^T = I (since it's a real permutation matrix)
check("C₃ is orthogonal (C₃ C₃^T = I)", C3 * C3.T == eye(3))


# ============================================================================
section("Part 3: D_3 + C_3 generates M_3(ℂ)")
# ============================================================================
# To show generation: produce all 9 matrix units E_ij from {P_X_i, C_3}.
# Diagonal: E_ii = P_X_i  ✓
# Off-diagonal: C_3 = E_21 + E_32 + E_13 (one cyclic step)
#                  = sum of off-diagonal entries
# We can isolate individual off-diagonal entries by:
#   E_21 = P_X_2 · C_3 · P_X_1
#   E_32 = P_X_3 · C_3 · P_X_2
#   E_13 = P_X_1 · C_3 · P_X_3
# And inverse:
#   E_12 = (E_21)^T = P_X_1 · C_3^T · P_X_2 = P_X_1 · C_3^2 · P_X_2

E_units = {}
for i in range(3):
    for j in range(3):
        E_units[(i, j)] = P[i] * (C3 if (j == (i-1) % 3) else (C3**2 if (j == (i+1) % 3) else (eye(3) if i == j else zeros(3, 3)))) * P[j]

# Better: use {P_i C_3^k P_j} for k = 0, 1, 2
for i in range(3):
    for j in range(3):
        if i == j:
            target = zeros(3, 3)
            target[i, i] = 1
            E_units[(i, j)] = P[i]  # diagonal
        else:
            # E_ij = P_i C_3^k P_j where C_3^k sends j -> i
            # C_3 sends j -> j+1, so to send j -> i we need k = i - j mod 3
            k = (i - j) % 3
            E_units[(i, j)] = P[i] * (C3**k) * P[j]

# Verify each E_ij is the standard matrix unit
for (i, j), E in E_units.items():
    expected = zeros(3, 3)
    expected[i, j] = 1
    ok = (E == expected)
    if not ok:
        print(f"  E_{i}{j} mismatch: got {E}, expected {expected}")
check("all 9 matrix units E_ij are generated by {P_X_i} ∪ {C_3, C_3², I}",
      all(E_units[(i, j)][i, j] == 1 and
          all(E_units[(i, j)][a, b] == 0 for a in range(3) for b in range(3) if (a, b) != (i, j))
          for i in range(3) for j in range(3)))

check("therefore <{P_X_i, C_3}> generates the full M_3(ℂ)",
      True,  # follows from the matrix-unit generation above
      detail="9 matrix units span M_3(ℂ) as a vector space")


