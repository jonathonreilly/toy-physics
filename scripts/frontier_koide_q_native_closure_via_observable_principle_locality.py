check(
    "T5a' identity 1 + omega + omega^2 = 0 (cube-root-of-unity sum)",
    sum_one_omega_omega2 == 0,
    detail=f"1 + omega + omega^2 = {sum_one_omega_omega2}",
)
# Brute-force a_0 with explicit omega values.
lam_concrete = [
    a_sym + b_sym * (omega_val ** k) + sp.conjugate(b_sym) * (omega2_val ** k)
    for k in range(3)
]
a0_concrete = sp.simplify(sp.expand(sum(lam_concrete) / sp.sqrt(3)))
check(
    "T5a'' brute-force Fourier coefficient a_0 = sqrt(3) a after omega substitution",
    sp.simplify(a0_concrete - sp.sqrt(3) * a_sym) == 0,
    detail=f"a_0 (concrete) = {a0_concrete}",
)

# Equivalent form: Q = 2/3 ⇔ a_0^2 = 2|z|^2 ⇔ a^2 = 2|b|^2 ⇔ κ = 2.
# At a^2 = 2|b|^2, κ = a^2/|b|^2 = 2.
