# T5 — Spectrum-operator bridge identity: a_0^2 - 2|z|^2 = 3(a^2 - 2|b|^2).
# Hand-derive eigenvalues using 1 + omega + omega^2 = 0 to keep the result
# in clean algebraic form, then verify the bridge identity symbolically.
#
# H = a I + b C + bbar C^2 has eigenvalues lambda_k = a + b omega^k + bbar omega^{-k}.
# a_0 := (lambda_0 + lambda_1 + lambda_2) / sqrt(3) = sqrt(3) a
#   (b and bbar contributions vanish by 1 + omega + omega^2 = 0)
# z   := (lambda_0 + omegabar lambda_1 + omega lambda_2) / sqrt(3) = sqrt(3) b
# Hence a_0^2 = 3 a^2, |z|^2 = 3 |b|^2, and the identity follows.

# Direct symbolic check using the closed forms a_0 = sqrt(3) a, z = sqrt(3) b.
a0_closed = sp.sqrt(3) * a_sym
z_closed = sp.sqrt(3) * b_sym
abs_z_sq = sp.simplify(z_closed * sp.conjugate(z_closed))
abs_b_sq = b_re ** 2 + b_im ** 2

LHS = sp.simplify(a0_closed ** 2 - 2 * abs_z_sq)
RHS = sp.simplify(3 * (a_sym ** 2 - 2 * abs_b_sq))
bridge_residual = sp.simplify(LHS - RHS)
check(
    "T5a spectrum-operator bridge identity a_0^2 - 2|z|^2 = 3(a^2 - 2|b|^2) symbolic residual = 0",
    bridge_residual == 0,
    detail=f"residual = {bridge_residual}",
)

# Confirm the closed-form Fourier coefficients agree with the brute-force
# eigenvalue sum after enforcing 1 + omega + omega^2 = 0 explicitly.
omega_val = sp.Rational(-1, 2) + sp.I * sp.sqrt(3) / 2
omega2_val = sp.Rational(-1, 2) - sp.I * sp.sqrt(3) / 2  # complex conjugate of omega
# Verify 1 + omega + omega^2 = 0 explicitly.
