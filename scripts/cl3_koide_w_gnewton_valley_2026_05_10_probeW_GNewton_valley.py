def neg_lap_apply(f, L):
    """Apply -Delta_lat to f : (L,L,L) -> (L,L,L) with periodic BC.

    -Delta_lat f(x) = 6 f(x) - sum_{|y-x|=1} f(y)
    """
    import numpy as np

    out = 6.0 * f
    for axis in range(3):
        out -= np.roll(f, +1, axis=axis)
        out -= np.roll(f, -1, axis=axis)
    return out


def solve_poisson_periodic(rho, L):
    """Solve -Delta_lat V = rho on periodic L^3 lattice using FFT.

    Sets the zero-mean (DC) component of V to zero (since the constant
    mode is degenerate for a pure Laplacian).
    """
    import numpy as np

    rho_k = np.fft.fftn(rho)
    k_vals = 2 * np.pi * np.arange(L) / L
    K1, K2, K3 = np.meshgrid(k_vals, k_vals, k_vals, indexing="ij")
    eigval = 2 * (3 - np.cos(K1) - np.cos(K2) - np.cos(K3))
    # k=0 mode is zero eigenvalue; handle carefully
    eigval_safe = eigval.copy()
    eigval_safe[0, 0, 0] = 1.0
    V_k = rho_k / eigval_safe
    V_k[0, 0, 0] = 0.0  # set DC mode to zero
    return np.fft.ifftn(V_k).real


