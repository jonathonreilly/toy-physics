        # Normalize to ensure exact unitarity
        V /= np.sqrt(np.abs(np.linalg.det(V)))

        # Rotated generators
        e1_rot = V @ sigma_x @ V.conj().T
        e2_rot = V @ sigma_y @ V.conj().T
        e3_rot = V @ sigma_z @ V.conj().T

