        for i, ei in enumerate([e1_rot, e2_rot, e3_rot]):
            for j, ej in enumerate([e1_rot, e2_rot, e3_rot]):
                ac = ei @ ej + ej @ ei
                expected = 2.0 * (1 if i == j else 0) * I2
                err = np.max(np.abs(ac - expected))
                max_err = max(max_err, err)
                if err > 1e-10:
                    all_equivalent = False

